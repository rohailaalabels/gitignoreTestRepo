<h3 >Applications and use </h3>
                          <p>Applications are predominately in market segments  where rigid containers are used (e.g. glass, polyester/PET). Due to the fairly  rigid nature of the film, care should be taken with application and use on  non-uniform surfaces and/or where a very high level of flexibility or  squeezability is required (e.g. soap and shower gel containers). A particularly  good choice for primary brand and product labels on rigid containers where it  is important for the content to be visible and not partially obscured by a  label. Also secondary promotional labels, informational labels, external  labels/signage and safety labels/signage etc.</p>
                          <p>
                          With the British standard (BS) compliance for the  marine performance test this material is widely used for haz-chem labels on  drums and containers.</p>
<h3>Conversion &amp; printing</h3>
                          <p>This product  is specifically designed for hot fuse sheet-fed and continuous (colour) laser  printing. Providing optimal flatness during conversion, storage and laser  printing processing. Some slight curl may be seen on exiting the printer but  this will flatten quickly.</p>
<h3>Approvals</h3>
                          <p>The adhesive  complies with the European food directives and legislations, FDA 175.105 and  the German recommendations XIV as published by BfR (Bundesinstitut  fur Riskobewertung) the German Federal Institute for risk assessment. The adhesive can be  used in direct contact with dry and moist, non-fatty foodstuffs.</p>
                          <p>This product  complies with the materials section of BS5609 Part 2 (Marine Performance Test).  Customers should independently determine the compliance or otherwise of the ink  and print process involved in the production of labels to the British standard.</p>
<h3 >Shelf Life</h3>
                          <p>One year under storage conditions as  defined by FINAT (Federation Internationale des Fabricants et Transformateurs d&rsquo; Adhesifs  et Thermocllants sur papiers et autres supports) 20° - 25° C and 40 – 50% RH.</p>
