                        <h2 class="BlueHeading">IS PRINTER INK BIODEGRADABLE?</h2>
                        <p class="text-justify">
                            The amount of ink deposited by inkjet printers onto labels is relatively minimal, by
                            comparison with the residual in empty cartridges requiring disposal. The droplets of ink
                            used to create print are extremely small (usually between 50 – 60 microns in diameter), so
                            small that they are less than the diameter of a human hair (80 microns). The amount of ink
                            dispensed by the print heads is measured in Picolitres (1 Trillionth of a litre) enabling a
                            very precise positioning of ink dots and high-resolution images from relatively small
                            amounts of ink.
                        </p>
                    </div>


                    <div class="col-xs-12 col-sm-4 col-md-4 text-center">
                        <img onerror="imgError(this);" class="img-responsive"
                             src="<?= Assets ?>images/header/printer-label.png">
