      <h3 >Applications and use </h3>
      <p>This product has been specifically designed for the logistics/transportation industry labelling applications when the batch/shipment need to be removed and can now be washed off quickly without leaving residue, but while in transit remain firmly adhered, even in freezer temperature conditions. The entire label comprising paper face-stock and adhesive dissolves in a very short time in cold water without any additional washing process. Delivering significant labour savings in the time taken to remove and re-label consignment cages and trays etc. The labels also have many other applications for the storage of frozen food, goods and the labelling of glass containers.</p>
      
       <h3 >Additional Information</h3>
       <p><strong>Advantages:</strong></p>
       <p>After the washing process there are no residues on the surface. Absolutely clean substrate and labels dissolve in a very short time without any mechanical agitation. No additional washing process is needed as the substrate is clean after the first process.</p>
       <p><strong>Limitations:</strong></p>
       <p>Adhesion will depend on the labelling surface quality e.g. dusty, moist, rough, treated etc. Therefore it is highly recommended to order a material sample and undertake adhesion trials in real application conditions.</p>
       
      <h3 >Approvals</h3>
