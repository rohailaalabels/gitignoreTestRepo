      <h3 >Applications and use </h3>
      <p>Very popular for information labelling where high  visibility, durable labels are required making these labels ideal for  tracking/warehouse applications and also for product/component identification  and instructional labels in industry. Recommended for drum and logistics  industry labelling.</p>
      <p>Ideal  for A4 and cut-size applications, with good removability, UV stability  (non-yellowing) and water resistance once labelled. Limited adhesion on very  rough surfaces and not recommended for labelling PVC substrates. </p>
      <h3>Conversion &amp; printing</h3>
      <p>Due to the pre-treatment, the film is printable by  common label printing methods like letterpress, flexography, screen and offset  as well as copier and laser toner printers, but in all cases inks designed for  non-absorbent materials should be used.</p>
      <h3>Approvals</h3>
      <p>The research centre ISEGA (EN ISO 17025  accreditation for analytics and EN 45011 for certification) has given the  adhesive and approval according to the European requirements for direct contact  with dry and moist, non-fatty foodstuffs and according to FDA regulations  adhesive may come into direct contact with dry foodstuffs, or it must be  separated from the foodstuffs by a functional barrier. Furthermore the adhesive  may come into contact with poultry, dry foodstuffs as well as fruit and  vegetables.<br>
