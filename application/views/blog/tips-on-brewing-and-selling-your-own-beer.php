      <h2 class="headingPost">Record keeping and research</h2>
      <p>Research really is the absolute most important aspect to home brewing. You will find literally
        thousands of guides online that will walk you through every step of the way, and there are
        plenty of forums where you can discuss the hobby with other enthusiasts, who will be more than
        happy to share their experiences and advice.</p>
    </div>
  </div>
  <div class="clear20"></div>
  <div class="row">
    <div class="col-xs-6">
      <p>Adequate research can help to save money in the long run as well, as you can seek advice on the
        most cost effective equipment, as well ways to save with bulk buying the necessary raw
        ingredients.</p>
    </div>
    <div class="col-xs-6">
      <p>After you’ve found a guide to help you through the ins and outs of beer making, you’re going to
        want to keep a brewing log. No matter how simple the recipe, you need to keep track of exactly
        what went in for a number of reasons.</p>
    </div>
  </div>
  <div class="row">
    <div class="col-xs-6">
      <p>Firstly, it gives you the opportunity to tweak your recipes to make them perfect (it’s said that
        even if your first batch is decent, it will take until after your tenth to really get it right),
        you can make this easy on yourself by labelling beers, so you have a clear reference in your
        brewing log.</p>
    </div>
    <div class="col-xs-6">
      <p>And secondly, keeping track of everything in a brewing log will be invaluable should you want to
        take the step up from brewing for yourself to eventually selling your beer.</p>
    </div>
  </div>
  <h2 class="headingPost">Bottling up</h2>
  <div class="row">
    <div class="col-xs-6 col-md-4 col-lg-3 col-sm-5"><img src="<?=BLOG_ASSETS?>b-4.jpg"
                                           class="img-responsive col-xs-12">
