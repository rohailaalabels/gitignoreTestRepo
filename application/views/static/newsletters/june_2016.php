                        <td align="center" style="font-family: Arial; color:#fd4913; font-size:12px;">AA Labels new "Dry Edge" trimmed label sheets. </td>
                      </tr>
                    </table></td>
                  <td valign="top" style="font-family: Arial; color:#666; font-size:12px; padding-left:10px; padding-top:10px; line-height: 18px;">The problems caused by the industry term "wet edge" on sheets of labels when printing are notoriously difficult to rectify. Because the "wet edge" refers to the label adhesive leaching out on the edges of label sheets, inevitably caused by pressure (whether the material coil has been wound too tightly following manufacture, or during material conversion to sheets of labels, or the sheets being stacked too high) and/or temperature, the outcome when trying to print the labels, is that the glue becomes transferred to the working parts of the printer and causes problems. </td>
                </tr>
              </tbody>
            </table></td>
        </tr>
        <tr>
          <td style="font-family: Arial; color:#666; font-size:12px; padding-bottom:10px; line-height: 18px;">To remedy this we have introduced our new "Dry Edge Sheets" in all materials on A4 sheets, as standard. Although relatively new to the UK, edge trimming of sheets has been a standard for label manufacture in Europe for some while now and significantly improves the print performance and on full sheets of A4 assists the label peel from the release liner. </td>
        </tr>
