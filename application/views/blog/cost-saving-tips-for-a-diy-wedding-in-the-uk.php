          <h2 class="headingPost">A few money-saving tips</h2>
          <p>Avoid relying on dedicated wedding suppliers for goods and services – they tend to know that they
            have a captive market, and as such they will push prices way up. At the end of the day there’s a
            lot that you can do yourself for half the cost.</p>
        </div>
      </div>
      <div class="clear10"></div>
      <p>There’s plenty you can do to DIY your wedding day, but it’s also important to be realistic about what you
        can and can’t achieve. However good it feels to do things yourself, the time spent sourcing everything
        and making things will soon add up.</p>
      <p>A good idea to begin with is to trim down your guest list; this can help you save money on absolutely
        everything. If you’re just catering for your nearest and dearest, you’ll also have a much more intimate
        occasion, and one where you’ll actually have time to talk to as well as thank everyone for being
        there.</p>
      <p>Try and keep your eyes peeled for some value for money solutions to your wedding favours. You can buy
        little boxes in bulk on popular web auction sites, and <a href="#">bespoke labels can be bought
        separately</a> to add a personal touch. What you put in them is up to you, but it could be something
        as simple as your favourite chocolate or teabag.</p>
      <div class="row">
        <div class="col-xs-6 col-lg-4 col-sm-4"><img class="img-responsive col-xs-12"
