  <p>I don’t even remember seeing any TV ads about prostate cancer before, though I’m sure there have been.
    Like many other men, I’d probably prefer not to be reminded of my own mortality but on this occasion I
    took note. That brings me to the third trigger for this post.</p>
  <div class="clear"></div>
  <p>What time is it? In this hour, one man will have died from prostate cancer. And the hour after that. And
    it goes on. One man dies of this dreadful disease every hour of every day. This fact made me shudder a
    bit. I also felt bad that I didn’t know it was Prostate Cancer Awareness Month in March. Did you know?
    So, I know I’m late but I wanted to do something – even if it was only something small. I had an
    idea.</p>
