  <p style="text-align: center;"><strong><em>&nbsp;</em></strong></p>
  <ul>
    <li>Depending on the product&nbsp;being exported, you may also need specific information to comply with
      legislation in your chosen country – for example, on an aerosol can, you&nbsp;must have regulatory
      information (text such as “keep out of reach of&nbsp;children”) and a flammable symbol of at least
      10mm in width and height as&nbsp;the product is flammable. So you will need to consult your
      manufacturer or factory to find out whether there are any additional requirements or ask locally
      with a trade advisor in the country in question. </li>
    <li>Translations are important if your product does not clearly specify what it is/what it does, or if
      there is specific information such as instructions for use that you wish&nbsp;to convey to the user. </li>
  </ul>
  <p>So, this covers what you might need on your product, but have you decided how you are going to get these
    on to it?</p>
  <p>Most products have some kind of label, and one of the great things about labels is they are extremely
    versatile. Does your company have the funds and quantities available, as well as the resource and
    capability to produce a number of different labels for your different markets? Do you instead intend to
    create one label that is ‘fit for all’?</p>
  <p>If you haven’t thought of using labels on your product this may be a consideration which could save you
    money and time in the long run.&nbsp; Labels can hold a variety of information and one of their
    attractions is the ability to add or remove information fairly quickly at any one time – had 1000 labels
    printed and need to make a change? No problem – it doesn’t take long or cost much to get some more
    reprinted.</p>
  <p>Exporting can be a scary thought, and many small business worry about the scale of exporting and the huge
    costs that might be involved. Just remember – by achieving domestic success, you have already attained
    the background knowledge needed to break into foreign markets. Trust in your brand, do your research,
    and use what you know to make your successful UK business into a global one!</p>
