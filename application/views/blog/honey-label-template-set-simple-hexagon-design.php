  <h2>The honey label design</h2>
  <p>I wanted to create a label that had simplicity at it's heart but still had a strong honey theme. A shape
    would be simple to base the design on and there is a shape that could instantly be associated with bees.
    You've probably already guessed what that shape is by the title and mentions of it on this page! Of
    course it's a hexagon – associated with bees due to the shape of the cells in honeycomb.</p>
  <p>So this design uses a repeated hexagon shape across the main jar label and down the tamper label too.</p>
  <p>Using just one colour (black) means you can print the labels on any printer – no colour needed. Should
    you want to have some colour to your labels, you can change the text and use coloured labels.</p>
