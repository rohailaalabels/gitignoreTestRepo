    <p>Packaging, especially when dealing with consumer products, is one of the most important aspects of
      marketing. You only have to look down a supermarket aisle to realise that we are not only enticed by
      packaging, we are bombarded with it.</p>
    <p>The outer wrapper of a product shapes the strongest image of a brand in a buyer’s mind. That’s why brands
      are hesitant to make drastic changes to their packaging and why many have kept the same design for
      years. This includes label design and logos. Through their packaging, brands want to create a connection
      with the consumer so that they return to that brand, time and time again.</p>
    <p>Packaging needs to appeal to all of the senses but first it needs to attract our eyes. It has to make a
      psychological connection, too. We have to like the colours. The shape of the box or wrapper should be
      pleasing too. The text should be easy to read and informative. Not too much, not too little. Then we
      have to carry the product, transport it, use it, store it… then we discard it, throw it away…
      considering all these things, no wonder it is a challenge for manufacturers to get packaging right!
      Obviously, personal choice comes into the equation, and whilst some prefer bold prints and even a bit of
      bling, others like more simple and sophisticated designs.</p>
    <p>So where does the content that is inside the packaging come in? And if the wrapper or label is so
      enticing, does it really matter if what’s inside isn’t of the best quality? Here are my top three
      household examples of where packaging really sells:</p>
    <p><em><strong>1) Benefit cosmetics</strong> </em></p>
    <p>With quirky product names like ‘Bad gal lash’ and ‘Dr Feelgood’, Benefit takes risks with its packaging.
      It borders cheeky and sophisticated, and each product has a different story, making you feel a part of
      the Benefit ‘family’. Where make up is concerned, I live by the rule ‘you have to see it to believe it’.
      Unless a beauty product has been recommended to me, I have absolutely no idea if it is any good or not,
      and so in my eyes the packaging of beauty products is more important than ever. I think any good make up
      artist would say that different make up suits different skin. Just because it has good packaging, it
      doesn’t mean it’s a good product for you. But Benefit hits the nail on the head for quality with that
      little bit of fun, sure to entice you to open up that purse and make a purchase!</p>
