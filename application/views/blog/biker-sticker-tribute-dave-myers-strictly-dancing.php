  <p>We weren’t the only ones, with the dance video reaching over 57,000 views on Youtube (their most popular
    video). And with comments such as “Dave is what Strictly is all about”, and “Simply awesome Dave, what a
    legend” we think his departure from the show was nothing short of too early.</p>
  <p>But for us, it doesn’t end here – what better way to celebrate Dave’s time on Strictly Come Dancing by
    paying tribute to his performance in a way that he can be reminded, day in, day out while on his
    favoured mode of transport – the motorcycle?</p>
  <p>That’s exactly what we thought when we commissioned a caricature that we could print on to our own <a
                        href="http://localhost">sticky labels</a> for the top boxes of their motorbikes. The caricature
    shows Dave dragging his Biker friend, Si King, by the leg as seen in his ‘Cha Cha’ with dance partner
    Karen Hauer.</p>
