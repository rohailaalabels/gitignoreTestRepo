      <h3 >Applications and use </h3>
      <p> This face material is designed for the manufacture of labels on rolls and sheets for use in copier, inkjet, laser and thermal transfer printers (uncoated white only). The surface structure provides excellent toner bonding and print resolution. </p>
      <p> These labels are ideal for applications where the vivid colours are used to distinguish products and product groups e.g. file labels, inventory labels, pallet labels, racking labels etc. Also packaging and hazard warning labels. </p>
      <p> The well balanced laminate construction ensures optimum lay-flat during conversion and end use for a wide range of conditions. </p>
