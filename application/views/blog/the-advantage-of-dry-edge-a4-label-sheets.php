      <h2 class="headingPost">WHAT IS A DRY-EDGE ON LABEL SHEETS?</h2>
      <p>This is an A4 sheet of labels on which a 1mm wide strip of label face-stock and adhesive has been
        removed from around all 4 edges of the sheet, leaving only the dry backing material. The edge waste
        is removed during the production cutting of the labels and label sheets, for the purpose of
        preventing the production of label sheets with a wet-edge.</p>
      <h2 class="headingPost">WHAT IS A WET-EDGE ON LABEL SHEETS?</h2>
      <p><img src="<?=BLOG_ASSETS?>01.jpg" alt="01" width="272" height="192"
                        class="alignright size-full wp-image-1728">Wet-edge is a term used to describe the leaching of
        adhesive from between the label face-stock and the silicon release-liner and backing or label
        carrier.</p>
      <p>This is most commonly caused by pressure, although temperature can also be a contributory factor. For
        example, if the raw material coil has been wound too tightly by the manufacturer, then once
        converted into cut label sheets the plasticised adhesive layer that was under pressure and migrating
        toward the open edge, can still be leaching at the sheet edges and feel slightly “tacky” to the
        touch. </p>
      <p>Similarly, if label sheets are left in potentially high temperature locations such as window sills in
        direct sunlight or on top of a heat source like a radiator then this could also lead to a heating
        and plasticisation of the adhesive, contributing to a similar outcome.</p>
      <h2 class="headingPost">WHY IS THIS A PROBLEM</h2>
      <p>When plain label sheets are being printed on a desktop or larger free-standing laser printer, the
        sheets of labels will pass through several full-width rollers around the imaging drum in addition to
        transporter rollers elsewhere in the assembly, which may not all be full-width. </p>
