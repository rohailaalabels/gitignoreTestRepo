<h3 >Applications and use </h3>
                          <p>Because this material is so durable and strong it is  ideal as a block-out material for information on external signage that is no  longer applicable and covering holes on reusable gun targets etc. It also has  internal usage applications in distribution and fulfilment centres. Because of  the nature of the face stock material it is not possible to over print using  digital printers.</p>
<h3>Approvals</h3>
                          <p>Compliance with EN71/3 covering application on toys  and European Packaging Directive 94/62/EC covering restrictions on the usage of  heavy metals. This label can be used safely for food packaging and  can come into direct contact with both dry and fatty foodstuffs (ISEGA Research &amp;  Development Institute, Aschaffenburg). </p>
<h3 >Shelf Life</h3>
                          <p>One year under storage conditions as defined by FINAT (Federation Internationale  des Fabricants et Transformateurs d&rsquo; Adhesifs et Thermocllants sur papiers et  autres supports) 20° - 25° C and 40 – 50% RH.</p>
