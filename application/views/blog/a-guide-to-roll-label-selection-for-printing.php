    <h2 class="headingPost">THE PROTECTION OF LABEL ROLLS IN TRANSIT</h2>
    <p>Fulfilment of roll label orders always involves the secure packing and protection of the rolls to
      minimise movement in transit and the potential for abrasion to the surface of the labels. Which is
      important for all labels whether plain or printed, but particularly so when transporting labels with a
      Thermochromic coating for use with Direct Thermal printers as any contact risks resultant black marks on
      the labels surface from the heat generated in friction.</p>
    <p>Therefore, to mitigate against this outcome all labels on rolls are despatched for delivery with
      additional labels to the quantity ordered on the outside of the roll (one revolution). So that in the
      event that the outer labels incur scuff-marks these can simply be taken off and discarded.</p>
