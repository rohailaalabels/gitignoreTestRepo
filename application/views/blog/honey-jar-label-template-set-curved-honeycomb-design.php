  <p>Got honey? Need to create a label for your jars? The work has been done here for you. Simply download the
    desired templates, edit and print (sheets of labels for the templates are available too).</p>
  <p><strong>This free printable label template set is designed for those who sell honey.</strong> It follows
    on from our <a title="Honey Jar Label Template: “Ode to The Honey Bee”"
                               href="https://localhost/blog/print-templates/honey-jar-label-template-ode-honey-bee/">previous
    honey label template</a> which had less space for text and, even though editable, it was more suited
    to gifting honey than for selling it. For that reason close attention has been paid to UK honey
    labelling regulations for this template set (although you’ll need to check with your local trading
    standards if you want to be sure everything’s covered).</p>
  <p>Editable parts include:</p>
  <ul>
    <li>Heading (Should contain the word ‘Honey’)</li>
    <li>Weight</li>
    <li>Name and Address</li>
    <li>Country of Origin (e.g. ‘Produce of England’).</li>
    <li>Best Before Date</li>
    <li>Lot number</li>
  </ul>
  <p>As each section is fully editable, you don’t have to use them as described although they may be required
    if you’re selling honey.</p>
  <p>Using templates like this enables you to fully personalise your jars quickly and easily. The benefit is
    that you can print as many or as few as you need for your batch and you can change the text whenever
    necessary, when you need to change the batch number for example.</p>
  <p><strong>This label template set includes a jar label, a lid label and a tamper seal (all
    editable)</strong>. All templates are in colour as well as black and white, in case you don’t have a
    colour printer or you want to print on labels of a particular colour – yellow for example.</p>
  <p>Features of the PDF templates:</p>
  <ul>
    <li>You can edit all the text (including font style, font size and colour).</li>
    <li>You can delete any of the text if it’s not required for your particular use.</li>
    <li><strong>Autofill</strong> template feature: When you make a change any of the text it will change on
      the other labels in the template. This saves time if you’re printing several labels with the same
      text. </li>
  </ul>
