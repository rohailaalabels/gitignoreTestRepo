        <p>Summer fetes are a quintessential British tradition, and we all have memories of them from
          growing up. And now as adults, we get to see it from the other side when we offer to help out
          our kid’s school on the big day. You may be tasked with arranging activities, or perhaps setting
          up a stall. Whatever it may be, you can be sure that there’s plenty to do in order to make the
          day run as smoothly as possible.</p>
      </div>
      <div class="col-xs-6">
        <p>So with this in mind it might be a good idea to get a head start and think about what you’d like
          to do. Most summer school fetes will have a charity in mind, or may even be raising money for
          the school itself, and there’s also the personal incentive of wanting to do your absolute best
          (and impressing everyone else who’s there).</p>
      </div>
    </div>
    <h2 class="headingPost">Plan some unique games and activities</h2>
    <div class="row">
      <div class="col-xs-6 col-md-4 col-lg-5 col-sm-5  pull-right">
        <div class="clear20"></div>
        <p><img class="img-responsive pull-right"
                            src="<?=BLOG_ASSETS?>ssf-1.jpg"></p>
      </div>
      <div class="col-xs-6 col-md-8 col-lg-7 col-sm-7">
        <p>Should you want to organise an activity, the Parent Teacher Association (PTA) have plenty of
          ideas in terms of games to play, including favourites such as ‘hook a duck’ and tin can alley.
          You can make variations of your own to make them unique, and may want to include references to
          local culture, or the school, or even the teachers and staff.</p>
        <p>You’re going to want to provide a little bit of everything so that everyone can get involved. Why
          not suggest a face-painting stall? Or making a putting challenge game for the dads? Get inspired
          by visiting the official PTA website that has plenty of suggestions for games. Just follow <a
                                href="#">this link</a>.</p>
      </div>
    </div>
    <p>You could even arrange with the school to see about inviting the local emergency services to be present
      on the day. They’re more than likely to be happy to show off the vehicles, and maybe even give a few
      talks about safety. Children love the experience and it’s good for the community.</p>
    <h2 class="headingPost">Get baking</h2>
    <div class="row">
      <div class="col-xs-6 col-md-4 col-lg-4 col-sm-5"><img class="img-responsive col-xs-12"
                                           src="<?=BLOG_ASSETS?>ssf-2.jpg"></div>
      <div class="col-xs-6 col-md-8 col-lg-8 col-sm-7">
        <p>A cake stand is as much of a part of a traditional fete as a tombola raffle, an egg and spoon
          race, and coloured bunting. To put it bluntly, a fete isn’t a fete without a good bake sale!</p>
        <p>This is one of the best ways to make a big impression, but not only that, it’s fun, it doesn’t
          cost much to set up and run, and you only need a few people to help, so anyone can join in. A
          good bake sale with lots of variety will have something for everyone, and it’s easy to get kids
          involved, which all helps towards making a great fundraiser.</p>
      </div>
    </div>
    <p>It’s also a chance to get creative and make it something special for your customers and volunteers. Why
      not offer healthy versions of familiar classic? Or gluten free options? This makes sure that nobody
      feels left out. Or you could make your cakes match the theme of the fete, or the school colours. You
      could even make one really special cake that stands out from all the rest, and then that one can get set
      aside for a raffle – another great way to raise that little bit of extra cash.</p>
    <h2 class="headingPost">What you need for a cake stand</h2>
    <div class="row">
      <div class="col-xs-6 col-md-4 col-lg-4 col-sm-5 pull-right">
        <div class="clear20"></div>
        <p><img class="img-responsive col-xs-12 pull-right"
                            src="<?=BLOG_ASSETS?>ssf-3.jpg"></p>
      </div>
      <div class="col-xs-6 col-md-8 col-lg-8 col-sm-7">
        <p>As with so many thing, presentation is everything. This can be an opportunity to show off your
          keen crafting skills, and your eye for detail. If you put the effort in, you could make some
          unique, homemade cakes, and if your stall looks the part, you could be cheeky and charge that
          tiny bit more.</p>
        <p>Firstly you need a good sturdy table that can withstand the weight of all your delicious array of
          goodies (as well the weight of the people leaning in to get a better look). Then find a cloth to
          match the theme or colour of the event, and of course, you need the cakes displayed as well.</p>
        <p>It’s a good idea to clearly <a href="#">label everything you sell</a>, this stops putting people
          off who may like the look of it, but are reluctant to come and ask. You should have a think and
          give the cakes inventive and attention-grabbing names; this can definitely increase sales, not
          to mention being a lot of fun as well.</p>
      </div>
    </div>
    <p>Then all you need is plenty of paper plates, plastic cutlery and serviettes. You may also want to have
      enough bags to hand as well in case people want to take cakes home with them.</p>
    <p>It shouldn’t be a problem to enlist the help of a friend, or another parent, when it comes to making the
      cakes and arranging the stall. And you’re sure to have a lot of fun no matter what you can do to help
      out the school.</p>
    <p>And if this summer is as good as last years, then there’s (hopefully) going to be plenty of days of
      sunshine to enjoy.</p>
