                          <h3 >Applications and use </h3>
                          <p> The commercial uses for this type of label are for cold storage and frozen/chilled distribution. Also used in temperature controlled laboratory environments and excellent for domestic use in home freezing.</p>
                          <h3>Approvals</h3>
                          <p> This label can be used safely for food packaging and can come into direct contact with both dry and fatty foodstuffs (ISEGA Research & Development Institute, Aschaffenburg) and is approved by recommendation XXXVI as published by BfR (Bundesinstitut fur Risikobewertung) the German Federal Institute for Risk Assessment.</p>
                          <h3 >Shelf Life</h3>
                          <p> One year under storage conditions as defined by FINAT (Federation Internationale des Fabricants et Transformateurs d’ Adhesifs et Thermocllants sur papiers et autres supports) 20° - 25° C and 40 – 50% RH.</p>
