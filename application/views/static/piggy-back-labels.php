                            if (!data == '') {
                                data = $.parseJSON(data);

                                if (data.response == 'yes') {
                                    $('.shape').html(data.shapes);
                                    $('#start_limit').val(0);
                                    $('#product_count').val(data.count);
                                    $('#label_counter').html(data.count_format);
                                    $('#ajax_finder_content').html(data.html);


                                    apply_hover_effect();
                                    $('#aa_loader').hide();
                                    $('#home_finder').prop('disabled', false);


                                }


                            }
                        }
                    });
                }
            });

            $(document).on("change", ".shape", function (e) {
                var shap = $(this).attr('id');
                var result = shap.split('_');

                var val = $(this).val();
                var cat = $('#category').val();


                if (val == 'Circular' || val == 'Square') {
                    if (result[1] === '1') {
                        $('#height1').hide();
                        $('#width1').val('');
                        $('#height1').val('');
