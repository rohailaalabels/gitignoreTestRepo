                                                <p>The backing material seldom influences your label design, but the
                                                    chosen backing with its silicone release coating is a factor that is
                                                    important in the production and application phase.</p>
                                                <p><strong>Glassine backing materials</strong><br>
                                                    This backing material is typically available in transparent, white
                                                    and honey for roll stock labels. The backing material is uncoated
                                                    and is available in a variety of weights.</p>
                                                <p><strong>High-density glassine</strong><br>
                                                    High-density glassine backings have a lower deviation in thickness
                                                    and a higher density. This means that they perform better when
                                                    die-cutting filmic face materials, compared to regular glassine or
                                                    kraft backings.</p>
                                                <p><strong>Filmic backing materials</strong><br>
                                                    Polyester (PET) or polypropylene (PP) backing materials are clear
                                                    and specially designed for use with transparent filmic face
                                                    materials in applications requiring excellent clarity. Film backing
                                                    provide a smooth adhesive layer for uniform contact with the face
                                                    material and have good strength characteristics for high-speed
                                                    (volume) production and demanding applications.</p>
                                                <p><strong>Kraft backing materials</strong><br>
                                                    Kraft backings are coated on one side and are more rigid than either
                                                    glassine or high-density glassine backings of the same weight. Kraft
                                                    backings are used in both sheet stock (A3/SRA3) and A4 applications
                                                    because they provide a suitable backing that feeds smoothly into
                                                    copier, inkjet and laser printers. A kraft backing is also ideal
                                                    when the application requires flat sheet conversion. </p>
