  <p>Not only does using these types of companies help the environment, but they show what a caring
    organisation you really are!</p>
  <h3>5. Store data, make purchasing decisions, and take your business online.</h3>
  <p>It is impractical to expect a business to operate solely online. However, it is becoming easier to store
    more data electronically, and have an online presence, rather than printing reams of paper. Strive to
    keep paper usage to a minimum; email brochures instead of printing, and communicate by email rather than
    a letter. You may also find this impacts on your postage fees too!</p>
  <p>Ultimately, every one of these 5 different examples is helping to conserve the world around us, but
    remember to shout about your green credentials.</p>
  <p>Don’t be afraid to promote the fact that your business values the environment and is taking steps to
    improve its processes; customers and clients really do like to hear about it.</p>
  <p>You may not believe you are changing the world, but by taking small steps to reduce your waste and be
    more environmentally aware, you are encouraging others to follow your lead. So tell your customers in
    your communication, add a label to your products or packaging when necessary to highlight your
    commitment, and start spreading the word.</p>
