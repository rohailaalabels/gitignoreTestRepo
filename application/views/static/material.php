    function fetch_category_mateials() {
        $("#aa_loader").show();
        var material = $("#material").val();
        var color = $("#color").val();
        var adhesive = $("#adhesive").val();

        $.ajax({
            url: base + 'ajax/get_material_filter',
            type: "POST",
            async: "false",
            dataType: "html",
            data: {
                material: material,
                adhesive: adhesive,
                color: color,
            },
            success: function (data) {
                if (!data == '') {
                    $("#aa_loader").hide();
                    data = $.parseJSON(data);
                    $('#color').html(data.color_options);
                    $('#adhesive').html(data.adhesive_options);
                    $('#material_droplist').html(data.html);
                }
            }
        });
    }

    function reset_filters() {
        $("#color").val($("#color option:first").val());
        $("#adhesive").val($("#adhesive option:first").val());
    }
