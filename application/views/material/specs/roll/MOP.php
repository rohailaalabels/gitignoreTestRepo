  <h3 >Applications and use </h3>
  <p>This face material is designed for the over labelling  of errors, or informational changes on existing printed materials and/or  signage. Also for bar-coding on asset labels where the underlying substrate  might interfere with the labels scan-ability. </p>
  <p>This face-stock provides excellent print quality by  all the usual printing technologies. Excellent conversion characteristics in  flat-bed and rotary on wide-web presses with high conversion speeds.</p>
  <h3 >Approvals</h3>
  <p>The adhesive complies with the European food  directives and legislation FDA 21; CFR 175, 105 and the German recommendations  XIV as published by BfR (Bundesinstitut fur Risikobewertung)  the German Federal Institute for Risk Assessment. Both of which state that the  adhesive can be used in direct contact with dry, moist and non-fatty  foodstuffs, however according to the FDA regulations the adhesive must be  separated from the foodstuffs by a functional barrier.</p>
  <h3 >Shelf Life</h3>
  <p>Two years under storage conditions as defined by FINAT (Federation Internationale  des Fabricants et Transformateurs d&rsquo; Adhesifs et Thermocllants sur papiers et  autres supports) 20° - 25° C and 40 – 50% RH.</p>
