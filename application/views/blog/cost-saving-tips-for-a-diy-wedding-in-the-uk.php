        <div class="col-lg-8 col-sm-8">
          <p>You don’t have to go it alone in the quest for keeping things as cheap as possible, everyone you
            know will be more than happy to lend a hand when they can. Maybe you could ask you dad to drive
            you to the wedding for old time’s sake? And there’s bound to be someone you know who would be
            willing to whip up a cake? Perhaps you could get people together who could help to make the
            decorations? Not only does this all save money, but it also gets those close to you more
            involved in the big day, making it that little bit more personal as well.</p>
        </div>
      </div>
      <div class="clear10"></div>
      <p>While you’re at it, is there anybody you know who would be happy to be the DJ for the night?</p>
      <p>Similarly, there must be a budding photographer out there who would be happy to do their share of the photos for the day. If not, you can save money by supplying tables with disposable cameras to get some fun candid shots, or you could get everyone to upload their own photos of the day onto a social media site, or get them to email them to you.</p>
      <p>Whatever it is, don’t be afraid to ask others for help – most people will be flattered that you want them involved in your big day.</p>
