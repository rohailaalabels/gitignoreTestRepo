  <p style="text-align: left;" align="center">According to <a
                        href="http://blog.ukti.gov.uk/2014/01/29/export-to-expand-in-2014/" target="_blank">Clive
    Drinkwater</a>, from UK Trade and Investment, businesses that export are, on average, 34% more
    productive, 75% more innovative, undertake three times as much R&amp;D and are 12.5% more resilient than
    businesses that don’t.&nbsp; With the UK housing only 0.8% of the world’s population, the opportunities
    that exist in exporting can be monumental.</p>
  <p>However, once you have made the decision to begin exporting (and for businesses that have never exported
    before), what considerations do you have to make before starting your journey?</p>
  <p>A good place to begin is with the UK Trade and Investment (UKTI), who offer expert trade advice to UK
    based companies wishing to grow their business overseas. Whatever stage of development you are at it is
    worth speaking to one of their specialist advisors before you start – <b><a
                            href="http://www.ukti.gov.uk/home.html">http://www.ukti.gov.uk/home.html</a>. </b>Many of
    their services are provided for free.<b></b></p>
