  <h3 >ADHESIVE</h3>
  <p>This adhesive offers good tack combined with excellent removability from most surfaces, even after long-term labelling. Thanks to its well-balanced adhesive characteristics this removable adhesive is perfectly suited for a wide variety of applications. However the lower adhesion properties are likely to be insufficient for applications on curved surfaces. The adhesive is resistant to light, heat and ageing. </p>
