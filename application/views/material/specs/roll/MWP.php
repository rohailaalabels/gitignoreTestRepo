  <h3 >Applications and use </h3>
  <p>Applications are predominately in market segments where rigid containers are used (e.g. glass, polyester/PET). Due to the fairly rigid nature of the film, care should be taken with application and use on non-uniform surfaces and/or where a very high level of flexibility or squeezability is required (e.g. soap and shower gel containers). A good choice for primary brand and product labels on rigid containers, secondary promotional labels, asset labels, informational labels, external labels/signage and safety labels/signage etc.</p>
  <p>Also used in label applications where a good durability and resistance against water is important.</p>
  <h3>Conversion and printing</h3>
  <p>The corona treated face material assists ink adhesion and can be printed by conventional printing techniques and is also suitable for Thermal Transfer printing with specific ribbons (exact inks, foils and ribbons should be specified by your ink/foil/ribbon supplier). The material has excellent register properties.</p>
  <h3 >Approvals</h3>
  <p>The adhesive complies with the European food directives and legislation FDA 21; CFR 175, 105 and the German recommendations XIV as published by BfR (Bundesinstitut fur Risikobewertung) the German Federal Institute for Risk Assessment. Both of which state that the adhesive can be used in direct contact with dry, moist and non-fatty foodstuffs, however according to the FDA regulations the adhesive must be separated from the foodstuffs by a functional barrier.</p>
  <h3 >Shelf Life</h3>
  <p>Two years under storage conditions as defined by FINAT (Federation Internationale des Fabricants et Transformateurs d’ Adhesifs et Thermocllants sur papiers et autres supports) section 2.5 (20° - 25° C and 40 – 50% RH). </p>
