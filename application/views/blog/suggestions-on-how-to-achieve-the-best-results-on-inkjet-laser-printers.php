      <h2 class="headingPost">LABEL AGE – HOW LONG DO LABELS LAST?</h2>
      <p>All label materials have a shelf-life, which can vary to some extent dependent upon how you store
        them. The standard industry directive is 2 years from the date of manufacture under storage
        conditions as defined by FINAT (Federation Internationale des Fabricants et Transformateurs d’
        Adhesifs et Thermocllants sur papiers et autres supports) 20° – 25° C and 40 – 50% RH.</p>
      <p>Labels past this point may look in good condition but the adhesiveness properties may have changed
        and the backing material may not retain the original properties to hold the sheets flat etc. Always
        try to store label sheets laid on a flat surface, avoiding excessive heat, humidity and direct
        sunlight and as a rule-of-thumb only purchase the quantity of labels required for a couple of months
        production and then re-order.</p>
