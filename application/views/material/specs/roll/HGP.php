  <h3 >Applications and use </h3>
  <p>This product is suitable for a wide range of commercial applications. The label can be used for product branding and promotional purposes, corporate identity and informational uses, such as batch coding and bin/pallet location labels. Also chilled goods display, storage and transportation, laboratory and home usage. </p>
  <h3 >Approvals</h3>
  <p> The dry film adhesive conforms to the requirements of BS5665; part 3: 1989 (equivalent to EN71: part 3: 1995)”Safety of Toys, part 3: migration of certain elements”. Further the ingredients of this adhesive conform to the compositional requirements of article 21 CFR 175.105 of the US code of Federal Regulation. However the product is not intended for direct food contact. </p>
  <h3 >Shelf Life</h3>
  <p> Two years under storage conditions as defined by FINAT (Federation Internationale des Fabricants et Transformateurs d’ Adhesifs et Thermocllants sur papiers et autres supports) 15° - 25° C and 45 – 55% RH.</p>
