                          <h3 >Applications and use </h3>
                          <p>Applications are predominantly in market segments where rigid containers are used e.g. glass and PET. Due to the fairly rigid nature of the film, care should be taken with use on non-uniform surface and where a very high level of sqeezability is desired. The adhesive removes cleanly from many substrates such as Aluminium, Fibreboard, Glass, Polymeric (ABS, PET, PP & PS) and window labelling (with limited outdoor UV exposure). Preliminary testing via a material sample is always recommended.</p>
                          
                          
							<h3>Conversion & Printing</h3>
                          <p>The corona treated face-stock can be printed by conventional printing techniques and is suitable for Thermal Transfer printing with specific ribbons. Exact foils, inks and ribbons should be specified by the OEM printer manufacturer and/or the foil/ink/ribbon supplier. Preliminary testing via a material sample is always recommended.</p>
                          
                          <p>On press corona treatment is recommended for good ink adhesion and has excellent register properties. For optimum ink adhesion use of one of our top-coated Polypropylene films is recommended.</p>
                          
                          
					<h3 >Approvals</h3>
                          <p>The adhesive complies with the European food directives and legislation FDA 175, 105 and the German recommendations XIV as published by BfR (Bundesinstitut fur Risikobewertung) the German Federal Institute for Risk Assessment. Both of which state that the adhesive can be used in direct contact with dry, moist and non-fatty foodstuffs with a reduction factor 2. However according to the FDA regulations the adhesive must be separated from the foodstuffs by a functional barrier.</p>
                          
                          
						  <h3>Shelf Life</h3>
                          <p>Two years under storage conditions as defined by FINAT section 2.5 (Federation Internationale des Fabricants et Transformateurs d’ Adhesifs et Thermocllants sur papiers et autres supports) 20° - 25° C and 40 – 50% RH.</p>
