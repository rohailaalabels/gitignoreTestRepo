  <h2 id="howto">How to Use the Templates</h2>
  <p>To use the templates you’ll need Adobe Reader (a free software – if you read PDFs you’re likely to have
    it). Other PDF readers may work but if you want to edit the PDF and get the same printing results we do
    then it’s best to use Adobe Reader.</p>
  <p><strong>Editing the Templates</strong></p>
  <p>There are two editable fields in each jar label. You can edit the type of fruit and you can edit the</p>
  <p>1. Download the PDF you like by right clicking on the link and selecting ‘save link as’. Then save the
    file wherever you like.</p>
  <p>2. Find where you saved the PDF and double-click to open it.</p>
  <p>3. Edit the template if you wish (lid templates aren’t editable) by clicking in the area where it refers
    to the fruit e.g. ‘Strawberry’. Then type. You can do the same where the date is underneath ‘Jam’. When
    you press enter or click away from the editable field it will automatically fill the rest of the
    template with the same text you entered.</p>
  <p><strong>Printing the Templates<br>
    </strong></p>
  <p>Printing should be quite simple but I suggest printing out on a plain sheet of paper before you use a
    sheet of sticky labels. You can then see if they line up correctly, which they should. Try it with these
    steps:</p>
  <p>1. Open the PDF in Adobe Reader.</p>
  <p>2. Edit as necessary.</p>
  <p>3. Go to <strong>File</strong> &gt; <strong>Print</strong>. A printing options box should pop up.</p>
  <p>4. In the ‘<strong>Page Sizing &amp; Handling</strong>‘ section, select ‘<strong>Actual size</strong>‘
    like this:</p>
  <p><a href="<?=BLOG_ASSETS?>printing-pdf-template-page-size.png"><img
                            class="img-responsive col-xs-12" alt="printing-pdf-template-page-size"
                            src="<?=BLOG_ASSETS?>printing-pdf-template-page-size.png"></a>5.
    (Optional) Ensure you have the colour and quality settings you desire by clicking ‘Properties’ and click
    ‘OK’.</p>
  <p>6. Ensure you have the right labels in the print tray (or plain paper if you are doing a test run)</p>
  <p>7. Click <strong>Print</strong></p>
