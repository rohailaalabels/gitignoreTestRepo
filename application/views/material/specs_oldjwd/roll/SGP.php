                          <h3 >Adhesive</h3>
                          <p>An aqueous compounded acrylic emulsion (PSA),  pressure sensitive, permanent &ldquo;deep freeze&rdquo; adhesive. This adhesive has been  formulated for labels, paper and filmic applications that need to adhere to  pre-frozen surfaces.</p>
                          <p>The adhesive exhibits good initial grab and peel  levels on a wide variety of substrates down to a temperature of -40°C whilst  retaining good cohesive strength at room temperature. Good adhesion to surfaces  where condensation is present can usually be achieved, however a trail under  actual conditions is always recommended as substrates and conditions can vary  considerably.</p>
<h3 >Release Liner</h3>
                          <p>A one side coated solvent-less siliconised glassine  with release levels designed for high-speed rotary label conversion. The paper  has excellent punchability and is therefore suitable for sprocket fed  fan-folded products, as well as labels on rolls for automatic applications.</p>
