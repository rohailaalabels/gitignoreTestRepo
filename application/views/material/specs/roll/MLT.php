      <h3 >Adhesive</h3>
      <p>Water based permanent, pressure sensitive acrylic adhesive, with good adhesion to wet surfaces at low temperatures. Especially designed for glass containers, with excellent resistance to immersion in water/ice. This adhesive should not be exposed to direct sunlight and/or high temperatures for long periods of time.</p>
      <h3 >Additional Information</h3>
      <p>The test of resistance to immersion in water/ice is carried out by sticking a blank (unprinted) label on a standard glass bottle. Five days later the bottle with label applied is submerged in an ice bucket containing ice and water for a minimum period of 4 hours and the labels resistance to immersion is checked.</p>
