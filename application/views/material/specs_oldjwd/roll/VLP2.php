                          <h3 >Adhesive</h3>
                          <p>An aqueous compounded acrylic emulsion (PSA),  pressure sensitive, permanent adhesive. This adhesive has been formulated for removable  label-stock, paper and filmic applications where a clean peelable bond is  required. The adhesive also has excellent conversion and ageing  characteristics.</p>
<h3 >Release Liner</h3>
                          <p>A one side coated solvent-less siliconised glassine  with release levels designed for high-speed rotary label conversion. The paper  has excellent punchability and is therefore suitable for sprocket fed  fan-folded products, as well as labels on rolls for automatic applications.</p>
