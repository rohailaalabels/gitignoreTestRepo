  <p><strong id="designer">This is what our honey label designer has to say:</strong></p>
  <p>Two tones… some of the best things around are made up of just two tones. The keys on a piano, rhubarb and
    custard sweets, chocolate chip cookies, a Sheffield Wednesday shirt (ok – that’s just me). And of
    course, the stripes on a honeybee.</p>
  <p>Yellow and black, they wear it well. They know how to rock that look better than anyone!</p>
  <p>That’s the basis of this design. Shocks of bright yellow and charcoal black to decorate your next batch
    of tasty honey and show off its name and details.</p>
  <p>The front of the jar focuses in on a central hexagon, symbolising the honeycomb structure synonymous with
    this tasty preserve. We know instantly what is within once we see this shape.</p>
  <p>Inside this, we see a white hive – almost like we’re looking through a window into a garden and seeing
    this in the middle distance. Hive by name, hive of activity by nature… this is the factory where the
    golden treasure is produced by this most diligent of workforce.</p>
  <p>Speaking of workforce – this hexagon leads down into a little graphic below, representing the bee
    herself, with her stripy uniform and sting mounted tail. A little reminder of who we owe thanks to for
    this tasty treat, whilst we crunch down on another slice of generously covered toast or golden sheened
    crumpet.</p>
  <p>The editable title and details use solid yet friendly fonts, change these to put your own stamp down and
    christen the bounty within. The bee did the work, but the world needs to know that you did your bit
    too!</p>
  <p>Enjoy the labels – enjoy the honey more!</p>
