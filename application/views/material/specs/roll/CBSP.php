      <h3>Applications and use </h3>
      <p>Specially designed for product labels of high added value bottled in glass (wine, liquors, champagne, oil, water, jar....). Labels with a luxury and metallic look. </p>
      <p>The test of resistance to immersion in water-ice is carried out by sticking a blank (unprinted) label on a standard glass bottle. Five days after the label is stuck on, it is submerged in an ice bucket containing icy water for a minimum period of 4 hours and its resistance to immersion is checked. </p>
      <h3>Approvals</h3>
      <p>The research centre ISEGA has given the adhesive SH-6020 Plus approval according to the European requirements for indirect contact with foodstuffs and compliance with the rules of the Code of Federal Regulations, FDA, 21 CFR Ch.I(April 1, 2013 edition) 175.105 . According to the FDA regulations the adhesive must be separated from the foodstuffs by a functional barrier.</p>
      <h3>Shelf Life</h3>
      <p> Two years under storage conditions as defined by FINAT section 2.5 (Federation Internationale des Fabricants et Transformateurs d’ Adhesifs et Thermocllants sur papiers et autres supports) 20° - 25° C and 40 – 50% RH.</p>
