                          <h3>Limitations</h3>
                          <p>Limited adhesion at low temperatures and the highest end-use temperature must be separately checked together with the face material.</p>
                          <h3 >Applications and use </h3>
                          <p>Applications  are predominately in market segments where rigid containers are used (e.g.  glass, polyester/PET). Due to the fairly rigid nature of the film, care should  be taken with application and use on non-uniform surfaces and/or where a very  high level of flexibility or squeezability is required (e.g. soap and shower  gel containers). A particularly good choice for primary brand and product  labels on rigid containers where it is important for the content to be visible  and not partially obscured by a label. Also secondary promotional labels,  informational labels, external labels/signage and safety labels/signage etc.</p>
<h3>Conversion &amp; printing</h3>
                          <p>Due to  the pre-treatment, the film is printable by common label printing methods like  letterpress, flexography, screen, offset lithography and digital printers  (copiers and laser). Special inks designed for non-absorbent materials should  be used.</p>
<h3>Approvals</h3>
                          <p>The research centre ISEGA has given an approval  according to the European requirements for direct contact with dry and moist  and fatty foodstuffs, which have a reduction factor of at least 4 according to  the regulation EU No. 10/2011. (ISEGA has an EN ISO 17025 accreditation for analytics and EN 45011 for  certification)</p>
<h3 >Shelf Life</h3>
