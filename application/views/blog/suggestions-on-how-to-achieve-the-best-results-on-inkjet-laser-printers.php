      <h2 class="headingPost">LABEL QUALITY – WHY IS THIS IMPORTANT?</h2>
      <p>Poor quality blank sheet labels are the number one cause of problems when printing labels on your
        laser or inkjet printers. You may have experienced label sheets going through your printers curling,
        or in the case of Polymer labels shrinking and wrinkling because of the increasing amounts of heat
        that will be generated by your printer during extended print runs. It is always advisable to be
        aware of the recommended duty cycle for your printer, especially the number of continuous sheets
        that can be printed, before allowing the printer to rest before restarting.</p>
      <p>Unfortunately, like most things in life, selecting labels on the basis of price alone can be a false
        economy, especially when using low quality label sheets, as the adhesive can often melt and labels
        come off the sheets in the printer, causing additional problems that will take time to rectify.</p>
      <p>When shopping for your labels, it is best to always consider the quality of the materials e.g. label
        face-stock, adhesive, release liner and backing, as a quality combination is most likely to result
        in problem free printing.</p>
