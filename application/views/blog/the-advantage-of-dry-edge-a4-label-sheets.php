      However even in small desktop printers there are transport rollers and the principle remains that
      adhesive transfer will cause problems with print output quality and printer productivity. </p>
    <p>HOW DO “DRY EDGE” EURO-DIE SHEETS PREVENT THIS PROBLEM<br>
      By removing a 1mm strip of label and adhesive from all 4 edges of the label sheet the adhesive moved to
      the sheet edges by pressure, is removed leaving a dry backing area in its place, without compromising
      the layout of the labels on the sheet. Ensuring dry, fast, problem free printing of your labels. </p>
    <p>Most printers are not able to print edge-to-edge on A4 sheets and leave an unprintable area of 3mm–5mm
      around the edge, therefore the removal of 1mm of label face-stock has no detrimental impact on the label
      layout on the sheet.</p>
    <p>Other benefits include the ease of removal for full sheet (A4) and half-sheet (A5) label formats that
      previously would have required a cut in the backing paper (back-cut/crack-back) to assist with the label
      peeling.</p>
    <h2 class="headingPost">SUMMARY – WHY TAKE THE RISK?</h2>
    <p>While the problem of “wet edge” label sheets is hopefully very much the exception and not the norm, why
      take the unnecessary risk?</p>
    <p>If you are unfortunate enough to experience this problem, not only is your label printing going to be
      disrupted, but you are going to have to undertake unnecessary cleaning and maintenance on your printer.
      Why when “dry-edge” label sheets are readily available to purchase on line, at no extra cost and improve
      the alignment and speed of problem free printing, would you choose not to select this format?</p>
