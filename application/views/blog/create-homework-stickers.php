  <p>&nbsp;</p>
  <p>We’ve created a blank template which you can use for any subject but we thought you might appreciate some
    subtly themed subject-specific templates for some of the main subjects.</p>
  <p>Each template has three editable fields for subject, homework task and due date. When you finish writing
    in one of the fields – the homework field for example – the template will automatically paste the same
    text into the rest of the homework fields.</p>
  <p>Indeed the templates aren’t limited to setting homework. The three text boxes are completely editable so
    you can use them whenever you need to quickly create and print individual stickers with the same
    content. Of course they are designed as a sticker template for setting school homework.</p>
