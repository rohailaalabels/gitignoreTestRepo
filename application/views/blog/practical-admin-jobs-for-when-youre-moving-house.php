  <p>One of the first things to do once you’re actually in your new home is to arrange having your <a
                        href="https://www.royalmail.com/personal/receiving-mail/redirection">post redirected</a>. This
    ensures that any sensitive post such as bank statements won’t ever be sent to the old address.</p>
  <h2 class="headingPost">Notify services and utility companies</h2>
  <p>It very important that as close to actually leaving the house for the last time as possible, you make
    sure to take note of your gas and electricity meter readings. This comes in handy when you notify the
    utility companies, as this is often the first thing they will ask for once you’ve given details of the
    new address. Also, by taking the meter readings at the very last minute you’ll also get a more accurate
    final bill.</p>
  <div class="row">
    <div class="col-xs-6">
      <p><img src="<?=BLOG_ASSETS?>paj-2.jpg"
                            class="img-responsive col-xs-12 pull-right"></p>
    </div>
    <div class="col-sm-6">
      <p>If you’re renting a house, your tenancy agreement will most likely stipulate exactly how much
        notice the agency (or landlord) requires. The general rule is that the sooner you let them know,
        the better. It’s also best to inform your landlord in writing as this holds more weight as a
        record of correspondence. Additionally, you should arrange a date for an inspection where
        everybody is present at the same time; this avoids the potential for disagreements regarding
        damage.</p>
    </div>
  </div>
  <p>Be sure to pause or change all regular subscription services you may use such as newspaper, grocery and
    milk deliveries. You may also need to consider (long in advance) arranging reinstallation of services
    such as cable or satellite television services, as well as letting your Internet providers know that
    you’re moving. The sooner you let these companies know, the sooner you can have them up and running at
    your new address.</p>
  <p>You will also have to contact your new local council to notify them for changes with paying your council
    tax. If you are moving far from your local area and are unsure as to exactly who your new local council
    is, you can find out their details <a href="https://www.gov.uk/find-your-local-council">here</a>.</p>
  <h2 class="headingPost">For the big day itself</h2>
  <div class="row">
    <div class="col-xs-6 col-md-4 col-lg-3 col-sm-5 pull-right">
