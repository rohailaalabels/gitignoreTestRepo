<br />
<br />

The introduction was in response to customer frustrations with the problems sometimes caused by the industry term "wet edge" on sheets of labels when printing, caused by pressure on the label sheets leaching small amounts of adhesive onto the printer rollers.
<br />
<br />

To remedy this we have introduced our new "Dry Edge Sheets" in all materials on A4 sheets, as standard. Although relatively new to the UK, edge trimming of sheets has been a standard for label manufacture in Europe for some while now and significantly improves the print performance and on full sheets of A4 assists the label peel from the release liner, without the need for "back cuts" in the release liner.
