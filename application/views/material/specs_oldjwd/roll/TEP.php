                          <h3 >Applications and use </h3>
                          <p> This Direct Thermal product is designed for use in dry weigh scales, process tracking, POS and  item information labelling where limited image durability is required. Typical applications include fruit and vegetable weighing scales, hardware, WIP material tracking.</p>
                          <h3 >Conversion and printing</h3>
                          <p> This product is designed to be converted and dispensed at high speed by all conventional roll conversion techniques, including flexographic and UV letterpress. Due to the thermographic properties exposure above 50° C may cause premature imaging or discolouration. Inks containing alcohol or volatile organic solvents may also cause discolouration. It is advisable to test inks and varnishes before conversion. </p>
                          <p> Also contact with moisture, oil, fats, plasticizers, strong lighting and direct sunlight for prolonged periods should be avoided because of potential image fade issues. </p>
                          <h3 >Approvals</h3>
                          <p>The adhesive complies with the European food directives and legislation FDA 21; CFR 175, 105 and the German recommendations XIV as published by BfR (Bundesinstitut fur Risikobewertung) the German Federal Institute for Risk Assessment. Both of which state that the adhesive can be used in direct contact with dry, moist and non-fatty foodstuffs, however according to the FDA regulations the adhesive must be separated from the foodstuffs by a functional barrier. </p>
                          <h3 >Shelf Life</h3>
                          <p> One year under storage conditions as defined by FINAT (Federation Internationale des Fabricants et Transformateurs d’ Adhesifs et Thermocllants sur papiers et autres supports) 20° - 25° C and 40 – 50% RH.</p>
