                          <h3 >Adhesive</h3>
                          <p>The adhesive has a very high tack even on cold surfaces, down to -10° C. Due to the high initial tack, even on moist surfaces, the adhesive is particularly suited for labelling both polar and non-polar surfaces, such as plastic containers, film wrappings and other packaging materials. This adhesive is also very suitable for fast running dispensers, on which the initial tack strength has to be built-up extremely quickly, when the labels are blown on to the substrate. Once labelled this adhesive is highly waterproof.</p>
                        
