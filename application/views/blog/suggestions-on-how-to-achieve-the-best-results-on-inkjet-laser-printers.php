      <h4>LABEL SHEET &amp; PAPER JAMS</h4>
      <p>A common problem, when label and paper sheets fed into the printer get jammed and the printer stops
        working. Printers usually have an indicator that lets the user know that there is a sheet stuck and
        where in the transport mechanism it is, so that the correct panel can be opened.</p>
        <div class="row"> <p><img class="aligncenter size-full wp-image-1748 col-sm-8 col-sm-offset-2"
                        src="<?=BLOG_ASSETS?>13.jpg" alt="13" ></p><div class="col-sm-2"></div></div>
      <p>The reason for sheets not feeding correctly are various and include printing too many sheets, in
        excess of the OEM recommendations, causing the printer to overheat. Also, unclean or old printer
        rollers will cause sheets to slip through reduced traction and jam. When the rubber printer rollers
        age and lose density, they also lose the ability to grip the sheets, causing mis-feeds and jams in
        the printer. Similarly, with some of the heavier label materials always ensure that you have
        selected the correct setting for the printer to cope with a slightly thicker material.</p>
      <h4>PAPER MISFEEDS</h4>
      <p>As with jams in the printer an uncleaned or old roller and incorrect settings for the heavier/thicker
        label sheet is usually the cause of this problem from the feed tray. Check the settings are correct
        for the label material being used and that the pick-up and small transport rollers are free of dust
        and/or adhesive and are not in need of replacing. If the problem persists then it may require use of
        the manual feed option in the immediate term until the problem can be solved.</p>
