                        <h3 >Applications and use </h3>
                        <p>Multi-purpose removable labels for commercial, industrial and retail applications. Particularly useful on product packaging where a very high level of flexibility or squeezability is required (e.g. soap and shower gel containers). A particularly good choice for primary brand and product labels on rigid containers where it is important for the content to be visible and not partially obscured by a label. </p>
                        <p>Also secondary promotional labels, informational labels, external labels/signage and safety labels/signage etc.</p>
                         
                         
						<h3>Conversion & Printing</h3>
                        <p>The modified acrylic based top-coating can be printed by most conventional printing techniques, including flexo; screen; offset-litho; letterpress, silkscreen; gravure and hot or cold foiling processes. UV, water-based and solvent-based inks can be used. The topcoat is designed for optimum ink adhesion. On-press corona treatment is not advised. The face material is suitable for Thermal Transfer printing with specific ribbons (exact inks, foils and ribbons should be specified by your ink/foil/ribbon supplier). The material has excellent register properties, especially when a high number of colour are used.</p>
                          
                          
						<h3 >Approvals</h3>
                        <p>The research centre ISEGA has given this adhesive approval according to the European requirements for direct contact with dry and moist, non-fatty foodstuffs.</p>
                        
