  <p>Holidays are coming… holidays are coming… (Coca Cola, anyone?)</p>
  <p>For many consumer businesses, Christmas can be a time of intense competition. Whilst we’re thinking about
    presents, food, decorations, trips abroad, even new furniture, big brands are ploughing millions of
    pounds into marketing their products so that they are at the forefront of the consumer’s mind.</p>
  <p>With big money being spent at Christmas (in 2012, shoppers spent a total of around £70 billion in shops
    and online over the six weeks to the end of December*) even businesses for which Christmas is a quiet
    time are trying to jump on the marketing bandwagon to capitalise on the festive period. So why not join
    them?</p>
  <h3>Those who fail to plan, plan to fail</h3>
  <p>Firstly, it is wise to do a bit of prep work. Plan ahead. Make sure your databases are up to date, and
    you have clear call to actions on your website (follow us on Facebook, sign up to our newsletter) so
    that you have as much data as possible to work with. You’ll be thankful of having your data organised
    when you come to sending out communications to your customers. Bad quality data is worse than no
    data!</p>
  <h3>Do you sell online or through social media?</h3>
  <p>Christmas 2012 showed some interesting statistics for the online retail industry. UK Internet users made
    2.8 billion visits to retail websites and spent 372 million hours shopping, with Boxing Day seeing 113
    million visits, setting a new <a
                        href="http://www.experian.co.uk/blogs/latest-thinking/2013/01/online-retailing-top-stats-of-christmas-2012/"
                        target="_blank">British retail record</a>.</p>
  <p>With high streets at their busiest at Christmas, more and more people are wanting to order from the
    comfort of their own home, so if you do sell online, focus your efforts here where you’re likely to see
    the majority of sales.</p>
  <p>Alternatively, even if you don’t sell online, you may wish to step up your internet presence or make use
    of your website and social media to promote your business during the holidays. Utilise social media
    sites, such as Facebook or Twitter, share interesting Christmas news and pictures with your audience and
    they may just pass on your message or “share” with others.</p>
  <h3>It’s the giving that counts</h3>
  <p>Offering special Christmas discounts on your products, or putting together a gift set, are great ways of
    drumming up business around the festive period. These can be packaged in expensive wrapping or for a
    cost effective option you could use a gift set label!</p>
  <p>Maximise these through a ’12 days of Christmas’ discount period, where you could run different discounts
    on different products. If you haven’t offered gift vouchers before, think about this as a potential
    Christmas gift idea. Make people aware of your discounts and offers through email campaigns, both to
    current customers and new ones. If you haven’t built up your own contact lists you can purchase them,
    however these tend to be expensive and you will be wise to check on the quality of email addresses.</p>
  <h3>C is for Charity</h3>
  <p>Instead of sending Christmas cards to your customers, why not use that money to give to a charity of your
    choice? Then, make people aware by sending them a pretty label (including the details of the donation)
    to add to their Christmas tree as a decoration.</p>
