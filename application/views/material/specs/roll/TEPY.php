  <h3 >Applications and use </h3>
  <p>This product is designed for use in retail item level applications, such as “eye catching” promotional labelling, along with many other commercial and industrial applications requiring the printing of variable data and/or information. It also provides excellent grades with EAN and other barcodes. Other typical end user applications include front of store labelling, process tracking, POS and item labelling. </p>
  <p>As economy thermal papers are not equipped with a protective surface coat, the labels should not be exposed to water or greasy substances. Also the image may fade over a period of time if applied to surfaces containing plasticisers e.g. Flexible Polyvinyl Chloride.</p>
  <h3 >CONVERSION & PRINTING</h3>
  <p>The face-stock offers a good quality of imprint provided that solvent-free inks and varnishes are applied. Inks should be suitable for printing thermal papers and consultation with the ink supplier is advised. Suitability tests should be performed under customer-specific conditions, as unpredictable interactions with the ink may occur, due to the lack of a protective coating. Web temperature during curing should not exceed 50° C in order to prevent premature imaging, or discolouration.</p>
  <p>Printing speeds of up to 250mm/s are possible in direct thermal printing.</p>
  <h3 >APPROVALS – Regulations concerning contact with food..</h3>
  <p>The thermal paper grade face-stock, is physiologically and toxicologically safe and may therefore be used in printers and scales for the marking of goods. However they can only be applied in direct contact with those groups of foodstuffs, which are washed and/or peeled, before consumption. ISEGA (Research & Development Institute, Aschaffenburg).</p>
  <p> The adhesive complies with the European food directives and legislation FDA 21; CFR 175, 105 and the German recommendations XIV as published by BfR (Bundesinstitut fur Risikobewertung) the German Federal Institute for Risk Assessment. All of which state that the adhesive can be used in direct contact with dry, moist and non-fatty foodstuffs, however according to the FDA regulations the adhesive must be separated from the foodstuffs by a functional barrier. </p>
  <p>The release liner complies with recommendation BfR XXXVI</p>
