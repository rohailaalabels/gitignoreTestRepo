  <p>Put your recyclables in the blue bin. Wash all your clothes on 30. Don’t fly if you don’t have to…
    instead, drive in your electric eco-friendly car. That’s just the start of what we’re now asked to do in
    order to consider our environment and lower our carbon footprint.</p>
  <p>So what about businesses? Surely they should be considering the same?</p>
  <p>With natural eco systems in decline, and the human population having increased, there has already been a
    harmful impact on both humans and other living systems; and it looks set to spiral. There is potential
    to create harm to the environment from any industrial process, and there has been a focus in recent
    years on trying to create global sustainability.</p>
  <p>Customers’ expectations are higher than ever, and they are now searching for healthier and greener
    products and services. Businesses are demanding stronger environmental credentials from their suppliers
    and manufacturers that match their own social and environmental responsibility programs.</p>
  <p>So as a small to medium size business, can you really make a difference to the environment? How easy is
    it for you do this?</p>
  <p>Here are five different ways we’ve found to help reduce your environmental impact (and how labels can
    help)!</p>
  <h3>1. Gain “green” accreditation</h3>
  <p>Whatever industry you are in, there are special accreditations given to companies either by demonstrating
    a commitment to the environment, or by complying with certain legislation, codes of practice or
    regulations.</p>
  <p>A good example of this is ISO. ISO (International Organisation for Standardisation) is the world’s
    largest developer of voluntary International Standards.</p>
  <p>ISO International Standards ensure that products and services are safe, reliable and of good quality. For
    business, they are strategic tools that reduce costs by minimising waste and errors and increasing
    productivity. Accreditations can help businesses to increase their credibility. You can find out more
    about ISO on their website here: <a href="http://www.iso.org/iso/home.htm" target="_blank">http://www.iso.org/iso/home.htm</a> </p>
  <p>The challenge faced for small to medium sized businesses is the cost associated with these accreditations
    and the fees charged may be outweighed by the benefit gained, so it is advised to do your research on
    whether this is a step forward for your business or a step back. There also needs to be management
    processes in place for these to function effectively, so if you are looking for fast and easy ways to
    reduce your environmental impact then these may not be for you!</p>
  <h3>2. Strive to design good packaging.</h3>
  <p>Using recyclable packaging wherever possible is a positive move towards helping the environment. However,
    you can go one step further by taking into consideration your packaging designs. For example, if you
    sell three items that are essentially the same product, only in different colours (say, a pen in white,
    black and blue), then using a label to differentiate the colour options instead of ordering three
    different types of packaging will not only reduce the amount of packaging you have to order but will
    also reduce your costs and overheads on storage!</p>
  <h3>3. Find ways to recycle your existing material effectively without waste.</h3>
  <p>So your existing brochures and sales literature have a product on there that you have discontinued? Or
    you have noticed that a product code is wrong and needs amending? It can be easy to simply discard what
    you think of as “out of date” materials, however, with a simple solution such as an over label
    containing your new information you can not only reduce your costs of re-printing, but also help to save
    the environment at the same time.</p>
  <h3>4. Dispose of your waste effectively</h3>
  <p>There are companies out there who will recycle your paper and cardboard waste and pulp it back into new
    packaging. They will also recycle plastic waste. An example of such a company is Veolia – <a
                        href="http://www.veolia.co.uk/" target="_blank">http://www.veolia.co.uk/</a></p>
  <p>If you print a lot, like most companies, you’ll get through a lot of ink and toner cartridges. Once
    they’ve come to the end of their useful life, store them away until you have quite a few (most recycling
    companies require a minimum quantity or value before picking them up). You can then get them collected
    and recycled. Some companies, such as <a title="GT Recycling" href="http://www.gtrecycling.com"
                                                         target="_blank">http://www.gtrecycling.com</a>, will even pay
    you for it. So not only can you feel good about reducing your impact on the environment but you’ll get
    some money back too.</p>
  <p>There are also directives in place such as WEEE (Waste from electrical and electronic equipment), which
    aims to reduce the waste from electrical equipment. All products that fall under this directive carry
    the “wheelie bin” label to show they have complied with the directive (examples shown below). There are
    companies who will manage this waste for you, such as WasteCare – <a
                        href="http://www.wastecare.co.uk/compliance-services/weeecollect/" target="_blank">http://www.wastecare.co.uk/compliance-services/weeecollect/</a> </p>
  <p>Have you ever noticed the “wheelie bin” sticker on any of your everyday electrical products?</p>
