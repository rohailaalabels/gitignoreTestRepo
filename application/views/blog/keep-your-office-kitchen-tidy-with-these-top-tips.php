      <p>The office environment is not only a productive place to be, but it can also be an enjoyable one
        as well. It’s a place where people can form lasting relationships with those who they work with,
        and as you tend to spend more time with colleagues than close friends, it’s important to make
        sure you keep grievances to a minimum.</p>
    </div>
    <div class="col-md-6">
      <p>With that in mind it brings us to talking about communal spaces in offices, the most obvious of
        which is the office kitchen. Easily the most interesting area of an office, the kitchen forces
        people to put their social skills to the test, and it’s where passive aggressive post-it notes
        speak of long-contested arguments regarding unfair fridge space and absent low-fat yogurt.</p>
    </div>
  </div>
  <p>Asides from being a potential cause of conflict, the kitchen can be a hotbed for food hygiene and health
    and safety problems. So it’s important to keep the office kitchen clean and clear. With that in mind,
    here are a few top tips for keeping the office kitchen tidy.</p>
  <h2 class="headingPost">Safety first with some safety labelling</h2>
  <div class="row">
    <div class="col-md-4 col-sm-4 pull-right"> <img class="img-responsive col-xs-12 pull-right"
                         src="<?=BLOG_ASSETS?>ok-1.jpg"></div>
    <div class="col-md-8 col-sm-8">
      <p>At home, the kitchen is the number one place for domestic accidents, and as such requires extra
        vigilance when it comes to safety, and this is absolutely no different at the office. To make
        sure that risks are reduced as much as possible, signs and <a href="#">labels highlighting
        potential dangers</a> are a must.</p>
      <p>Signs drawing attention to things such as hot water and electrical appliances need to be clear
        and bold, and information about the whereabouts of the nearest first aid kit and who the
        appointed first aiders are is also needed.</p>
    </div>
  </div>
  <div class="row">
    <div class="col-md-4">
      <div class="clear10"></div>
      <p><img class="img-responsive col-xs-12" src="<?=BLOG_ASSETS?>ok-2.jpg"> </p>
    </div>
    <div class="col-md-8">
      <h2 class="headingPost">Be clutter free</h2>
      <p>People are often in a hurry in their break time, and as such, they are less likely to clear up
        properly behind them. But a clear and clutter free kitchen helps others with food safety and
        reduces the risk of accidents.</p>
      <p>Keeping the kitchen counters clutter-free is not only cleaner and safer, but it is also much more
        polite.</p>
    </div>
  </div>
  <p>It’s a good thing to encourage others to clean as they go, meaning everything used gets washed and out
    away immediately. You could even suggest that anything that gets emptied such as the coffee pot or paper
    towel dispenser gets refilled straight away as well.</p>
  <h2 class="headingPost">Food safety and labelling</h2>
  <div class="row">
    <div class="col-md-4 pull-right">
      <div class="clear20"></div>
      <p><img class="img-responsive col-xs-12 pull-right"
                            src="<?=BLOG_ASSETS?>ok-3.jpg"></p>
    </div>
    <div class="col-md-8">
      <p>Keeping workspaces and storage areas clutter free also helps when it comes to proper food hygiene
        practices. Sinks filled with dirty dishes, and fridges and cupboards full of out of date food
        are all breeding grounds for bacteria and dirt. And as each day passes without it being
        resolved, the higher the risk is for some nasty germ and bacteria outbreaks.</p>
      <p>In some cases, people bring items for lunch into work and leave them in the refrigerator or
        cupboards, only to forget about them, which then sit for weeks (or even months) without being
        thrown away. By using brightly coloured labels it’s possible to gently remind others to be
        considerate when using communal storage.</p>
    </div>
  </div>
  <p>As hinted at previously, one of the oldest work-related clichés is the on-going conflict between people
    regarding food. To avoid such conflict, you could maybe have some labels on hand near the fridge to let
    others what is strictly out of bounds – or even what people are more than happy to share with
    others.</p>
  <div class="row">
    <div class="col-md-6"> <img class="img-responsive col-xs-12" src="<?=BLOG_ASSETS?>ok-4.jpg"> </div>
    <div class="col-md-6">
      <h2 class="headingPost">Set a policy and stick to it</h2>
      <p>If etiquette becomes a problem, the main point to consider is controlled and respectful
        communication. A clear written policy lets everybody know what’s expected, what that rules are
        that have been set in place, as well as what are the reasons for these rules. This set of
        expectations could even be included within the employee handbook, or brought up as part of a
        staff meeting.</p>
    </div>
  </div>
  <p>Signs posted in the break room, in hallways, or on company bulletin boards can be helpful reminders to
    everyone about the kitchen rules. Once a standard set of rules for cleanliness is put in place, it
    should help keep things running smoothly and help to ensure that the eating areas are sanitary for
    everyone to use.</p>
  <div class="row">
    <div class="col-md-6 pull-right"> <img class="img-responsive col-xs-12 pull-right"
                         src="<?=BLOG_ASSETS?>ok-5.jpg"></div>
    <div class="col-md-6">
      <h2 class="headingPost">Take ownership</h2>
      <p>A good idea is to implement a rolling rota for who will be responsible for the kitchen, this is a
        good way to ensure that everyone does their part, and reduces frustration between co-workers. By
        appointing one person at a time, everyone will see just how annoying it is when others don’t do
        their bit, and may help certain people be a bit more considerate.</p>
    </div>
  </div>
  <p>It’s also good to lead by example and use some initiative. Pitch in if you see others who need help with
    cleaning, and offer to do things like empty the bins or clean up the coffee machine. The office kitchen
    is where individual responsibility is the absolute key to a calm and friendly work environment.</p>
