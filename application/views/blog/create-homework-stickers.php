  <h2 id="howto">How to use the templates</h2>
  <p>It’s easy:</p>
  <p>1. Download and save the template.</p>
  <p>2. Open the homework sticker template in Adobe Reader.</p>
  <p>3. Write what you like in the subject, homework task and due date fields of the first sticker. What you
    wrote in the first sticker will now magically be on all the stickers!</p>
  <p>4. Print them on A4 sticker sheets with stickers that are 75mm x 50mm (there’s a link in the section
    below to buy stickers that are designed specifically for these templates). Or you could use a full A4
    sheet label and cut out the labels yourself but that takes a bit more time.</p>
