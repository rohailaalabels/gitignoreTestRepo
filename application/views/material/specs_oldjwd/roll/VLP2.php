                          <h3 >Applications and use </h3>
                          <p>This  product is suitable for a wide range of promotional and commercial label  applications where an attractive label appearance and removability is required.  The face-stock is suitable for solid and halftone images and text printing by  letterpress, flexo (UV and water based), offset and screen process in addition  to copier and laser printers. Also hot and cold foil stamping, varnishing and  variable imprinting with thermal transfer are also suitable for this product. The end user  should always ensure the suitability of this product for specific applications  as the adhesive may not be as readily removable from certain substrates.</p>
<h3 >Approvals</h3>
                          <p>This  label can be used safely for food packaging and can come into direct contact  with both dry and fatty foodstuffs (ISEGA Research  &amp; Development Institute, Aschaffenburg).</p>
                          <p>The  dry film adhesive conforms to the compositional requirements of either BGA XIV  or article 21 CFR 175.105 or the US code of Federal Regulation.</p>
                          <p>However  this product is not intended for use in direct food contact.</p>
                          <p>In  addition also conforms to the requirements of BS5665: Part 3: 1989 (equivalent  to EN71: Part 3: 1995) covering the Safety of Toys: Part 3: migration of  certain elements.</p>
<h3 >Shelf Life</h3>
