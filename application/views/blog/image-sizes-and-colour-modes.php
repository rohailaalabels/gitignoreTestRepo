  <p>Whether creating a PowerPoint presentation or printing out photographs for a friend, most people at some
    stage in their careers will have to deal with images. There are a few handy tips that will help you
    understand more about images and their colour modes, preventing undesired results. These tips are
    especially useful for those involved in professional print jobs, but without a degree in graphic
    design.</p>
  <h4>Colours</h4>
  <p>Have you ever printed out a picture or logo that looked way better on screen? Or maybe you have uploaded
    an image to a website and it looked wrong? Maybe the ‘colour mode’ of your image was wrong. There are
    two main colour modes for images; RGB and CMYK.</p>
  <p>RGB (which stands for red, green, blue) is the colour mode for web images or images meant to be seen
    on-screen. This is not to say that you can’t print a RGB image on your office printer, however if need
    it for a document that is to be professionally printed, RGB is not acceptable. It is also worth
    mentioning that vivid colors and neons can be achieved with RGB on-screen.</p>
  <p>CMYK (cyan, magenta, yellow, black) is the colour mode most printers work in so images for professional
    print will need to be set to or provided in this mode. If you were to take an image of a logo that
    on-screen looked neon green (for example) and then changed the colour mode to CMYK, you would see the
    neon green fade to more of a dull, boring green. This is because CMYK colour system cannot create vivid
    colours. This is something to think about when choosing a colour palette for a new brand or
    packaging.</p>
