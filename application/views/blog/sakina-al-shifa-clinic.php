<h2>HISTORY</h2>
<p>The charity was established in 2009 by Sohail Sethi and Asifa Sethi to provide free general medical services for families in the city of Lahore unable to afford private medical costs. When the clinic first opened it was intended to serve a small local community in north of Lahore, providing free basic consultation, medical and prescription services for c. 300 patients a week, but has since grown to become 5-6 times that size in response to demand. </p>
<h2>CHARITBLE AIMS & OBJECTIVES</h2>
<p>The charity has two main aims, constituted in the registration document with The Charity Commission, which can be summarised as:</p>
<p>1. The relief of the sick and poor in the Punjab province of Pakistan either generally or individually
    through the provision of funds for establishment of medical surgery, and</br>
2. To advance education in all areas relating to malnutrition and hygiene.</p>
<h2>STAFF & OPENING HOURS</h2>
<p>The clinic currently employs 6 full-time staff and is managed by Imran Bashir – Practice Manager. In addition, there are 2 Doctors and a Nurse, plus a Pharmacist and a non-clinical member of staff.</p>
<p>The clinic is open 6 days a week from Monday to Saturday, 07:30 – 18:30.</p>
<h2>OUTCOMES</h2>
<p>The clinic now serves and see 300-400 patients a day for consultations and repeat medication, dealing with ongoing illness and medical conditions such as Asthma, Diabetes, high blood pressure, respiratory illness and urinary tract infections, dispensing c. 300 free prescriptions for medication daily.</p>
<p>The clinic also provides a free testing service for blood sugar levels, blood tests, scans and urine tests.</p>

<p>Everything is free and there are no chargeable services provided and the clinic serves a particularly poor neighbourhood in the city of Lahore, which is Pakistan’s second largest city with an estimated population of 9.3 million residents in 2018.</p>

<p>Unfortunately, the provision of free clinical services such as this are not common, hence the need to expand the size of the clinic and range of services provided to keep pace with demand.</p>

