    <h3 class="headingPost">Capacitive &amp; Ultrasonic Sensors</h3>
    <p>These new generation sensors use capacitive or ultrasonic technology, both of which are optics-free to
      measure distance and detect differences in the label height relative to the backing liner and therefore
      detect labels that optical sensors cannot i.e. clear labels on a clear liner or opaque labels on an
      opaque liner.<br>
      Sensors engineered using capacitive or ultrasonic technology have no sensitivity to colour or contrast
      This improved sensor technology in label printers also makes the process faster and even more accurate. </p>
    <h3 class="headingPost">How A Clear Label Sensor Works</h3>
    <h4 class="blackheading">Capacitive Clear Label Sensing</h4>
    <p>Capacitive sensors use an electric field to sense the amount of material in the sensor. The electric
      field exists in the gap between a sensing element in the top of the sensor and the grounded baseplate of
      the sensor. Web material placed in that gap changes the way the electric field behaves — more material
      changes it more. The electronics in the clear label sensor can sense these changes in the electric field
      and this makes capacitive label sensors the most precise label sensors on the market.</p>
    <h4 class="blackheading">Differential Capacitive Clear Label Sensors</h4>
    <p>Differential sensing is achieved by using two sensing elements in the top of the sensor creating electric
      fields to the baseplate. When a label is in the sensor, both electric fields behave the same. When a gap
      enters the sensor, one of the electric fields over the gap changes but the other doesn’t. The sensor
      electronics detect this difference and signal the presence of a label to the print controller. This type
      of sensor only sees changes in thickness. For this reason, the sensor rarely needs adjustment because it
      doesn’t care if the label is thick or thin — it only sees the changes.</p>
    <p>Because of the way metallic materials interact with electric fields, if metallic artwork such as a foil
      logo enters the sensor, the sensor sees many differences between the two sensing elements as the artwork
      travels through the sensor. For this reason, labels using any metallic materials require a different
      sensing approach as set-out in the single-ended sensor version explained below. <img
                        class="img-responsive aligncenter size-full wp-image-1847"
                        src="<?=BLOG_ASSETS?>Screenshot_1-1.png" alt="" width="729"
                        height="270"
                        srcset="<?=BLOG_ASSETS?>Screenshot_1-1.png 729w, <?=BLOG_ASSETS?>Screenshot_1-1-300x111.png 300w"
                        sizes="(max-width: 729px) 100vw, 729px"></p>
    <h4 class="blackheading">Single-Ended Capacitive Clear Label Sensors</h4>
    <p>As the title above suggests this type of capacitive sensor has only one sensing element. It measures the
      absolute thickness of the material in the sensor. This type of sensor is more likely to require an
      adjustment between different labels in order to set the initial thickness measurement. After that, the
      sensor triggers the controller when the thickness exceeds a certain point; this point is set to trigger
      when the additional label thickness is added to the liner thickness. Once the thickness exceeds the trip
      point, any metallic material is ignored by the sensor because it only makes the label look thicker, but
      the sensor has already triggered. </p>
    <p><img class="img-responsive aligncenter size-full wp-image-1848"
                    src="<?=BLOG_ASSETS?>Screenshot_2.png" alt="" width="730" height="269"
                    srcset="<?=BLOG_ASSETS?>Screenshot_2.png 730w, <?=BLOG_ASSETS?>Screenshot_2-300x111.png 300w"
                    sizes="(max-width: 730px) 100vw, 730px"> </p>
    <h4 class="blackheading">Ultrasonic Clear Label Sensors</h4>
    <p>Ultrasonic sensing works much like single-ended capacitive sensing except instead of an electric field,
      it uses high-frequency sound energy. A transmitting transducer (“speaker”) in the base of the sensor
      transmits sound to a receiving transducer (“microphone”) in the top of the sensor. The label web reduces
      the amount of sound reaching the receiver. The thicker the web, the less sound at the receiver. The
      sensor electronics measure the sound pressure at the receiver and triggers the control when a label edge
      is detected. This technology has no sensitivity to metallic materials.<br>
      <img class="img-responsive aligncenter size-full wp-image-1849"
                     src="<?=BLOG_ASSETS?>Screenshot_3.png" alt="" width="754" height="288"
                     srcset="<?=BLOG_ASSETS?>Screenshot_3.png 754w, <?=BLOG_ASSETS?>Screenshot_3-300x115.png 300w"
                     sizes="(max-width: 754px) 100vw, 754px"><br>
      This is the latest application of technology and type of clear label sensor, but not necessarily the
      most reliably accurate for high speed printing, although accuracy is measured in parts of a millimetre.
      See the summary analysis below for more details.</p>
    <h3 class="headingPost">Clear Label Sensors &amp; Print/Application Speeds</h3>
    <p>Label sensor performance is a measurement of how accurately the sensor detects the edge of the label and
      how that accuracy is affected by speed. Label sensor performance is one consideration in determining
      which printer and sensor is best for your printing requirements.<br>
      Similarly, when used with automatic label application equipment, how accurately a label is located on a
      product and/or package is determined by many other factors like the mechanics of the machine and the
      other sensors responsible for detecting the package position. But label placement accuracy will never be
      better than the label sensor’s ability to accurately locate the label edge.</p>
