  <h3>Decorate your logo</h3>
  <p>A really simple but effective idea for the holiday season is to decorate your logo in a Christmas theme
    (think Google). This will involve using a graphics package such as Photoshop (if you’re not sure, you
    can pay a graphic designer) but once it is done, the image can be used year after year on websites,
    e-shots and other marketing materials.</p>
  <h3>Christmas yearly calendar</h3>
  <p>Yearly calendars make great customer gifts if you have a bit of spare cash to promote your products. The
    aim is that your customer will use the calendar and keep it on their desk, so your brand name is always
    in sight!</p>
  <p>So finally, when is the best time to start your Christmas preparations? Well, it doesn’t hurt to start as
    early as possible! People go travelling at Christmas, school children break up for the holidays and
    people plan ahead. To put it into perspective in fact, shoppers spent 70% more from email campaigns that
    were sent in November compared to December so bear this in mind and make the most of the festive
    season!</p>
  <p>*according to estimates by Professor Joshua Bamfield, a director at the Centre for Retail Research, a
    retail think-tank</p>
