  <h2 id="howto">How to use the Christmas label templates</h2>
  <p>The above templates are designed to work with the following label sizes (follow the links to the product
    page to buy):</p>
  <p><a href="http://localhost/a4-sheets/rectangle/t35/">For the rectangular address labels AAS021 63.5mm x
    38.1mm</a> This is also equivalent to Avery L7160 and J8160 – so if you already have these, they
    should work.</p>
  <p><a href="http://localhost/a4-sheets/circular/t83/">For the round Christmas stickers AAC035 35mm
    diameter</a></p>
  <p>You can choose matt white paper or even gloss white to create a shiny effect.</p>
  <p>To use the templates you’ll need Adobe Reader (a free software – if you read PDFs you’re likely to have
    it). Other PDF readers <em>may</em> work but if you want to edit the PDF and get the same printing
    results we do then it’s best to use Adobe Reader. Detailed guidelines on how to edit and print all our
    editable templates are <a title="How To Print and Edit Our Free Label Templates"
                                          href="https://localhost/blog/label-templates/print-edit-free-label-templates/">here</a>. </p>
  <p>The differences from the instructions given on that page are:</p>
  <p>1. With the Autofill Christmas Address Labels template you’ll need to enter the address you want repeated
    across the labels twice; once in the first column and once in the second. This is because the colour of
    the text is different.</p>
  <p>2. If you’d like to edit the font or colour, you need to delete the default content of the label first
    and then enter the new message/address <strong>before</strong> following the guidelines given in the
    link above.</p>
