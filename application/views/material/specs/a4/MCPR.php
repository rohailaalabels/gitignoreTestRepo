  <h3 >Applications and use </h3>
  <p>A  specially designed laser printable material which is very popular for product,  logistics and industrial applications, including variable information, where  label durability and removability is required, typically in retail for books,  greeting cards, electrical and IT items, also secondary promotional labels, but  equally useful for temporary guest and delegate badges. It can also be used in  address labelling or informational labels in difficult environmental conditions  and in SOHO (small office, home office) end uses for  removable applications.</p>
  <p>Ideal  for A4 and cut-size applications, with good removability, UV stability  (non-yellowing) and water resistance once labelled. Limited adhesion on very  rough surfaces and not recommended for labelling PVC substrates.</p>
  <h3>Conversion &amp; printing</h3>
  <p>Due to  the pre-treatment, the film is printable by common label printing methods like  letterpress, flexography, screen, offset lithography and digital printers  (copiers and laser). Special inks designed for non-absorbent materials should  be used.</p>
  <h3>Approvals</h3>
  <p>The research centre ISEGA has given an approval  according to the European requirements for direct contact with dry and moist  and fatty foodstuffs, which have a reduction factor of at least 4 according to  the regulation EU No. 10/2011. (ISEGA has an EN ISO 17025 accreditation for analytics and EN 45011 for  certification)</p>
