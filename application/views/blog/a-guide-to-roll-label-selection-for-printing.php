    <h3 class="headingPost">Thermal Printers</h3>
    <p>This is a digital printing process which produces a printed image via a heated print-head either applied
      directly to the paper or Polymer label face-stock, which has been coated with a thermochromic chemical,
      or by applying heat from the print-head to a resin and/or wax ribbon which then deposits residue from
      the ribbon onto the label.</p>
    <p>Many thermal printers will work in both modes i.e. as a direct and thermal transfer printer, providing
      that a ribbon has been loaded and these dual functionality models tend to commonly use the letter “T” at
      the end of the model number to denote that there is also a thermal transfer mode e.g. Zebra GK240D
      (Direct-Thermal mode only) &amp; GK420T (Direct-Thermal &amp; Thermal-Transfer modes).</p>
    <h3 class="headingPost">Direct Thermal Printing</h3>
    <p>Direct thermal printers use a chemically treated label material (paper and Polymer) that blackens when
      the thermal print-head applies heat to its surface.<br>
      <img class="img-responsive aligncenter size-full wp-image-1819 col-lg-12"
                     src="<?=BLOG_ASSETS?>rollimage.jpg" alt="" width="690" height="334"
                     srcset="<?=BLOG_ASSETS?>rollimage.jpg 690w, <?=BLOG_ASSETS?>rollimage-300x145.jpg 300w"
                     sizes="(max-width: 690px) 100vw, 690px"></p>
    <p><strong>Brief Summary</strong></p>
    <ul class="ribbon_li">
      <li>Requires no ink, toner, or ribbons.</li>
      <li>Prints in black only.</li>
      <li>Sensitive to UV light and age will fade the printed image.</li>
      <li>Cost effective and cheaper than Thermal Transfer because there are no additional consumables
        (ribbons). </li>
      <li>Minimal maintenance required or replacement of consumables.</li>
      <li>Not recommended for high-contact use.</li>
      <li>Commonly used for short-life barcode and variable data applications such as address labels, barcode
        labels, delivery labels, name-tag labels, pricing labels, receipt labels, shipping labels etc. </li>
    </ul>
    <p>Because heat is used to create the image, by heating the thermal sensitive coating, direct thermal
      printers don’t require ink, toner, or ribbons. An advantage of this technology is low maintenance costs,
      as with relatively few moving parts, there is theoretically less to require maintenance and/or
      replacement and also easy to operate and use.</p>
    <p>However, while the foregoing can be interpreted as benefits this printing technology is not without some
      constraints. Mainly that the image is not permanent and will fade over a period of time. Because the
      paper is heat-sensitive, additional heat or excessive light can cause the entire paper to darken,
      rendering any text unreadable and barcodes non-scannable. For this reason, direct thermal printing is
      mainly used for labels with a limited period of usage i.e. POS, delivery and shipping labels etc.
      Another consideration is that the image and text created is black, but coloured branding and graphics
      can of course be pre-printed leaving only the variable data and/or information to be printed in black on
      a white or coloured background.</p>
    <h3 class="headingPost">Thermal Paper</h3>
    <p>These papers for use in Direct-Thermal printers are produced to provide rich, legible image and text at
      all print speeds for excellent readability.</p>
    <p>The surface of the paper is coated with a solid-state mixture of a dye and a suitable matrix. The coating
      includes four different types of chemicals:</p>
    <h4 class="blackheading">Leuco Dyes</h4>
    <p>These can be in two chemical forms, one of which is colourless. The dyes used in thermal paper are
      usually Triaryl Methane Phthalide (TMP) dyes or Fluorian dyes or Crystal Violet lactone. These dyes have
      a colourless Leuco (white or lacking colour) form when crystalline or when in a pH neutral environment,
      but become coloured upon melting or when exposed to an acidic environment.</p>
    <h4 class="blackheading">Developers</h4>
    <p>Developers are basically organic acids which are also melted along with Leuco dyes in order to provide
      more colour. Examples of such developers include Phenols, Sulfonyl Urea’s and Zinc salts of substituted
      Salicylic acids.</p>
    <h4 class="blackheading">Sensitizers</h4>
    <p>Sensitizers are also added to the paper surface to optimize the colourisation temperature and to
      facilitate mixing of developers and Leuco dyes upon melting. Sensitizers are commonly simple Ether
      molecules.</p>
    <h4 class="blackheading">Stabilizers</h4>
    <p>Stabilizers are added to the layer of glaze formed by the Leuco dye, developer and sensitiser and prevent
      the dyes from returning to their original colourless, crystalline forms when stored in hot or humid
      conditions.</p>
    <p>In addition to the thermochromic coating explained above thermal papers can also have protective coatings
      over this to protect against abrasion (the heat generated from which is sufficient to darken the paper),
      grease and oils from foodstuffs. This is called a “top coat” and provides improved resistance and
      protection to the label and the printed information.</p>
