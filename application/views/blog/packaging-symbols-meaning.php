              <p><span class="tt-highlight highlight-cool-blue"><span class="highlight-style-1">12. Dispose of waste carefully</span></span><br>
                <a href="http://www.aalabels.com/blog/wp-content/uploads/B9Q12-dispose-waste-carefully.jpg"><img
                                                    class="alignleft size-thumbnail wp-image-211"
                                                    alt="dispose waste carefully"
                                                    src="https://www.aalabels.com/blog/wp-content/uploads/B9Q12-dispose-waste-carefully-150x150.jpg"
                                                    width="150" height="150"></a><br>
                This symbol shows that it is necessary to dispose of this packaging at a
                suitable site, and that the waste should be disposed of with care in a waste
                receptacle.</p>
