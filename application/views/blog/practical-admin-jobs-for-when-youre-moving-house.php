  <p>Moving house is right up there alongside arranging a wedding day as one of the most stress-inducing
    endeavours we willingly put ourselves through. But having said that, with just the right amount of
    planning and organisation, moving day needn’t be anything other than exciting.</p>
  <p>Aside from the obvious tasks such as getting everything you own packed, figuring out the new commute and
    arranging new schools, there are also whole numbers of other nitty gritty details that need your
    attention.</p>
  <p>With that in mind, here are a few practical admin tasks you may want to consider to make your life that
    little bit easier when it comes to the big day</p>
  <h2 class="headingPost">Let others know you’re moving</h2>
  <p>First things first, it’s important to let all the relevant people know that you’re leaving your current
    home, and to give details on the new address. </p>
