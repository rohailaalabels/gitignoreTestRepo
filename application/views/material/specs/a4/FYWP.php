  <h3>Applications and use </h3>
  <p>Eye catching (High Vis.) labels for pricing, display, promotions, product ID, location ID and hazard warning labels and signage.</p>
  <h3 >Approval</h3>
  <p>The fluorescent yellow face-stock complies with EN 71-3 on Toy Safety.</p>
  <p>The adhesive complies with Direct Contact with Foodstuffs from ISEGA (Adhesives). Also EN 71-3 & 71-9 on Toy Safety and the Packaging & Packaging Waste Directive 94/62/CE.</p>
  <p>The release-liner complies with the Contact with Foodstuff BfR XXXVI for face-stock and liners (Paper) directive. EN 71-3 for Toy Safety, Electric & Electronic Equipment RoHS and the Packaging & Packaging Waste Directive 94/62/CE.</p>
  <h3 >Shelf Life</h3>
  <p> Two years under storage conditions as defined by FINAT (Federation Internationale des Fabricants et Transformateurs d’ Adhesifs et Thermocllants sur papiers et autres supports) 20° - 25° C and 40 – 50% RH.</p>
