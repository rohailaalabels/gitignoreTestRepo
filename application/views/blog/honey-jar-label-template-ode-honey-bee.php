  <p>The design sticks to traditional amber/yellow colours often associated with honey labelling. Subtle
    hexagonal shapes make up the background while nature makes its appearance with the daisies and of course
    the bees. The word ‘Honey’ is appropriately penned as if written with honey – golden, thick and
    dripping. The editable text is given a rustic feel as if written on yourself.</p>
  <p><strong>Label Template Types:</strong></p>
  <p>There are four templates you can download. Two for the jar, two for the lid. The two types are called
    ‘autofill’ and ‘fillable’.</p>
  <p><strong>Autofill</strong>: When you edit the text in a field it automatically fills in the corresponding
    field across the whole template. This saves time if you want all the labels the same but you can’t have
    different text in each of the labels. That’s why you might sometimes want a fillable template.</p>
  <p><strong>Fillable</strong>: With this template you can enter different text for each label.</p>
