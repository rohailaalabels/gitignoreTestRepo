                    </div>

                    <div class="col-xs-12 col-sm-12 col-md-12">
                        <p class="text-justify">The Harmonized System is used to ease global trade by creating unified
                            categories to classify different types of goods.</p>
                        <p class="text-justify">The Harmonized System categorizes about 5,000 commodity groups in simple
                            eight to ten-digit codes, broken down into 21 Harmonized System sections, 96 Harmonized
                            System chapters, and thousands of headings.
                        </p>
                    </div>


                    <div class="m-t-10 col-xs-12 col-sm-12 col-md-12">
                        <h2 class="BlueHeading">HOW ARE COMMODITY/HS CODES USED?</h2>
                        <p class="text-justify">
                            Commodity or HS Codes are used by customs and logistics providers for a variety of purposes,
                            from calculating duties and tariffs to ensuring that an importer is not importing banned or
                            hazardous products. The true value of these codes is how globally recognized they are.
                            Whether you’re importing self-adhesive paper labels to the United States or Africa, the
                            first four digits of the code will usually be 4811 or 4821.
                        </p>

