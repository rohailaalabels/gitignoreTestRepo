  <p>If you’ve arrived here you’re most likely a teacher looking for a quick, easy to use homework stickers.
    This set of homework sticker templates was created especially with you in mind.</p>
  <p>They will save you time. If you have a class of 30 pupils you’ll need to copy and paste 29 times if
    you’re currently using a Word template to create your homework stickers. There’s no need to copy and
    paste with these templates – it does that for you! See for yourself:</p>
  <p><a href="<?=BLOG_ASSETS?>homework-sticker-gif.gif"><img
                            alt="Homework sticker demo"
