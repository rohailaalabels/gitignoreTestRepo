    <p>There are steps you can take to choose the right palette for your new brand.</p>
    <h2>1. Create a short list of the emotions/words associated with your brand/products</h2>
    <p>Some can be general emotions or descriptive words which would also describe your competitors’ product.
      For example, if you had an organic skincare range some of these words might be ‘Earthy’, ‘Gentle’,
      ‘Healthy’, ‘Natural’. One or two of the words however might be your specific to your point of
      difference. For example if the point of difference for your new organic skincare range is ‘more
      contemporary and trendy than your average organic skincare range’, the words ‘contemporary’ and ‘chic’
      might make it in to your list also.</p>
    <h2>2. Choose the colours that best suit these words</h2>
    <p>Use this Colour ‘Wheel of Emotions’ as a guide to choose the colours that pair nicely with your list of
