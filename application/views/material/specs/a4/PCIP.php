  <h3 >Applications and use </h3>
  <p>This material is perfect for applications that  require a degree of flexibility from the label (e.g. soap and shower gel  containers). A particularly good choice for primary brand and product labels on  flexible and rigid containers where it is important for the content to be  visible and not partially obscured by a label. Also secondary promotional  labels, informational labels, external labels/signage and safety labels/signage  etc.</p>
  <h3>Approvals</h3>
  <p>The adhesive complies with the European food  directives and legislation FDA 21; CFR 175, 105 and the German recommendations  XIV as published by BfR (Bundesinstitut fur Risikobewertung)  the German Federal Institute for Risk Assessment. Both of which state that the  adhesive can be used in direct contact with dry, moist and non-fatty  foodstuffs, however according to the FDA regulations the adhesive must be  separated from the foodstuffs by a functional barrier.</p>
  <h3 >Shelf Life</h3>
  <p>Two years under storage conditions as  defined by Premier Coating &amp; Converters Ltd 20° - 28° C and 40 – 60% RH.</p>
