      <h3 >Applications and use </h3>
      <p>Ideal for the labelling of cryogenically stored biological information and samples in various formats. Overprinting of variable data and updating of information via conventional hand written means. Also used for labelling items where heat is generated, such as PCB’s, automotive engine parts and heating systems etc. Along with chemical drum and logistics applications. Due to the exceptional thickness and stability large label sizes can be used.</p>
      <h3 >Conversion & Printing</h3>
      <p>The labels can be overprinted by colour digital offset, hot and cold laser, dot matrix, laser and thermal transfer printers.</p>
      <p>Due to the surface properties of the face-stock the Polyethylene film is pre-printable by screen, offset, flexography, letterpress, electro-photography, electro-graph, digital offset and dot matrix. Inks for filmic substrates are recommended. Also thermal transfer with selected ribbons.</p>
      <h3>Approvals</h3>
      <p>The ISEGA research centre has approved the adhesive RP Cryo according to European requirements for direct contact with dry and moist, non-fatty foodstuffs, as well as fatty foodstuffs with a correction factor of at least 2, or for which the foodstuff stimulant D1 applies according to Regulation (EU) No. 10/2011. In addition labels coated with this adhesive may be used to label food packaging materials. According to FDA regulations RP Cryo may come into direct contact with dry foodstuffs, or it must be separated from foodstuffs by a functional barrier. RP Cryo is approved from the patient safety point of view (migration tested) for labelling blood bags ISEGA has EN ISO 17025 accreditation for analytics and EN45011fro certification.</p>
