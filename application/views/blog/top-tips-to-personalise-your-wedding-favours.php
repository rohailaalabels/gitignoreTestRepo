  <p>In the build up to your wedding day there are going to be so many things to think about that when it
    comes to prioritising what to tackle next, you may find that the little details need just as much
    attention as the big stuff.</p>
  <div class="row">
    <div class="col-sm-7">
      <p>One such essential is the humble wedding favour. This central decoration of the wedding reception
        is your chance to offer guests a keepsake reminder of the day, as well as providing an
        opportunity to show your nearest and dearest that you really appreciate their support. Not only
        that, but wedding favours are also a common tradition, as synonymous with weddings as bouquets,
        first-dances, speeches and embarrassing drunken relatives.</p>
    </div>
    <div class="col-sm-5">
      <p>But all too often these tokens end up being thrown away (or worse, left on the table!), so what
        can you do to guarantee that they’ll get noticed and cherished? Here are a few tips for picking
        the perfect favours, and what you can do to personalise them for your guests.</p>
    </div>
  </div>
  <h2 class="headingPost">General tips</h2>
  <div class="row">
    <div class="col-xs-6 col-lg-4 col-sm-4 pull-right"><img class="img-responsive col-xs-12 pull-right"
                                                      src="<?=BLOG_ASSETS?>ywf-1.jpg"></div>
    <div class=" col-lg-8 col-sm-8">
      <p>If you opt to get extra crafty and make your own favours, it’s important to remember that it’s
        going to take a lot of work! So first things, first, be sure that whatever it is you’re making
        is the right choice. Make a few samples first to help gauge if you’re on the right track, and
        then why not enlist the help of a few friends?</p>
      <p>Whether you choose to give one favour per guest, or per couple, make sure that you have a good
        number of extra ones ready as backups in case some get lost or damaged before the big day.</p>
    </div>
  </div>
  <h2 class="headingPost">Winning favour with your guests</h2>
  <div class="row">
    <div class="col-xs-6 col-lg-4 col-sm-4"><img class="img-responsive col-xs-12"
                                           src="<?=BLOG_ASSETS?>ywf-2.jpg"></div>
    <div class="col-lg-8 col-sm-8">
      <p>When it comes to favours you can be as creative and as inventive as you like, and just like any
        gift, you’ll want to put in some extra thought into your selection. An important point to
        consider before you spend a lot of money on 200 personalised photo key rings, is that you have
        to ask yourself the question – Is this something that I would enjoy? Ideally, you should always
        land on something that you’d be happy to receive yourself.</p>
    </div>
  </div>
  <p>Photo booths have become a popular choice for wedding receptions these days, and sure are a lot of fun as
    the evening progresses and everybody starts to warm up. If you choose to have one at your wedding, then
    this is a good opportunity to have some fun with your favours. You could choose to supply your guests
    with small fancy dress favours such as stick-on moustaches and silly glasses; this will get people
    talking and make for some interesting (and personalised) photos.</p>
  <h2 class="headingPost">Incredible edibles</h2>
  <div class="row">
    <div class="col-xs-6 col-lg-4 col-sm-4 pull-right"><img class="img-responsive col-xs-12 pull-right"
                                                      src="<?=BLOG_ASSETS?>ywf-3.jpg"></div>
    <div class="col-lg-8 col-sm-8">
      <p>You can always go down the route of choosing sweets and snacks as favours, which are a fail-safe
        option that your guests can enjoy on the day, or to be taken away with them to enjoy later. You
        could bake some cookies, or flapjacks, you could make chocolate truffles or mini jars of
        homemade lemonade (with quirky <a href="#">personalised labels</a>). To save money on this you
        could incorporate part of your hen celebrations by hosting a favour-making party; this way you
        can have fun at the same time as keeping the costs down.</p>
    </div>
  </div>
  <h2 class="headingPost">Something more practical perhaps?</h2>
  <div class="row">
    <div class="col-xs-6 col-lg-4 col-sm-4"><img class="img-responsive col-xs-12"
                                           src="<?=BLOG_ASSETS?>ywf-4.jpg"></div>
    <div class="col-lg-8 col-sm-8">
      <p>You could also go with something practical such as photo frames or luggage tags, both of which
        can easily be personalised. As a heartfelt alternative to traditional favours, you could even
        opt to donate the same money you would have spent on a charity donation instead. Many charities
        will often send pins, badges or cards for the guests explaining that money has been donated in
        replacement of a favour.</p>
    </div>
  </div>
  <h2 class="headingPost">Get personal</h2>
  <div class="row">
    <div class="col-xs-6 col-lg-4 col-sm-4 pull-right"><img class="img-responsive col-xs-12 pull-right"
                                                      src="<?=BLOG_ASSETS?>ywf-5.jpg"></div>
    <div class="col-lg-8 col-sm-8">
      <p>When thinking about personalised wedding favours, it’s a nice idea to think of something that
        expresses your unique personalities, and really speaks to your guests. Are you both into wine?
        Mini bottles of your favourite <a href="#">wine with personalised labels</a> are sure to be a
        big hit. Maybe look to the season for inspiration – for spring, a great idea is to send guests
        home with potted plants or flowers with growing instructions, or a warming hot chocolate kit for
        the winter months.</p>
    </div>
  </div>
  <div class="row">
    <div class="col-xs-6">
      <h2 class="headingPost">Don’t over do it</h2>
      <p>Whatever you eventually choose there is one golden rule that can’t be ignored, and that is you
        should try to avoid putting your initials/names/photos or wedding date on absolutely everything.
        This is a sure way of turning an otherwise useful trinket into something a touch cringe worthy.
        For example, a personalised calendar makes for a fine, practical gift, as is a key ring
        customised with a picture of your first holiday together, but truth be told, not everybody is
        going to want to look at it every day (except for your mum of course).</p>
    </div>
    <div class="col-sm-6">
      <h2 class="headingPost">Personalised packaging</h2>
      <p>The one exception to this rule is packaging – it’s absolutely fine to put your personal touch on
        everything from the boxes and bags, to the ribbons, wrapping and labels. Not only will this make
        for lovely presentation, it also won’t put your guests off!</p>
      <p>At the end of the day, whatever you choose as your wedding favour, be sure to have fun, and
        remember that the personal touch is bound to make an impression.</p>
    </div>
