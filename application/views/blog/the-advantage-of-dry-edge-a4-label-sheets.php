        The small amounts of adhesive transferred onto the transfer and fusing rollers initially causes
        problems with the print output quality by transferring onto subsequent sheets, which can assist
        further migration across the effected rollers and will also pick-up toner and transfer this onto the
        labels, ruining the print quality with image ghosting (shadow) or worse and potentially will also be
        visible on future print runs.
        <p></p>
        <p>Adhesive on the rollers will also pick-up dust and fibre from the substrates being printed and
          can deposit this onto the print surface, which when brushed off the printed surface will then
          leave small white areas without print coverage, often giving the printed labels a speckled
          appearance. </p>
        <p>Also, while the adhesive now on the rollers is still wet, this can result in labels actually
          being lifted from the sheet and adhering to the roller. Which If this is the fuser roller, will
          potentially result in areas of missing print on the label sheet and will require the print-run
          to be stopped while the label is removed and the roller cleaned.</p>
      </div>
        <img style="float: left;" src="<?=BLOG_ASSETS?>area2-300x167.jpg"
             alt="area2" width="300" height="167" class="alignleft size-medium wp-image-1775 col-lg-3"  >
      <p>Any amount of adhesive transferred from label sheets
        onto the rollers, even the small transport rollers needs to be cleaned, because the distances of the
        upper and lower roller sets are calibrated tolerances for the thickness of the varying substrates
        being printed e.g. standard matt paper, coated papers, labels, photopaper etc and if the adhesive is
        left to harden, the tolerances can be compromised and the rollers are then not correctly controlling
        the substrate in the transport path. When this happens even fractional movement through lack of grip
        can cause misalignment of the sheets taken from the printer feed-tray, with poor print registration
        as a consequence.</p>
    </div>
    <p>Eventually if left unattended adhesive deposited onto rollers can also cause paper jams with subsequent
      non-label print runs.<br>
      <img style="float: right" src="<?=BLOG_ASSETS?>dry3.gif" alt="dry3"
                     width="279" height="280" class="alignright size-full wp-image-1761"><br>
      When this happens, there is no choice other than to stop the print run and attempt maintenance by
      cleaning the rollers concerned. Some OEM’s produce a cleaning kit for their printers and in every case,
      you should always refer to the printer manual and/or user guide. Assistance with this can often be found
      on OEM’s websites and customer support forums.</p>
    <p>Once cleaning of the rollers is completed, it is best practice to always first test by using a blank
      sheet of paper run through the printer and check to ensure that it emerges clean and free from glue and
      toner marks. </p>
    <p>The problem is similar with desktop inkjet printers, if slightly lessened in propensity by the absence of
      so many full-width rollers because of the different technology. Inkjet is a contactless print method,
      with a moveable set of print-heads and does not therefore require an image transfer roller and a set of
      fuser rollers.<br>
