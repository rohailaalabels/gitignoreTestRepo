                          <h3 >Applications and use </h3>
                          <p>A specially designed laser printable material which  is very popular for product, logistics and industrial applications, including  variable information, where label durability and removability is required, typically in retail for books,  greeting cards, electrical and IT items, also secondary promotional labels, but  equally useful for temporary guest and delegate badges. It can also be  used in address labelling or informational labels in difficult environmental  conditions and in SOHO (small office, home office) end uses  for removable applications.</p>
                          <p>Ideal for A4 and cut-size applications, with good  removability, UV stability (non-yellowing) and water resistance once labelled.  Limited adhesion on very rough surfaces and not recommended for labelling PVC  substrates.</p>
<h3>Conversion &amp; Printing</h3>
                          <p>Due to the  pre-treatment, the film is printable by common label printing methods like  letterpress, flexography, screen and offset as well as copier and laser toner  printers, but in all cases inks designed for non-absorbent materials should be  used.</p>
<h3>Approvals</h3>
                          <p>The research  centre ISEGA (EN ISO 17025 accreditation for analytics and EN 45011 for  certification) has given the adhesive and approval according to the European  requirements for direct contact with dry and moist, non-fatty foodstuffs and  according to FDA regulations adhesive may come into direct contact with dry  foodstuffs, or it must be separated from the foodstuffs by a functional  barrier. Furthermore the adhesive may come into contact with poultry, dry  foodstuffs as well as fruit and vegetables. </p><p>
                          The adhesive  is also in compliance with the demands of the Code of Federal Regulations, Food  &amp; Drug Administration (FDA) 21 CFR Ch. 01 (01.04.2012) 175.105 &amp;  175.125</p>
<h3 >Shelf Life</h3>
