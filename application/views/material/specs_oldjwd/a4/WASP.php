                          <h3 >Applications and use </h3>
                          <p>This face material is designed for the manufacture of labels on rolls and sheets for use in inkjet, laser (Because of the aluminium coating on the back of the paper face-stock, some makes/models of laser printers can have difficulty printing these label sheets. Therefore it is always advisable to request a sample before ordering.) and thermal transfer printers (uncoated white only). The surface structure provides excellent ink, resin/wax and toner bonding, with good quality print resolution.</p>
                          
                          <p>This product is ideal for label applications where high initial tack and strong adhesive bonding is required because of silicone protective coating, such as new rubber tyres, automotive body parts , stainless steel sink units and waxy fruits, such as Honeydew melons. Product labels that require total opacity and barrier effect.</p>
                          
                          
                          
<h3>Approvals</h3>
                          <p>The research centre ISEGA has given the adhesive (TT-3N) approval according to the European requirements for direct contact with dry and moist, non-fatty foodstuffs. The adhesive is in compliance with the rules of the Code of Federal Regulations, Food and Drugs (FDA), 21 CFR Ch.I (April 1, 2014 edition), 175.105. According to the FDA regulations, the adhesive may come into direct contact with dry food-stuffs or they must be separated from the foods by a functional barrier.</p>

