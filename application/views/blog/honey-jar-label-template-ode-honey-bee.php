  <p><strong id="designer">Here’s our designer’s take on it (written while watching The Great British Bake
    Off):</strong></p>
  <p>Here in Blighty, we’re in the middle of the wettest decade in over a hundred years. From drizzle to
    downpoor – we’ve been treated to more rain than we know what to do with.</p>
  <p>But this is good! Do you know what rain brings?</p>
  <p>Together with it’s friend sunshine, rain brings growth! They team up to give us flourishing fauna,
    plentiful pastures and verdant verges all abundant with colour, life and vitality.</p>
  <p>Daffodils, daisies, dalias. Sunflowers, strawberries, snowdrops. They all love a bit of rain and
    sunshine. But they’d be nowhere without an extra added ingredient. That little spark that really gets
    things ticking.</p>
  <p>The humble bee.</p>
  <p>That tireless worker clad in black and yellow, she slaves away night and day pollenating our countryside,
    spreading the love between one flower and its blossoming love on the other side of the meadow.</p>
  <p>So, Miss Bee – we thank you for this. Without you, we’d be without some of the most beautiful sights
    nature has to offer.<br>
    But this isn’t why I love you. I love you for that byproduct of all your graft. The fruits of your
    labour. That gold you work so hard for. YOUR HONEY!</p>
  <p>MMMM HONEY. Liquid gold, spread on a bit of toast. Stirred into the morning porridge, bringing flavour to
    that plainest of staple. A cheeky spoon into the mouth when no one is looking – absolutely impossible to
    do without that involuntary grin spreading across the face.</p>
  <p>So here’s to the Bee. God bless you, you wonderful little creature!</p>
