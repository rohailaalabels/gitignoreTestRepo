      and the final review and approval process to reach success.</b></p>
    <p>So, we’re busy sourcing a label. We’ve done our research, we’ve made a checklist, and we’ve called the
      experts in. What’s next?</p>
    <h4><b>Size matters</b></h4>
    <p>Now that you’ve knuckled down your checklist of things you might want or like, you need to think
      carefully about material and size. This will of course depend on what the label is being attached to, as
      well as how noticeable you want your label to be. I used to work within the clothing/retail business, so
      a big consideration for me was ‘will the label be sewn all the way around by a machine, or will it be
      hand sewn around the edges’? It would all depend on how delicate the piece of clothing was that it was
      being attached to.</p>
    <p>If you are attaching a label to some packaging (say, a box), and you want it to go on the outside, you
      might want something high gloss, to give a high-end effect that is highly noticeable. If it is going
      inside, you might want something more edgy, like a brown parcel paper label (this would be especially
      great if the product is eco-friendly). Maybe you’d prefer to ‘hide’ the label underneath, whereby you
      might choose something more matt.</p>
    <p>Of course, the size of the label will depend on what exactly you need on there. I would recommend keeping
      a label as ‘clutter free’ as possible, so be minimalist and only include the information you really
      need. Product labels might only include images or logos – don’t feel like you need to add lots of text
      just to make the space up. Perhaps you need your label to include instructions for use? If you export,
      make sure it works in other countries. The last thing you need is to print several different versions of
      a label, as costs can soar. Consider including different languages on one label, rather than printing
      separate ones for each.</p>
    <p>Colours are another consideration. You will pay more for more colours, so if your logo includes five
      different colours, you might want to consider creating a more simplified version!</p>
    <h4><b>Branding</b></h4>
    <p>Something to consider when creating your label is does it fit the brand? Is it consistent with other
      products? Do you want it to be?</p>
    <p>Some really great examples of packaging labels are Apple, and Innocent Drinks.</p>
    <p>Apple’s packaging is so simple – a plain white outer with a small, silver Apple logo. Surely that concept
      took less than five minutes to think up? No? Apparently, a lot of work was involved:</p>
    <p><i>“One after another, the designer created and tested an endless series of arrows, colours, and tapes
      for a tiny tab designed to show the consumer where to pull back the invisible, full-bleed sticker
      adhered to the top of the clear iPod box. Getting it just right was this particular designer’s
      obsession.”</i><i></i></p>
    <p> <b><a href="https://gizmodo.com/5879097/apple-packing-is-so-good-because-they-employ-a-dedicated-box-opener">https://gizmodo.com/5879097/apple-packing-is-so-good-because-they-employ-a-dedicated-box-opener</a> </b></p>
    <p>Innocent drinks is at the opposite end of the spectrum, filling their labels with ingredients, logos,
      contact information and even special poems or quirky facts. One of the more peculiar versions I have
      seen is:</p>
    <p><i>‘Rules for riding a dinosaur’ </i><br>
      <i>1. Never make eye contact with it. It gets very cross. Same goes for trying to pop a fez on its
      head. </i></p>
    <p>It certainly gets people reading their labels and recognising their brand!</p>
    <h4><b>Lead time</b></h4>
    <p>Last week, I touched on the importance of timing. Lead time on a label will make a difference. You might
      decide you want that luxury label with a number of design features, text, images and colours, but if you
      can’t make it happen in time for your project launch… well, it’s back to the drawing board.
      Literally.</p>
    <h4><b>Testing, testing</b></h4>
    <p>Of course, testing a product label is important, we all know that right? But when I talk about testing, I
      don’t just mean asking someone to try and peel the sticker off and if they can’t, it works. I mean
      actually use the product regularly! Use it, touch it, open it, close it, rub it, pull it, draw on it,
      full on attack it!! Do what you have to do to make sure it is durable and does what it is meant to
      do.</p>
    <h4><b>Review and final approval</b></h4>
    <p>So we’re almost done, but not before we do our final review and approval process. This might involve
      getting back together with a number of departments and sending out ‘field trials’ of a product to test
      its effectiveness. It also involves collating technical specifications and information ready for
      customers to view.</p>
    <p>Not only do we need to know if the product label really works, more importantly, we need to know if we
      would do this same process again, and what we might do differently. Hindsight is a wonderful thing, so
      use it!</p>
    <p>Some examples of what you might evaluate the product for could be:</p>
    <p>Cost effectiveness – is the product good value for money.</p>
    <p>Use – does the product fit the use of the target group / does the product reach the standards of the
      target group.</p>
    <p>Clarity – can the target group evaluate the product and its use based on the information provided by the
      supplier.</p>
    <p>And once you’ve done all that, and successfully brought this project to market, you can breathe a sigh of
      relief, pat yourself on the back, and start the process all over again!</p>
