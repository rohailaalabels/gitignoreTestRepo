  <p>It can be daunting when deciding if barcodes are right for your business. Don’t be discouraged though, as
    for many smaller organisations there is a simple guide to understanding how the barcode system
    essentially works, and why it works so well.</p>
  <p>Barcodes as we know them were first introduced commercially in the 1970’s, and have developed into a
    universal system worldwide, with an estimated 5 billion barcodes read by scanners each day.</p>
  <p>The consumer benefit of barcodes is clear to see, in the fact that we can walk into our supermarket or
    local shop and pick up what we want with ease. We can now even scan items ourselves at self-service
    checkouts.</p>
  <p>Retailers are able to gain better insight into customer spending patterns and trends, allowing them to
    become superior at forecasting and planning their stock holding (as well as cutting the number of staff
    they have to employ).</p>
  <h3>How do I create a barcode?</h3>
  <p>Whatever size business you operate, there can be real savings and benefits to utilizing the barcode
    system. There are many different styles of barcodes, and your means of application would affect which
    one you would choose. However, one of the most recognised barcode types is the EAN/UPC code, which is
    specified for retail point-of-sale, but can be used for other applications, such as on product
    packaging. EAN stands for “European Article Number” and UPC for “Universal Product Code”. The UPC code
    is widely used in the United States and Canada.</p>
  <p>In order to use an EAN code, a business or company has to apply to join a standardised barcode system,
    like GS1 UK: <a href="http://www.gs1uk.org/Pages/default.aspx" target="_blank">http://www.gs1uk.org/Pages/default.aspx</a> . The GS1 system of standards is the most widely used supply chain standards system in the world. GS1
    “manage a global system that allows companies all around the world to globally and uniquely identify
    their physical things like trade items (products &amp; services), assets, logistic units, shipments, and
    physical locations and logical things like a corporation or a service relationship between provider and
    recipient.”, essentially helping businesses work more efficiently and with greater traceability.</p>
  <p>For an annual fee, your business will receive a “Company prefix” – a number which relates to only you,
    and allows you to quickly and easily generate your own barcode numbers through their website. A 13-digit
    EAN barcode is split into the company prefix, your product code and something called a check digit. Once
    you have been assigned your company prefix, the process to generate a new barcode number can take less
    than 5 minutes using their handy online calculator <a
                        href="http://www.gs1.org/barcodes/support/check_digit_calculator" target="_blank">http://www.gs1.org/barcodes/support/check_digit_calculator</a>! </p>
  <h3>How do I get the barcode from the computer on to my packaging or product?</h3>
  <p>GS1 UK can generate your barcode image for you online, and when it comes to printing barcodes, this can
    either be outsourced to a specialist printing firm or they can be printed in-house on generic labels.
    This is often a flexible way for small businesses to work with barcodes cost effectively.</p>
