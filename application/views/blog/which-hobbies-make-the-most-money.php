  <h2 class="headingPost">Darn Good Yarn</h2>
  <p>Nicole Snow’s passions have always been art and helping others. After finding a silk yarn supplier, she
    decided to create Darn Good Yarn, a fiber importer and retailer. Not only did she use the yarn and her
    knitting ability to make her own colourful creations, she worked with others to refine the yarn and help
    other crafters by providing quality raw materials. Nicole’s efforts have seen her business grow from a
    modest single employee to over 300 women, and over 4.5 years, her business has grown by a whopping
    2900%!</p>
