  <p>There are hundreds of packaging symbols found on our everyday household goods, ranging from washing
    powder to make-up. These icons are essentially there to keep the user safe from harm and to stop
    spillages, improper storage or accidents from happening. Some are voluntary symbols but some are set out
    by law and a necessary part of your product packaging.</p>
  <p>But do consumers know the difference between these symbols, and do they actually understand the meaning
    of them?</p>
  <p>To find out, we surveyed a sample of the general public, with no prior manufacturing or packaging
    knowledge, and asked them to identify some symbols commonly found on packaging/labelling. The results
    were interesting!</p>
  <p>Out of the sample we surveyed, none of the respondents asked could name all of the 12 symbols correctly,
    and in fact, most of the respondents got over half of the descriptions wrong. There were also the people
    who knew ‚Äúroughly‚Äù what the symbols meant, (for example, that something with an ‚ÄúX‚Äù was some sort of
    hazard, but couldn‚Äôt name the exact meaning).</p>
  <p>More interestingly, 90% of those people questioned recognised the recycle symbol but only 40% recognised
    the shelf life. As this is a common symbol on items such as make-up and cosmetics, it is worrying that
    over half of people would not be aware that they need to dispose of the items after this time. With over <a href="https://premiumbeautynews.com/en/Lipsticks-a-safe-bet,675" target="_blank">800-900 million</a> lipsticks sold every year worldwide, that could equate to quite a number!</p>
  <p>So if people aren‚Äôt always aware of the impact an icon has on their product storage, life or well-being,
    do some of the icons need to be on there at all? Or is it a consumer‚Äôs obligation to do their research
    and make themselves familiar with the symbols?</p>
