                          <h3 >Applications and use </h3>
                          <p>Designed for general textile labelling applications where a degree of removability is needed. The high cohesion of the adhesive assists clean and stable performance during label conversion and dispensing. However not suitable for labelling silk, leather, suede and PVC fabrics.</p>
                        
                          
<h3>Conversion &amp; printing</h3>
<p>Suitable for flexography, offset lithography, letterpress, thermal transfer, dot-matrix and some inkjet and laser printers (we strongly advise always testing a sample before ordering if using an inkjet or laser printer).</p>


<h3 >Shelf Life</h3>
                          <p>Two years under storage conditions as defined by FINAT  <small>(Federation Internationale des Fabricants et Transformateurs d’ Adhesifs et Thermocllants sur papiers et autres supports)</small> 20° - 25° C and 40 – 50% RH. Prolonged storage at higher temperatures and/or humidity levels will shorten the shelf life.</p>
