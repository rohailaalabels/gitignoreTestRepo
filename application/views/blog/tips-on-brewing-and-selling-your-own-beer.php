  <p>Beer is said to be the third most popular beverage in the world, and it’s been with us for a very long
    time. Dating all the way back to 9000 BC in Ancient Egypt, beer remains a firm favourite, and there’s
    nothing quite like a cold beer on a hot day!</p>
  <p>The good news if you’re a fan of beer is that we’re currently going through somewhat of a renaissance at
    the moment. More and more people these days are looking to buy craft beers, as they are generally
    regarded as offering greater flavour variations than your bog standard mass-produced lager.</p>
