          <h2 class="headingPost">The time and place</h2>
          <p>If you’re looking to save money, do not get married on a Saturday, this is a prime day for most
            venues, which will charge accordingly. Also, look to get married towards the end of the peak
            season of spring and summer, or even off-season entirely. Besides, a winter wedding has it’s own
            unique charm.</p>
        </div>
      </div>
      <p>A long engagement means that you can book a venue long in advance. This is perfect because the cost will
        always increase as venues look to fill competitive dates. While you’re at it, think about having the
        ceremony and reception at the same place – this cuts down on travel time for any vendors you might
        use.</p>
      <p>In terms of the venue itself, maybe look for those who don’t require you to use all of their own
        services. Hotels are notoriously bad for this, as they usually insist that all food and drink goes
        through them, which is typically quite expensive.</p>
      <p>Choosing a venue that has it’s own beautiful features won’t need much decorating, and if you were to
        choose having your wedding in December then it may already look beautiful with all the festive
        decorations.</p>
      <p>In terms of the venue itself, maybe look for those who don’t require you to use all of their own
        services. Hotels are notoriously bad for this, as they usually insist that all food and drink goes
        through them, which is typically quite expensive.</p>
      <p>Decorating needn’t be expensive. Try using greenery and shrubs instead of traditional flowers, swap out
        costly bouquets for less expensive ones and stick to just one or two types of flower (it helps if they
        are in season as well). You could also simply opt for non-florals such as lanterns.</p>
