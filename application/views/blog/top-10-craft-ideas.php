        <h2 class="headingPost">Vintage Sign</h2>
        <p>This quirky craft idea is simple and versatile. Simply take a piece of wood (either new or
          salvaged) and paint over it using a whole host of colours, it doesn’t matter what as it’s only
          the first layer so the brighter the better. Then once that has dried, paint over it again with a
          single colour of your choice and allow it to dry fully.</p>
        <p>Now you get to either stencil or freehand a design of your choice, and once that is dry, take a
          piece of sandpaper and sand down the whole piece, you’ll be amazed at how the colours come
          through and give the whole thing a weathered, vintage look.</p>
