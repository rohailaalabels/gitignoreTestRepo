  <h3 >Applications and use </h3>
  <p>Very popular  for information labelling where label durability is important and over-printing  is required by laser or thermal transfer, such as &ldquo;hazchem&rdquo; labels where the  pictograms to be used within the printed red borders are interchangeable  dependent upon the container contents. Recommended for drum and logistics  industry labelling.</p>
  <p>Ideal for A4 and cut-size applications, with good  removability, UV stability (non-yellowing) and water resistance once labelled.  Limited adhesion on very rough surfaces and not recommended for labelling PVC  substrates.</p>
  <h3>Approvals</h3>
  <p>The research  centre ISEGA (EN ISO 17025 accreditation for analytics and EN 45011 for  certification) has given the adhesive and approval according to the European  requirements for direct contact with dry and moist, non-fatty foodstuffs and  according to FDA regulations adhesive may come into direct contact with dry  foodstuffs, or it must be separated from the foodstuffs by a functional  barrier. Furthermore the adhesive may come into contact with poultry, dry  foodstuffs as well as fruit and vegetables. </p>
  <p>This  product complies with the materials section of BS5609 Part 2 (Marine  Immersion), which is a recognised standard for self-adhesive labels for marine  use. It contains provisions to ensure that dangerous goods transported by sea  should be durably labelled and identified. This includes surviving immersion at  sea.</p>
  <p>Section  2 details the requirements for pressure-sensitive adhesive laminates and rates  their performance in terms of dimensional stability, adhesion, artificial  weathering, temperature cycling and colour fastness, together with exposure to  marine conditions for three months. </p>
  <p>Customers  converting labels themselves should independently determine the compliance of  the ink and print process involved in the production of labels to the British  Standard. </p>
  <h3 >Shelf Life</h3>
  <p>From the  date of manufacture 2 years at 20° C and RH50%. Prolonged storage at higher  temperatures and/or humidity levels will shorten the shelf in optimum condition  terms.</p>
