      <div class="col-xs-6 col-md-8 col-lg-9 col-sm-7">
        <p>If you want to get in to making your own jam, there are literally thousands of recipes and guides
          online which can help you decide on the flavours you want to create, as well as illustrate the
          jam-making process. Once you get going though you’ll undoubtedly want to push things further and
          really get experimenting.</p>
        <p>If however, one day you find that your newfound enthusiasm keeps growing and you’d like to maybe
          start selling your jam for profit, there are a number of factors that you need to bear in mind.
          As with any business, no matter the size, if you’re selling any food for consumption, you must
          comply with the rules and regulations of the Food Safety Act 1990, which covers all legislation
          regarding food.</p>
      </div>
    </div>
