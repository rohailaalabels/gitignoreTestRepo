  <p>A hobby can mean something different for everybody, whether it’s simply something meant as a distraction
    from the hustle and bustle of every day life, or done in the pursuit of a lifelong interest. <a
                        href="#">Some even take it to the next level</a> and explore ways to make a little extra money
    on the side. We’re looking at the ins and outs of the world of hobbies, and how you can go that extra
    mile and turn that interest or pastime into a viable business option.</p>
  <p>It’s clear to see that almost any hobby can be turned into a business providing you’re offering something
    that most people are going to want, and depending on your level of skill and how much spare time you
    have, there is definitely some scope for creating profit.</p>
