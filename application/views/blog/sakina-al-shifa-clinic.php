<p>
    As part of our corporate social responsibility (CSR) ethos the business has chosen to create and support a charity with particular importance to the directors of AA Labels. Sohail Sethi (MD) was born in the UK and at an early age experienced the loss of his mother to illness and subsequently spent his early formative years living with family in Lahore, Pakistan. Sohail went on to marry his wife Asifa Sethi (Director) whose family also originate from Lahore.</p>

<p>Following the strategic decision by AA Labels to locate and develop its accounts, IT and studio design teams in Lahore, a property was acquired for the purpose and the idea for a clinic to occupy the ground floor area was born.</p>

<p>The motivation was to assist with the alleviation of hardship associated with poverty through the provision of free medical services in a city that has connections and history for Sohail and Asifa.
