    <div class="col-xs-6 col-md-8 col-lg-9 col-sm-7">
      <p>Once you’ve settled on your favourite beer, and you’ve nailed the production stage, you’re going
        to be hugely proud and might want to start thinking of the next logical step. You need to ask
        yourself – do you want to keep it all to yourself and your loved ones? Or maybe, do you consider
        scaling up and selling it on?</p>
      <p>The first step is to decide on a fitting name for your beer, and then start bottling it and
        labelling it. You can opt for a flip-top cap, as these are reusable but expensive, or you could
        go for the traditional crown cork bottle top, these will need a bottle capping machine, or screw
        top bottles.</p>
    </div>
  </div>
  <p>For your first homebrew batch you can use the same glass bottles over and over again. This saves money on
    resources and energy, and then once you begin selling on a small scale, you could introduce a reward
    scheme for customers who return their bottles. However, things change when you want to start supplying
    shops and renting brewery space. In essence everything here hugely depends on how large a scale you want
    to take your beer making.</p>
  <h2 class="headingPost">Licensing requirements</h2>
  <p><img src="<?=BLOG_ASSETS?>b-5.jpg" class="img-responsive col-xs-12 col-md-12 col-lg-12 col-sm-12"></p>
  <div class="row">
    <div class="col-xs-6 col-md-6 col-lg-6 col-sm-6">
      <p>The size of your operation greatly affects the legality of home brewing as well. If you want to
        sell your own beer, the 2003 Licensing Act states that the sale of alcohol requires you to have
        a license. And wherever you make your beer will come under food standards trading requirements
        and be subject to inspections form the Food Standards Agency (FSA).</p>
    </div>
    <div class="col-xs-6">
      <p>Throughout the UK, the sale of alcohol is restricted, and your local authority must license any
        premises selling it. You can find your local authority here. The rules and regulations vary
        greatly depending on where the alcohol is being sold, and as a brewer you will really need to do
        your homework in order to adhere to the strict laws in place, as well as correctly pay your
        taxes.</p>
    </div>
  </div>
  <div class="row">
    <div class="col-xs-6">
      <p>Beer Duty tax is chargeable on any beer that has an alcoholic strength of over 1.2% alcohol by
        volume (ABV), and if you’re producing beer for commercial reason, then you must <a href="#">register
        as a brewer with HM Revenue and Customs (HMRC)</a>. Once registered you must keep specific
        records and accounts of all income and expenses, and calculate any Beer Duty that is due, and
        pay that tax.</p>
    </div>
