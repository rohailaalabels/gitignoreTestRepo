                                                    <p> There are four main component elements in a self adhesive label.
                                                        The top layer, or face-stock is a paper, or filmic material e.g.
                                                        Polypropylene, with an adhesive applied to the back and the top
                                                        prepared for printing. These two elements form the label
                                                        itself. </p>
                                                    <p> There are a variety of different types of adhesives available,
                                                        but the majority are either a general purpose permanent acrylic,
                                                        or rubber based pressure sensitive adhesive.</p>
                                                    <p>The bottom layer is usually a paper or filmic backing material
                                                        (release liner) for carrying the label through the print
                                                        process, before the label is applied to a product, or
                                                        substrate.</p>
                                                    <p>The fourth element in the labels construction is a silicone
                                                        release coating is added to the backing material to facilitate
                                                        the removal of the label for application.</p>
                                                    <p>With roll labels the waste matrix is removed from around the
                                                        labels.</p>
