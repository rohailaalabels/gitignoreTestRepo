  <p>December is creeping up on us again. The weather is getting colder, the nights are getting darker, and
    Christmas is just around the corner. Some baa humbugs might claim to hate this time of year (and who
    could blame them, really), but we have to admit that Saturday nights during the winter months have
    become a bit of a guilty pleasure, with the arrival of those somewhat addictive prime time TV shows,
    Strictly Come Dancing and The X-Factor.</p>
  <p>The 11th series of Strictly kicked off back in September. We all wondered who’d hit the great highs (the
    ultimate perfection like Kimberley and Pasha in series 10 with their Tango), and who we’d be laughing at
    with their extreme lows (Nancy and Anton with their Waltz, and the infamous wardrobe malfunction with
    her dress, leaving them with a score of just 1 from Craig and a score of 12 overall). We all have our
    favourites though and we were disappointed when Dave Myers, from the Hairy Bikers, was voted off at week
    7 with his Scottish inspired Tango not being quite good enough to wow the public.</p>
  <p>We felt for Dave, who this series has been caught up in a public row as to whether Strictly is a dance
    competition, or just light entertainment. With comments from the judges including “that was like a
    Cumberland sausage chasing the mustard” and “I wish there was some jive to judge”, it’s a surprise Dave
    wasn’t voted off earlier, but the public warmed to his charm and wit. If you could guarantee anything,
    it was that Dave would provide a whole host of Saturday night entertainment.</p>
  <p>We thoroughly enjoyed his performances week on week, and we especially love his version of the Cha Cha to
    “Moves Like Jagger” – see it here:</p>
