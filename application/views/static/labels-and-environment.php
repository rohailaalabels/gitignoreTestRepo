                        <h2 class="BlueHeading">Polymers</h2>
                        <p class="text-justify">

                            Typical label materials such as Polyester (PE), Polyethylene (PET), Polypropylene (PP) and
                            Poly Vinyl Chloride (PVC) are not so easy to manage in terms of their disposal. Most are
                            biodegradable over time being derived from carbon fossil fuel, but may still have unwelcome
                            residual effects, they are not compostable and not always easily recyclable and therefore
                            require disposal through incineration for energy generation and/or landfill. Although this
                            is not the most desirable outcome there are of course options to offset this and to some
                            extent mitigate the environmental impact and carbon footprint.
                        </p>

                        <p class="text-justify">
                            The research and development to find substitutes for traditional Polymer materials is a
                            priority for material manufacturers and the label industry. Options are gradually emerging
                            and becoming available and can be categorised as follows:
                        </p>

                        <p class="text-justify">
                        <ul style="padding-inline-start: 15px;">
                            <li>Material with recycled content</li>
                            <li>Material made from sustainably sourced content</li>
                            <li>Material made with recycling “enabling” technology i.e. adhesive</li>
                            <li>Thinner caliper materials</li>
                        </ul>
                        </p>
