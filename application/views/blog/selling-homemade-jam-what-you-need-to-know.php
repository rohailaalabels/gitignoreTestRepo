        <h2 class="headingPost">What you need to know</h2>
        <p>The following guidelines don’t apply if you’re offering up your jam as gifts to friends and
          family, but if you intend to sell it, you have to do your homework. Firstly, you need to
          register the premises at which the jam is made, even if this is at home. Registration applies to
          all catering premises, and this includes your own kitchen. If that’s where you’re making and
          packaging your jam, it needs to be registered as an official food premise. And if you are teamed
          up with someone and split production between your homes, theirs will need to be registered as
          well.</p>
      </div>
    </div>
    <p>Registration is free, and you can do this via the environmental services at your local authority. You can
      find yours by following this <a href="#">link</a>.</p>
    <p>You also need to register as being self-employed, you can find more information about this via this link
      to the <a href="#">Gov.uk</a> website. This needs to happen so that you can register for VAT if needs
      be, as well as advice on how to correctly keep records of all income and expenses. Who would have
      thought selling jam could get so complicated? The complexity of what will be required from you greatly
      depends upon how far you’re pushing your jam business. For example, if you’re just selling a few jars
      here and there at the local country fete, then the process is somewhat simpler than if you’re trying to
      get shelf space at a national supermarket.</p>
    <h2 class="headingPost">Safety and hygiene</h2>
    <div class="row">
      <div class="col-xs-6 col-md-4 col-lg-3 col-sm-5 pull-right"><img class="img-responsive col-xs-12 pull-right"
                                                      src="<?=BLOG_ASSETS?>jam-3.jpg"></div>
      <div class="col-xs-6 col-md-8 col-lg-9 col-sm-7">
        <p>The purpose of registering your premises is that because you want to officially produce food for
          consumption, you will be subject to inspections from the Food Standards Agency (FSA). During
          these inspections, not only will they ensure that the food you’re producing is safe to eat,
          they’ll also be checking your premises, how you work, the kinds of food you make or prepare, as
          well as your food safety management systems you have in place.</p>
      </div>
    </div>
    <p>In terms of food safety and hygiene, <a href="#">here</a> are the ‘Safer food, better business’ advice
      packs from the FSA. These cover everything from showing how to comply with food hygiene regulations, to
      how to save wastage and improve cleanliness.</p>
    <h2 class="headingPost">Other absolute essentials to keep in mind</h2>
    <div class="row">
      <div class="col-xs-6 col-md-4 col-lg-3 col-sm-5"><img class="img-responsive col-xs-12"
                                           src="<?=BLOG_ASSETS?>jam-4.jpg"></div>
      <div class="col-xs-6 col-md-8 col-lg-9 col-sm-7">
        <p>One of the most important factors when preparing food to sell on to others is how products are
          labelled. This still applies to jam, and inspectors will look to see how you jam is described,
          and how clearly the ingredients are displayed. This is really important because of allergies,
          and a customer purchasing something that could be potentially harmful without knowing is very
          serious.</p>
        <p>A quick guide for how to correctly <a href="#">label your jam</a> can be found <a
                                href="#">here</a>.</p>
        <p>Another important issue has to do with the actual jars you’ll be putting your jam in. Since your
          jam is being sold (and not given as gifts), the snappily titled EC Regulations 1935/2004 and
          2023/2006 state that you have to use brand new jars each and every time. So it’s no good
          thinking environmentally friendly and offering a reward scheme to customers who bring theirs
          back; it’s got to be a new jar every time.</p>
      </div>
    </div>
