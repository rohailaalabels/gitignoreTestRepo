    <h3>Q5. Have you invested in Marketing and PR ideas?</h3>
    <p>You should start planning your PR and marketing activity as soon as you can, and most definitely before
      you have the final product in your hand! It might be better for you to get someone else in to help you
      with this (even if you feel you can manage it yourself).</p>
    <p>The reason for this is you often become too close to your product, and even if the chosen person just
      checks your work to make sure it reads and comes across well, it is better than failing to be understood
      when you launch your material!</p>
    <h3>Q6. Do you have a website? Will you sell online?</h3>
    <p>Also be aware that as you become larger your costs for marketing and PR will more than likely (no,
      actually they will definitely) increase rapidly. And to become a large contender in any market you will
      need good marketing activity, and this involves either your money or your time.</p>
    <h3>The final countdown</h3>
    <p>So you aren’t going to necessarily know everything until you jump in and take the leap of faith, but
      aside from the list of things above you may wish to consider, there are almost certainly a list of
      don’ts.</p>
    <p>Don’t be tempted to launch when your product is not ready or you will lose your credibility and give
      yourself a bad reputation.</p>
    <p>Don’t try and launch without properly testing your product or service for faults or changes.<br>
      And don’t be too caught up and swayed by your own opinion. Just because it happens to be your idea, it
      doesn’t mean it is a foolproof or good one. Make sure you listen to others and take their point of view
      on board – it may just save you money in the long run!</p>
