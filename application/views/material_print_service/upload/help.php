            <div id="intro" class="titleOrange">Introduction</div>
            <p>The following brief guidelines are intended to assist in preparing digital artwork for submission to AA Labels. They are not intended to be exhaustive or infallible, but following the simple tips laid out below will help to minimise the risk of some common problems which can occur when printing from digital files. <br />
            </p>
            <div id="printing" class="titleOrange">Printing Digital Images</div>
            <p>Printing digital images can be frustrating if you don't have an understanding of print quality versus screen quality. A screen quality image, which looks fine on your monitor will often look ragged or pixellated when you print it. This section explains how to ensure you get the best possible print result from your digital images, regardless of what computer program you are using to create your artwork. <br />
              <br />
            </p>
            <p>Digital images come in two main types: 
              Vector & Raster Images. 
              Vector Images are usually logos or line art graphics which can be enlarged or reduced in size without affecting the quality of the printed result. Most commonly used images, however, including digital photographs (Jpeg being the most common file format), are raster images, which means that they are made up of a finite number of dots or pixels. The quality of this  type of image when printed, varies depending on the image and label size.&#13;</p>
            <br>
            <p> For example, you might have a picture file which is 500 pixels by 250 pixels in size – and this will not change, no matter how much you enlarge or reduce the size of the picture on the page. So if you make the picture 2 inches wide by 1 inch high, it's resolution will be 250 dots per inch (dpi). As a general "rule of thumb" this is the maximum size you can make this particular picture, without starting to compromise its quality when it is printed. <br>
              <br>
              If you enlarge the picture to 4 inches wide by 2 inches high, its resolution will drop
              to 125 dpi, because you've got the same number of pixels spread over a greater area. This will impair the output quality when you print it. For the purposes of printed label applications, all your graphics should be a minimum of 200 dpi to print cleanly. <img onerror='imgError(this);' class="helpImage-right img-responsive" src="<?=Assets?>images/help/MonaLisa.png" alt="MonaLisa"> For large format printing (e.g. A4 or A3 size labels) 300 dpi is usually preferable, depending on the image. Web graphics and "screen-grabs" are usually only 72 dpi, which is screen quality and looks fine on a monitor, but the results are not as good when printed. Above is a digital image of the Mona Lisa and on the right is an enlargement of the eye. A beautiful raster image made up of millions of tiny coloured squares, becomes pixellated when enlarged. </p>
