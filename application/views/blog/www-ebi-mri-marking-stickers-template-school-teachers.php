  <p>As a teacher you’ll no doubt have had many pieces of work to mark and give feedback on. It’s a time
    consuming and sometimes thankless task, particularly when pupils look at their grade or level and ignore
    the detailed written comments that you have spent hours formulating. In light of government changes and
    OFSTED regulations, feedback is more crucial than ever before and, in particular, teachers are
    increasingly expected to provide written feedback which pupils can reflect on and, most importantly, act
    upon.</p>
  <p>This sticker template allows you to feedback quickly, by typing up your responses and sticking them
    directly onto pupils’ work or in their books.</p>
  <p>Crucially, by filling in the electronic template you can also save a copy for future reference, allowing
    you to easily reflect on previous marking comments and assess whether pupils have actually worked on
    their EBIs and MRIs.</p>
  <p>The template below allows pupils much more ownership of their results and forces them to reflect on and
    evaluate their progress. There are two editable sections on each sticker for the teacher to write
    something that the pupil has done well and something they could do to improve. Finally there is a
    section for the pupil to give their responses to the teacher’s comments or make the necessary
    corrections to their work.</p>
  <p>Evidence suggests that by allowing pupils the time to correct their work, based on your feedback, rates
    of progression are much higher. These stickers therefore provide a much more interactive way of marking
    and giving feedback, one in which the child plays a central role in their own learning and
    progression.</p>
  <p>For example, in response to a History source analysis task, the teacher may comment as follows:</p>
  <p><strong>WWW:</strong> “Well done, you have explained how and why the author of the source may have had an
    impact on its reliability”.</p>
  <p><strong>EBI:</strong> “You also need to consider how the date and purpose of the source could have had an
    effect on the source’s reliability”.</p>
  <p>In response, in the <strong>MRI</strong> (My response is..) section, the pupil may then be given the
    opportunity to carry out the additional explanation, thus addressing the issues immediately.</p>
