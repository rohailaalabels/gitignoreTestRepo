      <h3 >Adhesive</h3>
      <p>A general purpose super-permanent acrylic based, pressure sensitive adhesive. The adhesive is characterised by a higher initial tack, excellent adhesion for difficult surfaces such as wood, some cardboards and plastics such as HDPE, PP, PET and PVC and good low temperature performance on a wide variety of substrates, including smooth and slightly rough, or curved surfaces. This adhesive should not be exposed to direct sunlight and/or high temperatures for long periods of time. Matt white face-stocks are also available with a standard permanent, peelable and freezer grade adhesive.</p>
