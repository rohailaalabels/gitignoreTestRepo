  <h3 >Applications and use </h3>
  <p>This face material is designed for the manufacture of labels on rolls and sheets specifically for use in inkjet printers. The surface structure provides excellent toner bonding and print resolution, because of the inclusion of an instant-dry top coating for water based pigmented and die based inks. High smudge-free, printed image resolution on a satin finish and consistent performance at high print speeds. </p>
  <p> This product is ideal for label applications where face stock and finished label appearance is important such as corporate ID, labels for external application, product branding/packaging, promotional labels and many other applications. </p>
  <h3 >Approvals</h3>
  <p> The adhesive complies with the European food directives and legislation FDA 21; CFR 175, 105 and the German recommendations XIV as published by BfR (Bundesinstitut fur Risikobewertung) the German Federal Institute for Risk Assessment. Both of which state that the adhesive can be used in direct contact with dry, moist and non-fatty foodstuffs, however according to the FDA regulations the adhesive must be separated from the foodstuffs by a functional barrier. </p>
  <h3 >Shelf Life</h3>
  <p> Two years under storage conditions as defined by Premier Coating & Converters Ltd 20° - 28° C and 40 – 60% RH.</p>
