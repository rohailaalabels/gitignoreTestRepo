      <p>Whatever your situation, it’s always good to think about exactly what you’re going to need for
        the term ahead. With that in mind, here are a few tips to help you cover all the essentials, as
        well a few ideas for what else you might need to stock up on in order to make your life that
        little bit easier.</p>
      <p></p>
    </div>
  </div>
  <div class="clear10"></div>
  <p>First things first, let’s cover the absolute basics. You’re going to need pencils and pens for writing
    tasks (standard and fun coloured markers), as well as colouring crayons and pencils. Other standard
    supplies include notebook paper, sticky tape, folders erasers, a staple remover, glue sticks,
    construction paper and scissors.</p>
  <p>Extra essentials that spring to mind are things such as: a timer or desktop clock so you can make sure
    the lesson isn’t getting ahead of itself; clipboards in case your class are out and about, or sat on the
    floor whilst working; Velcro or magnet strips to bind items together; and maybe even a handy supply of
    healthy snacks and water for you just in case!</p>
  <div class="clear10"></div>
  <h2 class="headingPost">Getting Organised</h2>
  <p><img src="<?=BLOG_ASSETS?>cr-4.jpg" class="img-responsive col-xs-12"></p>
  <p>Aside from the daily necessities, there are plenty more essentials that need your attention. Take for
    example what you need in order to keep on top of things and get extra organised. Only teachers know
    first-hand about the sheer amount of planning and extra work that comes along with the job, so getting
    into the habit of fantastic organisation is an essential skill.</p>
  <p>Start by using plastic tubs or cardboard boxes to store all the materials, books, and supplies that go
    into special art projects. Be sure to label these boxes with the name of each project or unit. Or, you
    could keep different boxes for each student as an easy organisational tool. Storage bins, baskets, and
    magazine files to keep students’ papers and work organised.</p>
  <div class="clear10"></div>
  <div class="row">
    <div class="col-xs-4 pull-right"><img src="<?=BLOG_ASSETS?>cr-5.jpg"
                                                      class="img-responsive col-xs-12 pull-right"></div>
    <div class="col-xs-8">
      <p>Consider using index cards or prompt cards if you need them to help with the flow and pace of a
        lesson. You can keep a special box or drawer set aside filled with these lifesavers, which can
        easily be referred to as and when they’re needed. You could even make things easier for you by
        colour coding and labelling these with a handy index in your lesson planner.</p>
      <p></p>
    </div>
  </div>
  <div class="clear20"></div>
  <div class="row">
