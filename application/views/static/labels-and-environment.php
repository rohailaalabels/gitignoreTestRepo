                    <div class="col-xs-12 col-sm-8 col-md-8">
                        <p class="text-justify" style="font-size: 13px;margin-bottom:10px;">The biodegradability of
                            inkjet ink is dependent on the ingredients used in its production. Desktop inkjet printers,
                            as used in offices or at home, tend to use aqueous inks based on a mixture of water, glycol
                            and dyes or pigments. These inks are inexpensive to manufacture, but are difficult to
                            control on the surface of media, often requiring specially coated “Inkjet” media.</p>
                        <p class="text-justify">The two main category of inks used in commercial printing are petroleum-based and vegetable
                            oil-based, although the two can be mixed together and the biodegradability is dependent upon
                            the percentage of vegetable-based oils.</p>
                    </div>


                    <div class="m-t-15 col-xs-12 col-sm-12 col-md-12 ">
                        <h2 class="BlueHeading">Petroleum Based Inks</h2>
                        <p class="text-justify">
                            Because they dry faster than vegetable-based inks, petroleum-based inks have become the
                            widespread standard in the printing industry. Even Soy-based ink and other partially
                            biodegradable inks contain petroleum-based chemical derivatives which consist of inorganic
                            compounds and minerals that are not biodegradable.
                        </p></div>

                    <div class="m-t-15 col-xs-12 col-sm-12 col-md-12 ">
                        <h2 class="BlueHeading">Biodegradable Inks</h2>
                        <p class="text-justify">
                            Because they are less toxic than petroleum oils and decompose over time, biodegradable inks
                            take up less space in landfills. While some inks available to consumers contain
                            vegetable-based oils and are biodegradable in part, there is still no widely available ink
                            that is completely biodegradable, as of now. Most soy-based inks, for example, still
                            contain at least 10 percent petroleum oils, according to the EPA.
                        </p></div>

                    <div class="m-t-15 col-xs-12 col-sm-12 col-md-12 ">
                        <h2 class="BlueHeading">Soy-Based Ink</h2>
                        <p class="text-justify">
                            Soy bean oil has increasingly been used in combination with petroleum based-chemicals in
                            inks. According to the EPA, any "soy ink" must contain at least 20 percent soy-based oils,
                            and ink biodegradability increases as this percentage of soy oils increases. The
                            manufacturers hope to develop a highly biodegradable ink with high performance properties
                            made from 100 percent soy-based oils and no additional petroleum-based chemicals.
                        </p></div>

