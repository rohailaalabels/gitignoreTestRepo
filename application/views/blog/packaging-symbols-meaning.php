              <p><span class="tt-highlight highlight-cool-blue"><span class="highlight-style-1">1. Eco-Label Mark</span></span><br>
                <a href="http://www.aalabels.com/blog/wp-content/uploads/B9Q1-eco-label.jpg"><img
                                                    class="size-thumbnail wp-image-206 alignleft" alt="eco label"
                                                    src="https://www.aalabels.com/blog/wp-content/uploads/B9Q1-eco-label-150x150.jpg"
                                                    width="150" height="150"></a><br>
                The Eco-Label is an icon of a daisy, and is a voluntary certification awarded by
                the European Union to products and services that have a reduced environmental
                impact throughout their life cycle, from the extraction of raw material through
                to production, use and disposal. Launched in 1992 to try and introduce a
                European mark that consumers could trust, the Eco-label can now be found on over
                17,000 products.</p>
              <p>&nbsp;</p>
              <p>&nbsp;</p>
              <p>&nbsp;</p>
              <p><span class="tt-highlight highlight-cool-blue"><span class="highlight-style-1">2. Shelf Life</span></span><br>
                <a href="http://www.aalabels.com/blog/wp-content/uploads/B9Q2-shelf-life.jpg"><img
                                                    class="alignleft size-thumbnail wp-image-217" alt="shelf life"
                                                    src="https://www.aalabels.com/blog/wp-content/uploads/B9Q2-shelf-life-150x150.jpg"
                                                    width="150" height="150"></a><br>
                This symbol shows the product life of your item once it has been opened. The
                number in the middle of the ‚Äúpot‚Äù shows the number of months that the product is
                good for before it should be thrown away.</p>
              <p>&nbsp;</p>
              <p>&nbsp;</p>
              <p>&nbsp;</p>
              <p>&nbsp;</p>
              <p><span class="tt-highlight highlight-cool-blue"><span class="highlight-style-1">3. Flammable Symbol</span></span><br>
                <a href="http://www.aalabels.com/blog/wp-content/uploads/B9Q3-flammable-symbol.jpg"><img
                                                    class="alignleft size-thumbnail wp-image-207" alt="flammable symbol"
                                                    src="https://www.aalabels.com/blog/wp-content/uploads/B9Q3-flammable-symbol-150x150.jpg"
                                                    width="150" height="150"></a><br>
                The flammable symbol shows that the product has hazardous properties and is
                flammable. This means that you must be aware of factors such as proper storage,
                removal of any sources of ignition or other factors necessary to prevent a fire. </p>
              <p>&nbsp;</p>
              <p>&nbsp;</p>
              <p>&nbsp;</p>
              <p><span class="tt-highlight highlight-cool-blue"><span class="highlight-style-1">4. Marine Pollutant</span></span><br>
                <a href="http://www.aalabels.com/blog/wp-content/uploads/B9Q4-marine-pollutant-symbol.jpg"><img
                                                    class="alignleft size-thumbnail wp-image-212"
                                                    alt="marine pollutant symbol"
                                                    src="https://www.aalabels.com/blog/wp-content/uploads/B9Q4-marine-pollutant-symbol-150x150.jpg"
                                                    width="150" height="150"></a><br>
                This icon shows that the product is a marine pollutant, or can be harmful to the
                environment.</p>
              <p>&nbsp;</p>
              <p>&nbsp;</p>
              <p>&nbsp;</p>
              <p>&nbsp;</p>
              <p><span class="tt-highlight highlight-cool-blue"><span class="highlight-style-1">5. CE Mark</span></span><br>
                <a href="http://www.aalabels.com/blog/wp-content/uploads/B9Q5-CE-mark.jpg"><img
                                                    class="alignleft size-thumbnail wp-image-213" alt="CE mark"
                                                    src="https://www.aalabels.com/blog/wp-content/uploads/B9Q5-CE-mark-150x150.jpg"
                                                    width="150" height="150"></a><br>
                The CE mark is mandatory for certain items sold within the European Economic
                area, introduced in 1993. The CE mark is perhaps one of the most recognised
                marks and is used even by companies that are not based in the European Economic
                area but simply export there. The mark ‚Äúensures the free movement of products
                that conform to the legislation ‚Äì eg safety, health and environmental protection
                ‚Äì within the European market‚Äù. It covers a vast array of products. The full
                details of its application are detailed here: <a
                                                href="https://www.gov.uk/ce-marking" rel="nofollow">https://www.gov.uk/ce-marking</a> </p>
              <p>&nbsp;</p>
              <p>&nbsp;</p>
              <p><span class="tt-highlight highlight-cool-blue"><span class="highlight-style-1">6. The ‚Äòe‚Äô Mark</span></span><br>
                <a href="http://www.aalabels.com/blog/wp-content/uploads/B9Q6-e-mark-symbol.jpg"><img
                                                    class="alignleft size-thumbnail wp-image-214" alt="e mark"
                                                    src="https://www.aalabels.com/blog/wp-content/uploads/B9Q6-e-mark-symbol-150x150.jpg"
                                                    width="150" height="150"></a><br>
                Any item between between 5 grams or millilitres and 10 kilograms or litres can
                be distributed within the EU with the ‚Äúe‚Äù mark, without having to follow other
                rules about weights and measures.</p>
              <p>&nbsp;</p>
              <p>&nbsp;</p>
              <p>&nbsp;</p>
              <p>&nbsp;</p>
              <p><span class="tt-highlight highlight-cool-blue"><span class="highlight-style-1">7. Recycling symbol</span></span><br>
                <a href="http://www.aalabels.com/blog/wp-content/uploads/B9Q7-recycling-symbol.jpg"><img
                                                    class="alignleft size-thumbnail wp-image-215" alt="recycling symbol"
                                                    src="https://www.aalabels.com/blog/wp-content/uploads/B9Q7-recycling-symbol-150x150.jpg"
                                                    width="150" height="150"></a><br>
                Perhaps one of the more widely used packaging icons, the recycle symbol is a
                sign that your packaging can be set aside for recycling and hopefully not end up
                in landfill.</p>
              <p>&nbsp;</p>
              <p>&nbsp;</p>
              <p>&nbsp;</p>
              <p>&nbsp;</p>
              <p><span class="tt-highlight highlight-cool-blue"><span class="highlight-style-1">8. Keep Dry</span></span><br>
                <a href="http://www.aalabels.com/blog/wp-content/uploads/B9Q8-keep-dry-symbol.jpg"><img
                                                    class="alignleft size-thumbnail wp-image-216" alt="keep dry symbol"
                                                    src="https://www.aalabels.com/blog/wp-content/uploads/B9Q8-keep-dry-symbol-150x150.jpg"
                                                    width="150" height="150"></a><br>
                This symbol is commonly found on cargo packages, and encourages the handler to
                keep the package dry. In fact, this symbol was the most confusing of all for our
                survey group. Half of the people we asked actually thought that the symbol meant
                the opposite ‚Äì i.e. that the package could indeed be water resistant.</p>
              <p>&nbsp;</p>
              <p>&nbsp;</p>
              <p>&nbsp;</p>
              <p><span class="tt-highlight highlight-cool-blue"><span class="highlight-style-1">9. WEEE Symbol</span></span><br>
                <a href="http://www.aalabels.com/blog/wp-content/uploads/B9Q9-weee-symbol.jpg"><img
                                                    class="alignleft size-thumbnail wp-image-208" alt="weee symbol"
                                                    src="https://www.aalabels.com/blog/wp-content/uploads/B9Q9-weee-symbol-150x150.jpg"
                                                    width="150" height="150"></a><br>
                The WEEE symbol is an icon used to show that the product is electrical waste,
                and therefore cannot be disposed of in a normal waste receptacle, but must be
                recycled in accordance with the WEEE directive. See here for more details on
                WEEE and how your business can comply: <a
                                                href="http://www.environment-agency.gov.uk/business/topics/waste/139283.aspx"
                                                rel="nofollow">http://www.environment-agency.gov.uk/business/topics/waste/139283.aspx</a> </p>
              <p>&nbsp;</p>
              <p>&nbsp;</p>
              <p>&nbsp;</p>
              <p><span class="tt-highlight highlight-cool-blue"><span class="highlight-style-1">10. Biological Hazard</span></span><br>
                <a href="http://www.aalabels.com/blog/wp-content/uploads/B9Q10-biological-hazard.jpg"><img
                                                    class="alignleft size-thumbnail wp-image-209"
                                                    alt="biological hazard"
                                                    src="https://www.aalabels.com/blog/wp-content/uploads/B9Q10-biological-hazard-150x150.jpg"
                                                    width="150" height="150"></a><br>
                Biological hazards present a threat to the health of humans and this symbol
                warns to take precautionary measures when using or handling the product.</p>
              <p>&nbsp;</p>
              <p>&nbsp;</p>
              <p>&nbsp;</p>
              <p>&nbsp;</p>
              <p><span class="tt-highlight highlight-cool-blue"><span class="highlight-style-1">11. Harmful</span></span> </p>
              <p><a href="http://www.aalabels.com/blog/wp-content/uploads/B9Q11-Harmful.jpg"><img
                                                    class="alignleft size-thumbnail wp-image-210" alt="Harmful"
                                                    src="https://www.aalabels.com/blog/wp-content/uploads/B9Q11-Harmful-150x150.jpg"
                                                    width="150" height="150"></a><br>
                These are similar to toxic products but not as dangerous. However, users should
                be careful to wear goggles and handle with care.</p>
              <p>&nbsp;</p>
