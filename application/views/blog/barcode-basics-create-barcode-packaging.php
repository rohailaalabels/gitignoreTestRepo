  <h3>Taking the barcode to the next level – A new way of marketing using a QR code</h3>
  <p>A fairly new style of barcode called the QR code (Quick Response Code), which is two dimensional and can
    be read using smartphones, is popular with many consumer brands. QR codes can be easily created for free
    by typing in your web address or telephone number to a QR code generator website. When a customer scans
    the QR code with their phone, they will be taken directly to your website (or chosen destination)
    quickly and easily. You can see how it is done here: <a href="http://www.the-qrcode-generator.com/"
                                                                        target="_blank">http://www.the-qrcode-generator.com/</a>.
    So rather than printing your web address on a label or packaging, perhaps try a QR code instead.</p>
