      <p><strong>Inkjet Printer: Cleaning Cartridges or Ink Nozzles</strong></p>
      <ul>
        <li>Use the cleaning process on your printer, if your computer has that software option. Click on
          “Start &gt; Printers &amp; Faxes.” </li>
        <li>Open the printer software by double-clicking on a printer icon, or by right-clicking on the
          printer icon. </li>
        <li>Click a button labelled one of the following: “Options, Troubleshooting, Clean Print Cartridges,
          Toolbox, Printer Utilities”, or similar as these options vary by OEM. </li>
        <li>Click a button labelled “Head Cleaning, Run Nozzle Check, Clean Print Cartridges,” or similar to
          begin the process. </li>
        <li>Follow the on-screen instructions and repeat the process if the results are not satisfactory. </li>
      </ul>
      <p>Some inkjet printers do not have this option, in which case you can also try to manually clean the
        print heads.</p>
      <ul>
        <li>On some inkjet printers the ink cartridge contains the print head, therefore changing the
          cartridge will provide a new, unclogged print head. </li>
        <li>Where the print head is part of the printer itself, then it usually requires changing by the
          OEM </li>
        <li>Isopropyl alcohol is an effective solvent for removing dried ink.</li>
        <li>Soaking the print heads in hot water or WD40 for 2-3 minutes and then blotting them dry with a
          tissue, can also be restorative. </li>
        <li>Using tweezers and/or compressed air helps to remove debris and paper shreds from inside of the
          printer. </li>
        <li>If you refill an inkjet cartridge you should clean the cartridge nozzles immediately after the
          refilled cartridge has been reinstalled. </li>
      </ul>
