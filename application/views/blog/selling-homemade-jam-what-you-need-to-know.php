    <p>Sometimes there’s nothing better than a slice of toast or a scone that’s liberally coated in a hearty
      layer of sticky, sweet jam, especially if it’s packed with fresh fruit.</p>
    <div class="row">
      <div class="col-xs-6">
        <p>This quintessentially British tradition historically dates back to long before the invention of
          refrigerators, when preserving food meant all the difference at those times when food was
          scarce. Today, jam is less about survival, and more about the taste and enjoyment we get from
          unique and flavoursome fruit creations.</p>
      </div>
      <div class="col-xs-6">
        <p>The jams you buy in the shops are usually too full of sugar and artificial flavours, so if you’re
          a big jam fan, and want to try a new hobby, why not try your hand at making a variety of tasty
          preserves?</p>
      </div>
    </div>
    <div class="row">
      <div class="col-xs-6 col-md-4 col-lg-3 col-sm-5 pull-right"><img class="img-responsive col-xs-12 pull-right"
