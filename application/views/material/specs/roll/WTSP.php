      <h3 >Applications and use </h3>
      <p> This face material is designed for the manufacture of labels on rolls and sheets for use in copier, inkjet, laser and thermal transfer printers (uncoated white only). The surface structure provides excellent toner bonding and print resolution.</p>
      <p>This product is ideal for label applications where label adherence is difficult. The uncoated face-stock provides limited resistance to moisture, temperature & chemicals. </p>
      <h3>Approvals</h3>
      <p>The face-stock complies with the RoHS guidelines on Electric & Electronic Equipment, the safety European directive 2009/48/CE. Contact with foodstuff (BfR XXXVI) and the Packaging & Packaging Waste Directive 94/62/CE.</p>
      <p>The adhesive complies with the European food directives and legislation FDA 21; CFR 175, 105 and the German recommendations XIV as published by BfR (Bundesinstitut fur Risikobewertung) the German Federal Institute for Risk Assessment. Both of which state that the adhesive can be used in direct contact with dry, moist and non-fatty foodstuffs, however according to the FDA regulations the adhesive must be separated from the foodstuffs by a functional barrier. Also EN 71-3 and EN 71-9 for Toy Safety and the Packaging & Packaging Waste Directive 94/62/CE.</p>
      <p>The release liner complies with the RoHS guidelines on Electric & Electronic Equipment, the safety European directive 2009/48/CE. Contact with foodstuff (BfR XXXVI). The Packaging & Packaging Waste Directive 94/62/CE and EN 71-3 and EN 71-9 for Toy Safety.</p>
      <h3 >Shelf Life</h3>
      <p> Two years from the date of manufacture under storage conditions as defined by FINAT (Federation Internationale des Fabricants et Transformateurs d’ Adhesifs et Thermocllants sur papiers et autres supports) 20° - 25° C and 40 – 50% RH.</p>
