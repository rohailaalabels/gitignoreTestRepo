                          <h3 >Applications and use </h3>
                          <p>Applications are predominantly in market segments where rigid containers are used e.g. glass and PET. Due to the fairly rigid nature of the film, care should be taken with use on non-uniform surface and where a very high level of sqeezability is desired. Also for product labels in applications where good durability and resistance against water is important. </p>
                          
                          
							<h3>Conversion & Printing</h3>
                          <p>The top-coated face-stock can be printed by most conventional printing techniques and is suitable for Thermal Transfer printing with specific ribbons. Exact foils, inks and ribbons should be specified by the OEM printer manufacturer and/or the foil/ink/ribbon supplier. Preliminary testing via a material sample is always recommended.</p>
                          
					<h3 >Approvals</h3>
                          <p>The research centre ISEGA has given the adhesive approval according to the European requirements for direct contact with dry, moist and such kids of fatty foodstuffs which have a correction factor of at least 3 according to the Regulation (EU) No. 10/2011. The adhesive is also in compliance with the FDA 21 CFR 175.105 “Adhesives.”</p>
                          
                          
						  <h3>Shelf Life</h3>
                          <p>Two years under storage conditions as defined by FINAT section 2.5 (Federation Internationale des Fabricants et Transformateurs d’ Adhesifs et Thermocllants sur papiers et autres supports) 20° - 25° C and 40 – 50% RH.</p>
