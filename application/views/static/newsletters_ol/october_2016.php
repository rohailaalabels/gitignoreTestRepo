                                                                <td style="font-family: Arial; font-size: 16px; color: #16b1e4; text-align: left; line-height: 24px;"><strong>New Packaging for A4 Label Sheets - Boxes
</strong></td>
                                                              </tr>
                                                             
                                                              <tr>
                                                                <td valign="top" style="font-family: Arial; font-size: 12px; color: #58585a; text-align: justify; line-height: 18px;"><p>For larger quantities of A4 sheet labels c. 250 sheets of WTP we have designed  an innovative new box that protects labels and facilitates ease of access, via  a "drop-down" lower section of the box opening, as shown in the diagrams below.<br />
                                                                  <br />

The label sheets are also sealed in a thin inner Polypropylene polymer (CPP)  bag with an "easy-tear" opening. This provides additional protection  and assists with maintaining the flatness of the label sheets in the  box. Once sealed in the protective inner bag the labels are placed into the new  cardboard box packaging and the box shrink wrapped for additional protection in  transit. Upon receipt of your labels and removal of the outer shrink wrapping, open the upper lid of the box and pull down the lower  section to remove the top of the serrated inner bag to access the label sheets  and simply close again once the labels  are removed, retaining the sheets in a clean, dust free, flat condition.<br />
<br />
                                                           <strong>Biodegrading  &amp;  Recycling:</strong> The thin inner CPP  inner bag degrades from exposure to heat and UV radiation, such as that present  in sunlight and the outer shrink wrap is a degradable Polyolefin, designed to  retain functionality as a commodity plastic for the required service life but  degrade to non-toxic end products in a disposal environment. The cardboard box  itself is both biodegradable and recyclable. </p>                                                                </td>
