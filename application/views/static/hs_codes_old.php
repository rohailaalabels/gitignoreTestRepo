                        <div>
                            <h2 class="BlueHeading">WHAT ARE HS CODES?</h2>
                            <p class="text-justify">HS Codes (The Harmonised Item Description & Coding System) or HTS
                                Codes (The Harmonised Tariff Schedule), also known as the Harmonized Commodity
                                Description and Coding System, or simply the Harmonized System, are a standardized
                                international system to classify globally traded products. The system was first
                                implemented in 1988 and is currently maintained by the World Customs Organization. The
                                HS Convention, signed in 1983, has over 205-member countries. As signatories, each
                                country agrees to classify its tariff and duty structure according to the HS Code
                                categories.</p>
                        </div>
                    </div>
                    <div class="col-xs-12 col-sm-4 col-md-4 m-t-48 text-center"><img onerror='imgError(this);' class="img-responsive"
