            <div id="dtp" class="titleOrange">Professional DTP Software (e.g. Illustrator, InDesign Quark XPress, etc) </div>
            <p><img onerror='imgError(this);' class="helpImage-type2" src="<?=Assets?>images/help/type/ai.jpg" alt="AI"> DTP (desk-top publishing) software is specifically designed to prepare documents with a view to having them printed on press. They work alongside a family of graphics and imaging programs, e.g. Photoshop and Illustrator . The graphics programs allow you to create 
              and manipulate graphics files, and the DTP software allows you to organise these graphics onto the page along with your text. DTP programs can be tricky to use until you've had a bit of practice, and in order to use them correctly, some basic knowledge of printing processes is required. For the purposes of this document it is assumed that if you are using this kind of program, you will already have this knowledge and be familiar with use of DTP software. However, the following tips will help ensure that the files you submit are print-ready. <br>
              <br>
              <strong>1. Use the right colour type</strong><br>
              There are several ways in which computers handle colour in picture files, the main two colour modes being RGB (red-green-blue) colour and CMYK (cyan-magenta-yellow-key black) colour. RGB colour is based on properties of light, and is suitable when images are to be viewed on screen, not for printing with ink or toner on paper. CMYK colour is based on properties of ink and should always be used when a colour image is to be printed. The colour mode of your graphics files can be set to CMYK from within your graphics program; the colour mode of your document text can be set from within your DTP program. In addition to RGB and CMYK colour, you may also need to work with Pantone colour, if you are creating a document which is to be printed in just one or two specific colours (e.g. the AA Labels corporate logo colours are Light blue: PANTONE 2995 & Dark blue: PANTONE 3015)<br>
              <br>
              <img onerror='imgError(this);' class="helpImage-right" src="<?=Assets?>images/help/type/pentone_match.png" alt="Pentone Colour"> The Pantone Matching System is a universally accepted system which allows you to specify the exact colour ink you want to use, based on a pre-defined colour scale. If you're using Pantone colours, you can't use CMYK graphics alongside them, unless you're intending to print your document in full colour with additional Pantone colours on top – which will increase the cost of your print job. To print only in Pantone colours, you need to make sure that your graphics are suitably set up. The usual way of doing this is to convert them to greyscale (i.e. to discard the colour information and save them as black and white) and then to allocate them the Pantone colour you are using from within your DTP program. <br>
              <br>
              <strong>2. Check the image resolution</strong><br>
              Image quality is explained in detail in the Printing Digital Images section of this document – but worth a reminder here. For publication print quality any raster images you are using must be a minimum of 250 dpi at the size they are to be printed. An image which is 250 dpi at A6 (postcard) size will effectively drop to 125 dpi if you decide to fill an A4 page with it, so make sure the images you are using are of suitable quality for the size you want to print them. <br>
              <br>
              <strong>3. Remember</strong><br>
              to allow for bleeds This one is important – and often overlooked. A bleed is where an image or background colour is positioned so that it prints right to the edge of the label. If you're working on a document that has bleeds you need to make the object that bleeds off the label overlap the edge of the label by at least 3mm (see diagram below). The printer prints the document on a larger roll or sheet size than the label size. The extra 3mm bleed allows any movement on the convertor to not necessarily result in a visible white border and/or edge to the labels </p>
