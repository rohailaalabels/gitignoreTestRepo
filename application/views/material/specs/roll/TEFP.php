  <h3 >Applications and use </h3>
  <p>This product is designed for use in short-life commercial and retail applications requiring the printing of variable data and/or information. It also provides excellent grades with EAN and other barcodes. </p>
  <p>As economy thermal papers are not equipped with a protective surface coat, the labels should not be exposed to water or greasy substances. Limited resistance to thermal image scratching.</p>
  <h3 >Conversion & Printing</h3>
  <p>This product is designed to be converted and dispensed at high speed by all conventional roll conversion techniques, including flexographic and UV letterpress. Due to the thermo-graphic properties exposure above 50° C may cause premature imaging or discolouration. Inks containing alcohol or volatile organic solvents may also cause discolouration. It is advisable to test inks and varnishes before conversion.</p>
  <p>Also contact with moisture, oil, fats, plasticisers, strong lighting and direct sunlight for prolonged periods should be avoided because of potential image fade issues.</p>
  <h3 >Approvals – Regulations concerning contact with food.</h3>
  <p>The research centre ISEGA (Research & Development Institute, Aschaffenburg) has given the adhesive approval according to the European requirements for direct contact with dry and moist, non-fatty foodstuffs and compliance with the FDA, 21 CFR Ch.1 (April 01, 2013 edition) 175.105 for direct contact with dry foodstuffs, or they must be separated from the foodstuffs by a functional barrier.</p>
  <h3 >Shelf Life</h3>
  <p>Two years under storage conditions as defined by FINAT <small>(Federation Internationale des Fabricants et Transformateurs d&rsquo; Adhesifs et Thermocllants sur papiers et autres supports)</small> section 2.5 20° - 25° C and 40 – 50% RH.</p>
