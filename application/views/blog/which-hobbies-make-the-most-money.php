      <h2 class="headingPost">Jewellery maker</h2>
      <p>If you love jewellery and have a creative streak, you might consider setting up your own
        jewellery making business. It’s easy to get started with this as many suppliers offer raw
        materials such as chains and gemstones in bulk. You don’t need to spend a fortune on tools
        either, with many starter kits providing everything you’ll need.</p>
      <p>Create a business name and logo and you’ll be ready to put your brand on the internet. Use a
        marketplace like Etsy to sell your goods and you could earn a tidy profit. Jewellery making can
        be very lucrative as the raw materials are often very cheap versus the prices you can charge for
        your hand-made goods.</p>
