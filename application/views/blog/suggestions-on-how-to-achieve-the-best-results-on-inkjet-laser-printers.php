      <h4>PRINTED LINES ON SHEETS</h4>
      <p>This sometimes occurs on laser printers if the cleaner bar inside the printer is not clean. New toner
        cartridges are usually supplied with a replacement cleaner bar, replace the cleaner bar and print a
        test sheet to ensure that the issue has been resolved.</p>
      <h4>SOFTWARE ISSUES</h4>
      <p>Problems caused by software can produce scrambled image and text, or none at all, just blank areas.
        This is due to file corruption and/or incompatibility, try updating the print driver, or completely
        remove the old driver an install a new one. Alternatively, the elements of the file that you are
        using may not be supported e.g. font styles, which if not installed on your PC and encrypted will
        print with bold line uprights. Similarly, the program that you are using such as Adobe Acrobat or MS
        Word may not support elements of the print file that you are using. </p>
