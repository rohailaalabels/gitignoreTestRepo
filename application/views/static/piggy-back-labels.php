        <p class="text-justify">
        The unusual name often ascribed to this type of label comes from their design, since they comprise two layers of labels, one “piggybacking” on the other. This creates labels within labels and both layers are self-adhesive. with the main, primary or “parent” label being applied and the secondary labels peeled off for application onto related files, forms or items. Dynamic and versatile piggback labels can be created in design constructions that suit multiple and specific applications in a wide variety of label materials.
