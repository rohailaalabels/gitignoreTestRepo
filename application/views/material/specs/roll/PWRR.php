  <h3 >Applications and use </h3>
  <p>Applications are predominately in market segments where rigid containers are used (e.g. glass, polyester/PET). Due to the fairly rigid nature of the film, care should be taken with application and use on non-uniform surfaces and/or where a very high level of flexibility or a squeezable function is required (e.g. soap and shower gel containers). A good choice for primary brand and product labels on rigid containers, secondary promotional labels, asset labels, informational labels, external labels/signage and safety labels/signage etc. </p>
  <p> This adhesive is suitable for use across a broad range of “wet wipe” applications and is also suitable for  many dry pack re-closure applications and can be used for promotional labels and clothing “stickers” combining excellent UV and weather resistance with good removability from most substrates e.g. glass, PET and PP. </p>
  <p> On a few substrates (e.g. PVC, Polystyrene, PC and ABS) the adhesion tends to become stronger over the course of time and this may affect functional removability, therefore preliminary testing on such substrates is recommended. </p>
  <h3>Conversion and printing</h3>
  <p>The modified acrylic based top-coating can be printed by most conventional printing techniques, including flexo; screen; offset-litho; letterpress, silkscreen; gravure and hot or cold foiling processes. UV, water-based and solvent-based inks can be used. The topcoat is designed for optimum ink adhesion. On-press corona treatment is not advised. The face material is suitable for Thermal Transfer printing with specific ribbons (exact inks, foils and ribbons should be specified by your ink/foil/ribbon supplier). The material has excellent register properties, especially when a high number of colour are used.</p>
  <h3 >Approvals</h3>
  <p> The adhesive complies with the European food directives and legislation FDA 21; CFR 175, 105 and the German recommendations XIV as published by BfR (Bundesinstitut fur Risikobewertung) the German Federal Institute for Risk Assessment. Both of which state that the adhesive can be used in direct contact with dry, moist and non-fatty foodstuffs, however according to the FDA regulations the adhesive must be separated from the foodstuffs by a functional barrier. </p>
  <h3 >Shelf Life</h3>
  <p>Two years under storage conditions as defined by FINAT (Federation Internationale des Fabricants et Transformateurs d’ Adhesifs et Thermocllants sur papiers et autres supports) 20° - 25° C and 40 – 50% RH. Prolonged storage at higher temperatures and/or humidity levels will shorten the shelf life.</p>
