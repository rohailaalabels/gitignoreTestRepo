                        <h2 class="BlueHeading">HOW ARE HS CODES USED?</h2>
                        <p class="text-justify">
                            HS Codes are used by customs and logistics providers for a variety of purposes, from
                            calculating duties and tariffs to ensuring that an importer is not importing banned or
                            hazardous products. The true value of HS Codes is how globally recognized they are. Whether
                            you’re importing self-adhesive paper labels to the United States or Africa, the first four
                            digits of the HS Code will be 4811 or 4821.
                        </p>

