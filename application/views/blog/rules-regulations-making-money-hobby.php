    <div class="col-md-7 col-lg-8">
      <p>Of course, anything to do with earning money has to run through the proper channels, adhering to
        the laws and regulations in place by HM Revenue and Customs (HMRC) in the UK. So here is a quick
        stop guide for you to take these rules and regulations into account to ensure that what you’re
        earning is perfectly above board and legitimate.</p>
    </div>
  </div>
  <p>As far as HMRC is concerned, you can be considered self-employed even if you already have a job, or even
    if what you’re doing is not making any profit at all. You can even be classed as self-employed if you
    don’t end up spending any of the money that you eventually make, so it’s very important to be absolutely
    sure and that you follow the correct guidelines.</p>
  <div class="row">
    <div class="col-md-4 col-sm-4 col-lg-3"><img src="<?=BLOG_ASSETS?>rnr-2.jpg"
                                           class="img-responsive col-xs-12"></div>
    <div class="col-md-8 col-sm-8 col-lg-9">
      <div class="clear20"></div>
      <p>The official HMRC guidelines are as follows; you are considered self employed if you:</p>
      <hr class="no-margin">
      <div class="clear20"></div>
      <ul>
        <li>Run the business for yourself and take responsibility for its success or failure.</li>
        <li>You have several customers at the same time.</li>
        <li>You personally decide how, where and when you work.</li>
        <li>You hire other people at your own expense in order to help you either make or sell your
          goods. </li>
        <li>You agree on any fixed price for what you’re offering.</li>
      </ul>
      <p>and</p>
      <ul>
        <li>You sell these goods or services in order to make a profit.</li>
      </ul>
    </div>
  </div>
  <p>You might not think that you’re running a business empire, but as long as you are trading with the
    intention of earning money there is a good chance that HMRC will consider you to be self-employed.</p>
  <p>As previously mentioned, depending on how profitable your hobby is, you may need to consider the tax
    implications. As a general rule, if any money at all exchanges hands the HMRC calls it a “profit seeking
    motive” and whether you’re making a profit or not, you will need to register as self employed. So what
    you need to do is follow this <a href="#">link</a> and have your NI number and business details to hand
    such as address and the date you started trading.</p>
  <div class="row">
    <div class="col-md-5 col-lg-4 pull-right"><img class="img-responsive col-xs-12 pull-right"
                                                      src="<?=BLOG_ASSETS?>rnr-3.jpg"></div>
    <div class="col-md-7 col-lg-8">
      <div class="clear10"></div>
      <p>So what happens now? You’ve established that your hobby has evolved to the next step and that you
        are officially considered self-employed (even if you have a regular job, remember), you’ve
        registered with HMRC, and now it’s time to make some money. Once things are off the ground you
        must, must, must remember to <a href="#">keep records</a> of any and all business incomes and
        outgoings in order to correctly pay your taxes. This means any receipts for equipment or
        supplies needed, and proof of sales for every single transaction you make.</p>
    </div>
  </div>
  <p>Should you want to take your goods on to the street (on a stall in a marketplace for example) you will
    need to contact your local council who may require you to apply for a street trading licence and even
    pay a fee.</p>
  <div class="row">
    <div class="col-md-5 col-lg-4"><img class="img-responsive col-xs-12"
                                           src="<?=BLOG_ASSETS?>rnr-4.jpg"></div>
    <div class="col-md-7 col-lg-8">
      <div class="clear10"></div>
      <p>Now that you’re self-employed you will be required to complete a self assessment tax return each
        year and pay any tax that’s due. This only applies if your earnings exceed the personal
        allowance of £10,600 (which is important to bear in mind if you have a regular job as well, as
        this makes it more likely that you’ll be earning more than that). Taxes are usually paid in 2
        payments on the 31 January and 31 July, and you can use the official <a href="#">HMRC tax
        calculator</a> to help you correctly budget for this.</p>
    </div>
  </div>
  <div class="clear10"></div>
  <p>The government have been taking all of this very seriously in recent years, clamping down on the 1 in 5
    adults who are considered “hidden entrepreneurs” and as such aren’t correctly paying their taxes.</p>
  <p>With some hard work, a ton of research, and a little luck, you can turn any hobby into a viable source of
    income, just remember that it’s extremely important that you follow the correct rules and regulations
    that are in place as you don’t want to be unknowingly breaking the law and ending up with a hefty
    fine.</p>
