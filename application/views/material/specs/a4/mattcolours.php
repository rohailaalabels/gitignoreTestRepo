  <h3 >Applications and use </h3>
  <p>This face material is designed for the manufacture of  labels on rolls and sheets for use in copier, inkjet(testing advised as results can be  variable), laser and thermal transfer printers  (uncoated white only). The surface structure provides excellent toner bonding  and print resolution.</p>
  <p>This product is ideal for label applications where  face stock appearance is important and colour adds both aesthetics and/or  information such as special occasion address and gift message labels, merit/award  labels, particularly in shaped formats e.g. circles, hearts, ovals, stars and  triangles. Instruction and inventory labels and many other applications. The  face-stock provides limited resistance to moisture, temperature &amp;  chemicals. </p>
  <h3>Approvals (Permanent Adhesive)</h3>
  <p>This label can be used safely for food packaging and  can come into direct contact with both dry and fatty foodstuffs (ISEGA Research &amp;  Development Institute, Aschaffenburg) and is approved  by recommendation XXXVI as published by BfR (Bundesinstitut fur Risikobewertung) the German Federal Institute for Risk Assessment.</p>
  <h3 >Approvals (Peelable Adhesive)</h3>
  <p>The adhesive complies with the European food directives and legislation FDA 21; CFR 175, 105 and the German recommendations XIV as published by BfR (Bundesinstitut fur Risikobewertung) the German Federal Institute for Risk Assessment. Both of which state that the adhesive can be used in direct contact with dry, moist and non-fatty foodstuffs, however according to the FDA regulations the adhesive must be separated from the foodstuffs by a functional barrier.</p>
  <h3 >Shelf Life (Permanent  &amp; Peelable Adhesive)</h3>
  <p> Two years under storage conditions as defined by FINAT (Federation Internationale des Fabricants et Transformateurs d’ Adhesifs et Thermocllants sur papiers et autres supports) 20° - 25° C and 40 – 50% RH.</p>
