                                                                <td style="font-family: Arial; font-size: 16px; color: #16b1e4; text-align: left; line-height: 24px; font-weight:bold;">New Packaging for A4 Label Sheets - Bags
</td>
                                                              </tr>
                                                              <tr>
                                                                <td style="font-family: Arial; font-size: 12px; color: #58585a; text-align: justify; line-height: 18px; ">For smaller quantities of A4 label sheets e.g. up to 100 sheets of matt-white paper labels (WTP) and slightly fewer sheets of other thicker label face-stocks, we have designed an attractive, white "grip seal" low density Polyethylene (LDPE) bag, with an anti-tamper serration to open upon delivery. The labels are kept flat within the bag and protected by a 3mm cardboard insert that helps to maintain the flatness of the label sheets. Once the serrated strip is removed from the bag, the labels can be accessed by opening the "grip seal" and removing the number of sheets required and resealed after use to protect the labels from damage.<br />
<br />
<strong>Biodegrading & Recycling:</strong> With advances in research, it is now possible through various non-chemical treatments to quickly and significantly degrade LDPE, lessening the negative environmental impact and the material can also be recycled for reuse.


</td>
