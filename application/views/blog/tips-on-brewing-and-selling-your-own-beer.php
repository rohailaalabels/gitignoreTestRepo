      <p>But as we become gradually more environmentally aware, there is a big challenge facing any
        beverage or food product aside from packaging alone, and that is transportation (often referred
        to as ‘food miles’). Regardless of whether beer is made from organic materials and bottled in
        recycled glass or aluminium, it still has to be transported form producer to supplier. This
        contributes to greenhouse emissions as well as all the associated oil consumption that goes in
        to production, which seems like a lot of waste to ship what is essentially just water.</p>
