      <h3 >Adhesive</h3>
      <p>A specialist purpose permanent, acrylic based, pressure sensitive, waterborne adhesive (RP Cryo). This adhesive is designed to perform in cryopreservation storage conditions down to -196°C. The adhesive is recommended for identification labelling on Polypropylene (PP) cryogenic vials, tubes and storage boxes (cardboard and PP).</p>
      <p>In addition the adhesive also provides good performance in high temperature applications, withstanding temperatures of up to 121°C for intermittent periods of time.</p>
