            <div id="colour" class="titleOrange">Colour Gamut</div>
            <p><img onerror='imgError(this);' class="helpImage-left img-responsive" src="<?=Assets?>images/help/RGB-CMYK.png" alt="RGB-CMYK Colurs"> The range of hues that can be produced by a given colour space is known as its colour gamut. The gamut of RGB varies from that of CMYK. This can cause issues if you submit RGB artwork to a printer. Your RGB file will have to be converted to CMYK prior to printing. If your image includes colours that exist in the RGB colour gamut but not in the CMYK colour gamut then those colours will be shifted by the conversion software to a nearby colour that is. The new colour may not be what you expect… We most commonly see this with customers who have RGB images with very bright vibrant colours. These almost luminous colours look fantastic when viewed on a screen (which is light emitting remember) but are impossible to reproduce in print (which is light absorbing). Set your artwork up in CMYK in advance! If you are working with artwork that is RGB, convert it to CMYK yourself before sending it to us, so you are prepared for how the image will finally look when printed. A comparison of the colour gamut between RGB and CMYK is shown above. </p>
