    <p><em><strong>2) Absolut Vodka</strong> </em></p>
    <p>I personally love the story of how Absolut chose the packaging that we are all so familiar with today. It
      goes like this: ‘American experts were called in. They recommended modern-looking packaging with a
      bright, colorful label that would stand out on the shelf. They recommended a royal touch, mimicking so
      many of the “Czar themes” on the market at the time. The Swedish team was less than thrilled. ’</p>
    <p>The breakthrough came when a bottle was finally chosen. Like many breakthroughs it came purely by chance
      and in retrospect seems almost too obvious. ’</p>
    <p>Advertising man Gunnar Broman was looking through an antique shop window in Stockholm’s “Old Town” when
      he saw an old Swedish medicine bottle, a cultural icon was unchanged for more than a hundred years. The
      bottle was elegant, different, simple and very Swedish. In the 16th and 17th centuries vodka had been
      sold in pharmacies as medicine to cure everything from colic to the plague. ’</p>
    <p>The choice was a stroke of genius. Several Swedish designers were given the job of helping the Absolut
      Vodka team further develop the bottle. ’</p>
    <p><a href="<?=BLOG_ASSETS?>Absolutvodka.jpg"><img
                            class="size-medium wp-image-457 aligncenter" alt="Absolutvodka"
                            src="<?=BLOG_ASSETS?>Absolutvodka-300x216.jpg" width="300"
                            height="216"></a></p>
    <p>It had been decided that there should be no label to hide the crystal clear contents. After much
      discussion and several prototypes the team came to the conclusion that some kind of coloured lettering
      was required. Blue was decided upon as a the most visible and attractive colour, the colour that is
      still used today for the famous Absolut Vodka logo.’</p>
    <p>Funny enough, I actually enjoy other vodka brands better, purely because of taste. But even though I
      prefer other brands, I always find myself wanting to buy Absolut, because of its packaging. What I
      particularly love about Absolut Vodka packaging, is the way it can be adapted for special occasions and
      flavours – for example, the new copper Absolut Elyx, whose packaging includes copper lines over a clear
      bottle with a copper bottle top. Mixed with genius advertising campaigns to match, no wonder it is the
      world’s best selling Vodka!</p>
    <p><em><strong>3) Ella’s kitchen</strong> </em></p>
    <p>Mothers (especially new ones) are one of the most marketable groups in terms of consumers – they want the
      best for their child, and will go great lengths to get it! But they also want easy-to-use products that
      work for them. Great for mums on the go are Ella’s Kitchen food products. Gone are glass jars and hard
      to open lids – instead, baby food comes inside a squeezy pouch which keeps food fresh even when it’s
      been inside a warm changing bag all day. Ella’s Kitchen says:</p>
    <p>‘We believe that, just as adults do, babies eat with all of their senses – so it’s not just the texture
      or taste that’s important. That’s why we make sure all our scrummy, 100% organic baby food appeals to
      all their senses, with bright, colourful, squeezy packs that we know they love to grab and squish.’</p>
