                    <div class="col-xs-12 col-sm-12 col-md-12">
                        <p class="text-justify">
                            The Harmonized System is used to ease global trade by creating unified categories to
                            classify different types of goods.</p>
                        <p class="text-justify">The Harmonized System categorizes about 5,000 commodity groups in simple
                            six-digit codes, broken down into 21 Harmonized System sections, 96 Harmonized System
                            chapters, and thousands of headings.
                        </p>
                    </div>


