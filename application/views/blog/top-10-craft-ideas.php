        <h2 class="headingPost">DIY String Lantern</h2>
        <p>This relatively simple project allows you to create great looking and functional lanterns and
          lampshades. They really aren’t that difficult to make either, all you need to do is wrap cotton
          string around a ball or balloon, paint it in any colour you wish, and then simply pop or deflate
          what’s inside once the paint has dried, and then carefully remove it. Be sure to leave a large
          enough gap somewhere to allow the lantern to fit around a light bulb fixture.</p>
