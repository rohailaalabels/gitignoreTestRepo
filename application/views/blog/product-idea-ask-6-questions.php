    <p>The 11th series of Dragons Den opened in August 2013 with approx. 2.95 million viewers, according to <a
                        href="http://www.digitalspy.co.uk/tv/news/a505891/dragons-den-series-11-opens-with-29-million-on-bbc-two.html"
                        target="_blank">DigitalSpy</a>. Many of us settle down on a Sunday night ready to watch an
      episode, and almost all of us will have said at least once ‘that will never sell’. But what will sell? </p>
    <p>Lots of us aspire to own our own successful business, wracking our brains trying to come up with the
      latest design or trendy product in the hope there is a product or service out there that someone has
      failed to identify that is right under our nose!</p>
    <p>But how many products actually make it to market, and how many succeed and fail every year? If you are
      confident enough to bring a product to market is there anything you can do to try and gauge consumer or
      customer response? Here are a few questions to ask yourself before you begin:</p>
    <h3>Q1. Do you need to protect your product, and make sure you are not infringing?</h3>
    <p>I would suggest before you start day dreaming about the most amazingly catchy name, or thinking you have
      the most “unique” idea since the iPod, you should undertake a basic search to make sure you are not
      infringing on someone else’s patent, name or design. The IPO (Intellectual Property Office) can give you
      information and advice if needed on Trademarks, Patents and licencing among other things – <a
                        href="http://www.ipo.gov.uk/" target="_blank">http://www.ipo.gov.uk/</a> .</p>
    <p>You can conduct a basic TM search on their website which will tell you immediately if someone else has a
      similar product name for example. If you want to go ahead and register a name online, it can be
      relatively straightforward and cost as little as £200. However if you have no previous experience with
      this type of activity, then it may be worth contacting a specialist Trademark lawyer, but be aware that
      this can be quite an expensive process.</p>
    <p>It is also important to point out that unless you are willing to invest in protecting your Trademark or
      patent then think carefully about this – if you do find someone who you think infringes your
      intellectual property, will you have the funds, experience or confidence to go through with legal action
      for example? If not then it may be worth seeking specialist advice before you start to ascertain where
      you stand!</p>
    <h3>Q2. Have you done your research and do you know your market?</h3>
    <p>Just because people like the product you have invented, it doesn’t mean that your product will be
      commercially viable or even profitable. Are the people who like your product willing to purchase it and
      at what price? Who and what is your competition?</p>
    <p>You need to establish your USP (Unique Selling Point) and determine whether this is enough to make it a
      success. Often a product will take off fantastically well, but how are you planning to maintain sales or
      increase moving forward?</p>
    <p>Many manufacturers see the benefit of having a “razorblade” scenario – where you are essentially selling
      a “razor” (for example a fragrance dispenser), and then a “razor blade” (an aerosol refill). This way,
      even when a customer has purchased a dispenser from you they will still require a refill for the product
      to make it work. In other words, you are securing repeat business! Could this apply to your idea?</p>
    <h3>Q3. How are you planning to bring your product to market?</h3>
    <p>Have you thought about how you will manufacture your product – will you do this yourself, and is this
      financially feasible? How will you store the items and are there any restrictions on shipping or
      handling your inventory (an example being hazardous materials or customs and export issues)? One idea is
      to approach another manufacturer and ask them to do it on your behalf. Be aware however, if this is a
      route it looks likely you will take, to be cautious and ask them to complete a confidential agreement or
      NDA (non-disclosure agreement) to stop them copying or stealing your idea!</p>
    <h3>Q4. Are you familiar with the Marketing Mix – (The four P’s)</h3>
    <p>You may have heard this term before, even if you don’t know what it means. The four P’s were classified
      in 1960 by E. Jerome McCarthy, and have been used by marketers since. Essentially they are “Price,
      Product, Promotion and Place” and are a business tool which aims to determine a product’s offering. A
      simple and basic explanation of this can be found here and is worth a read: <a
                        href="http://www.completebusinessstartup.co.uk/starting-business/the-4ps.html" target="_blank">http://www.completebusinessstartup.co.uk/starting-business/the-4ps.html</a>. </p>
