  <h2 style="text-align: left;" align="center"><b>Did Heinz succeed with their redesign of the Mayo product
    label?</b></h2>
  <p>Rewind to 2011. I was out shopping for some Heinz Mayonnaise (who doesn’t love a spoonful of mayo in
    their sandwich) and had to look twice when I reached the aisle – was this the right Heinz Mayonnaise
    that I had bought for years? It looked different – so I checked it over. It was definitely Heinz, only
    they had changed the label.</p>
  <p>The new look Heinz Mayonnaise had a new dark blue label and included the traditional Heinz keystone. The
    ‘light’ variety featured a light blue label, to differentiate calorie contents.</p>
