    <h3 class="headingPost">Thermal Transfer Printing</h3>
    <p>With Thermal Transfer printing and printers, the thermal print-head instead of applying heat directly to
      a thermochromic coated surface, the heat is applied to a resin based or wax ribbon which is melted to
      the label surface and the ink absorbed into the label material.</p>
    <p><img class="img-responsive aligncenter size-full wp-image-1827"
                    src="<?=BLOG_ASSETS?>thermal.jpg" alt="" width="404" height="279"
                    srcset="<?=BLOG_ASSETS?>thermal.jpg 404w, <?=BLOG_ASSETS?>thermal-300x207.jpg 300w"
                    sizes="(max-width: 404px) 100vw, 404px"><br>
      <strong>Brief Summary</strong></p>
    <ul class="ribbon_li">
      <li>Ribbon required for printing</li>
      <li>Different colour ribbons provide colour options</li>
      <li>The printed image should not age and fade</li>
      <li>The wax and/or resin image is usually durable and resistant to chemicals and temperature.</li>
      <li>Good reproductive quality</li>
      <li>Compatible with a large range of label face-stocks</li>
      <li>Commonly used for product ID’s, asset and inventory labels</li>
    </ul>
    <p>Because environmental factors will not alter the print quality, this type of technology is known to
      create a very strong printed area that should withstand extreme temperatures and contact with chemicals.
      This print method is recommended for labels that need to last longer than six months.</p>
    <p>In contrast to Direct Thermal printing Thermal Transfer printing provides the ability to use spot-colours
      via the change of ribbon. Because the label face-stock does not require a pre-coated surface like Direct
      Thermal this print method is also very versatile in terms of the label materials that can be used.</p>
