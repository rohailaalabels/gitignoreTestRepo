    <h2>The honey label design</h2>
    <p>For this label set the brief was to create a simple bee stripe design that included all the necessary
      requirements for labelling regulations. It also needed to be printable from most printers to allow for
      anyone to print it whatever printer they had.</p>
    <p>So a simple but striking design was created for this honey label set. Used on the jar label, lid label
      and tamper label, the black stripes dominate the design. Unsurprisingly they are used to indicate the
      stripes that most people associate with bees – especially when there’s honey in the jar!</p>
    <p>It’s in black and white for a variety of reasons. Firstly it allows anyone with a printer to use the
      design – you obviously don’t need a colour printer. You can also choose to print the design on a sheet
      of coloured labels if you’d like to introduce colour to the design. So using black and white allows you
      quite a bit of flexibility.</p>
