  <h3 >Applications and use </h3>
  <p> This face material is designed for the manufacture of labels on rolls and sheets for use in copier, inkjet, laser and thermal transfer printers (uncoated white only). The surface structure provides excellent toner bonding and print resolution. </p>
  <p> This product is ideal for label applications where face stock appearance is less important such as address, instruction and inventory labels and many other applications. The Face-Stock provides limited resistance to moisture, temperature & chemicals. </p>
  <p> The label has been primarily designed for the purpose of labelling high value products, removable after purchase, but has numerous other applications, such as temporary delegate and guest badges, access and entry labels, charity clothing labels etc. </p>
  <h3>Approvals</h3>
  <p>The adhesive complies with the European food directives and legislation FDA 21; CFR 175, 105 and the German recommendations XIV as published by BfR (Bundesinstitut fur Risikobewertung) the German Federal Institute for Risk Assessment. Both of which state that the adhesive can be used in direct contact with dry, moist and non-fatty foodstuffs, however according to the FDA regulations the adhesive must be separated from the foodstuffs by a functional barrier.</p>
  <h3 >Shelf Life</h3>
  <p> Two years under storage conditions as defined by FINAT (Federation Internationale des Fabricants et Transformateurs d’ Adhesifs et Thermocllants sur papiers et autres supports) 20° - 25° C and 40 – 50% RH.</p>
