                                                                The  reason why standard permanent adhesives will not stick to new tyres is because  of the silicone present from the casting mould. Similarly new automotive body  parts, kitchen worktops, sink units and melons with a “waxy” skin. Anything  that has been sealed for protection from abrasion and handling damage, or in the  case of fruit produces a protective surface.</td>
