      <p>Your wedding day is going to be one of the most enjoyable experiences you’re ever likely to have, but
        unfortunately, as more and more details emerge during the planning phase, it’s going to turn out to be
        one of the most stressful (and expensive) things to do as well.</p>
      <p>When making plans it’s all too easy to get over-excited in creating the most fantastical wedding
        imaginable, but the reality of it is, unless you’re super rich, you’re going to want to keep the cost
        down as much as possible.</p>
      <p>So first things first, you need to establish a set budget and stick to it. Don’t fall into the trap of
        not doing this straight away, as the costs can quickly get away from you.</p>
