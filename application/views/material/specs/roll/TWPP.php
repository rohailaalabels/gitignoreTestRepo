  <h3 >Applications and use </h3>
  <p>This product combines the characteristics of thermal papers with the durability of Polypropylene films and printing speeds of up to 200mm per second are possible in direct thermal printing.</p>
  <p>The adhesive has a very high tack even on cold surfaces down to -10° C. Due to the high initial tack even on moist surfaces this label is particularly suited to labelling polar and non-polar surfaces, such as plastic containers, film wrappings and other packaging materials. The adhesive works well for fast running labels dispensers, in which the initial tack has to be built up extremely quickly, once the labels are blown onto the substrate.</p>
  <p>Once labelled the adhesive is also highly waterproof.</p>
  <p>The Polypropylene face-stock features a high resistance to water and oil. It does not contain BPA adn is suitable for applications in market segments where labels may require protection via the top-coat from grease and oils, such as fresh food “Delicatessen” counters, as food and meat labels. Also where abrasion may occur in commercial and industrial applications and durability is required e.g. logistics labels, luggage and packaging labels.</p>
  <h3>Conversion and printing</h3>
  <p>Although a direct thermal heat sensitive film, printing with traditional printing methods such as UV Flexo, or UV Offset Litho. However the inks should be suitable for printing thermal films and consulting the ink supplier is recommended. A corona treatment prior to printing may have a positive effect in ink trapping and adhesion. The web temperature during curing should not exceed 50° C in order to prevent an early reaction of the thermo reactive ink. </p>
  <h3>Approvals</h3>
  <p>The thermal film according to the sample material is physiologically and toxicologically safe when used as intended and may therefore be used in printers and scales for labelling food and goods. However it may only stand in direct contact with such kind of foodstuffs which are peeled, shelled or washed before consumption according to experience. (ISEGA Research & Development Institute, Aschaffenburg.)</p>
  <p>All ingredients of the adhesive comply with the recommendations BfR XIV and FDA 21 & CFR 175.105 (Adhesives).</p>
  <p>The paper release liner complies with recommendation BfRXXXVI</p>
  <h3 >Shelf Life</h3>
  <p>Two years under storage conditions as defined by FINAT <small>(Federation Internationale des Fabricants et Transformateurs d&acute; Adhesifs et Thermocllants sur papiers et autres supports)</small> 20° - 25° C and 40 – 50% RH. Dark storage is recommended and moisture, heat and direct sunlight also have to be avoided.</p>
