                          <h3 >Applications and use </h3>
                          <p>This label is designed with extreme tamper evidence as a priority. The heavily filled face is highly frangible, of low calliper, providing little label edge and thereby increasing the difficulty of removal, while being both conformable and semi-flexible to avoid accidental breakage after application.</p>
                         
                          <p>The adhesive is aggressive on a wide range of flat and (assisted via film conformability) curved surfaces, both polar and a-polar. These typically include glass, steel, varnished and unvarnished carton boards, along with most plastics used in packaging and mouldings.</p>
                          
                          <p>The specific combination of face-stock and adhesive results in a label which will usually only be removable in very small pieces, destroying it completely. At the same time the face has been specifically formulated with just enough tensile/tear strength to allow simple label shapes to be die-cut and the waste matrix stripped for subsequent automatic application where required.</p>
                          <p>The excellent durability of the combination of face-stock and adhesive facilitates its use for both indoor and outdoor applications, including identification and inspection record labels for instrument panels and equipment, warning signage, information labels on electrical appliances and tamper evident security seals.</p>
                          
        
           <h3 >Applications and Printing </h3>  
           <p>This product is designed for both automatic and manual application. The label can be printed by all conventional roll label technologies, including water based inks, UV letterpress and screen print. However as with all materials we recommend ordering a FOC material sample for evaluation and print testing before the placing of an order.</p>              
                     
                         
