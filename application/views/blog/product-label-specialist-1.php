    <p>Sourcing product labels, amongst other things, is a thoroughly rewarding job as you get to see a product
      go from an idea to reality. One of the beautiful things about it is that on looking at the finished
      product, I know that the consumer will view it in a very different way to how I do. They can’t quite
      imagine the blood, sweat and tears that has actually gone into it (not to mention the hundreds of
      decisions made at my level), but it works, because ‘it just does’. Making something appear ‘seamless’ in
      the way it has transformed from a concept to something concrete is to me what makes something a success.
      It might be revolutionary, but I like to make it look easy!</p>
    <p>There are a number of traits that you need to have to be in my job. You need to be open-minded, and a
      good listener. You are always learning, analysing, and revisiting. The ability to take other people’s
      thoughts and opinions (and perhaps even constructive criticism) on board is crucial, yet the ability to
      make the final decision concisely and confidently is also key. Good analytical skills are critical to
      look at a product and decide what it really needs.</p>
    <p>You need to be a good communicator. You’re often ‘in the middle’ – bridging every department that
      ‘touches’ the product. You might be working with Managing Directors, Sales Teams, Marketing, Customer
      Service, Design, Operations, and more. Often, they’ll have different points of view, so you have to be
      prepared to fight your corner whilst also acknowledging other people’s frustrations and needs.</p>
    <p>So what does the person in charge of product labelling actually do? Well, most of their role is focused
      on delivering solutions to market needs, whilst keeping budget considerations in mind. They need to
      identify a labelling solution that meets the product’s needs. Then, they’ll need to assist marketing in
      launching that product into the market, whilst also overseeing other products that are already on the
      shelves.</p>
    <p>So, if I was sourcing a new product label, typically where would I start?<b>&nbsp;</b></p>
    <p>&nbsp;</p>
    <h4><b>Research </b></h4>
    <p>You need to have a real in-depth understanding of your market. Who is your target audience? What do they
      want, like, respond to? Where do they buy, how do they buy? What are competitors doing? What can we do
      better? It might seem like I’m reenacting my own episode of Question Time, but it really is so important
      to do your research.</p>
    <p>One of the biggest mistakes product managers can make is to partake in a game of ‘Chinese Whispers’. The
      customer tells the sales team that when it comes to instructions, they’d like them in a larger font size
      on the label, to make them easier to read. The sales team then tells marketing they’d like a different
      font on the label. Marketing then tells you that they’d like a new font layout on the label. Can you see
      where I’m going with this? It is important that you do your own research to find out exactly what
      changes you need to make and why. Ask targeted questions, and challenge assumptions. Leverage the
      knowledge of experts inside and outside the company.</p>
    <p>&nbsp;</p>
    <h4><b>Budget</b></h4>
    <p>Ever heard of fashion over function? It’s something I’m victim to every Saturday night (and I always end
      up wishing I had listened to my mother)!</p>
    <p>Often, you can get so carried away with how something looks that you start to forget all about function,
      or worse; that horrible ‘B’ word (budget). But that looks soooooo nice, does an extra 2p per label
      really matter? The answer is yes, it really does. It might look and work great, but no sales guy is
      going to thank you when they can’t sell the thing. Splashing out on the small things will add up, and no
      Managing Director likes a pricey product with a small profit margin. Before you even start looking at
      labelling options, set a realistic budget, and stick to it.</p>
    <p>&nbsp;</p>
    <h4><b>Checklist</b></h4>
    <p>Next comes thinking about materials, aesthetics… that sort of thing. Do you want to print your own
      labels? What material do they need to be to stay on the product? How long will the labels last? Will
      they last with the product? How will they be assembled? Do you want them to be removable? If not, can
      they be removed? What look are you going for, matt or high gloss?</p>
    <p>See a familiar pattern here? We’re back to Question Time. I can’t reiterate enough though, how important
      it is to have a checklist drawn out before you start to explore suppliers.</p>
    <p>&nbsp;</p>
    <h4><b>Ask the experts</b></h4>
    <p>Ok, so I have been working in this field for years, but no one knows more than the actual labelling
      manufacturers themselves. Now you have your checklist, a quality labelling supplier will be able to
      guide you through which options are best suited to your needs. Often, they will provide samples for you
      to have a look at. Most of all, you want to ensure that you are getting a high quality label at a
      competitive price. Shop around, but make sure you set yourself a realistic timeframe of when the project
      will start and when it will end – remember, the more time it takes for you to get the product to market,
      the less your company is making on sales (and the more your Managing Director will be on your back
      trying to make it happen).</p>
    <p>So we’ve done our research, we’ve made a checklist, and we’ve called the experts in. What’s next? Next
