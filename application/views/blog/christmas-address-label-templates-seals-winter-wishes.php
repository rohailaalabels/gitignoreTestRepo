  <p><strong id="designer">This is what our honey label designer has to say:</strong></p>
  <p>Winter Wishes Address Label Range</p>
  <p>Christmas is a time of giving. It is also a time when people send their love and thoughts to others
    through cards and letters. Whether it is their best friend in the whole wide world or a long lost friend
    they have not seen for years, Christmas is a time of telling people that you are dear to them.</p>
  <p>I love receiving mail from family and friends. I still get excited when I see a letter by my front door
    with my name on it (Unless it is a bill). It is this great feeling that was my main source of
    inspiration for the address labels. I really wanted to design lovely stickers that would make someone
    smile and get excited before they even opened up the envelope for the surprise that’s inside.</p>
  <p>When designing this range of address labels and stickers, 1950s illustrations and colours were my
    inspiration. I wanted them to have a retro feel but with a modern twist. Even though I used the
    traditional red and green Christmas colours, I feel the shades I used help to give the design that 50s
    vibe I was going for. But the font I chose and the minimalistic illustrations help to keep it
    modern.</p>
  <p>One of my favourite parts of Christmas is decorating the tree and wrapping up the presents to go under
    it. So I thought what better design to do other than a tree and some baubles. I think these stickers
    could make great labels for presents too! I could imagine a brown paper package with one of these bright
    labels stuck on it and some matching red ribbon.</p>
  <p>I hope your friends and family will get as much joy receiving a card or gift that is adorned with these
    labels as I did designing them.</p>
