  <h3 >Applications and use </h3>
  <p>This  label material performs excellently on very difficult surfaces e.g. HDPE (High Density Polyethylene) drums. The product also offers  good medium-life durability and is therefore ideal for tracking/warehouse  applications and also product/component identification in industrial  applications.</p>
  <h3>Conversion &amp; Printing</h3>
  <p>The  label has been specifically designed for sheet-fed and continuous laser  printing through high speed industrial laser printers. The product displays  optimal flatness during conversion, storage and the industrial laser printer  processing. Some curl may be seen on exiting the printer but this will flatten  quickly. The backing paper is specially suited to the more critical  re-fan-folding demands of high-speed computer printers, providing optimum  lay-flatness and excellent inter-folding characteristics.</p>
  <p>The  product is also suitable for overprinting with other print technologies i.e.  dot matrix, thermal transfer (in combination with the correct ribbon grade and  machine adjustment), ink jet (up to 720 dpi). The face-stock can also be  pre-printed by conventional printing processes, such as flexographic and offset  lithographic. For flexographic good results can be achieved with water or  solvent-based printing inks. Consult your printing ink supplier about suitable  printing inks for your application.</p>
  <p>In  view of the enormous variety of printers on the market we recommend carrying  out process trials beforehand.</p>
  <h3>Approvals</h3>
  <p>The  adhesive complies with the European food directives and legislation FDA 21; CFR  175, 105 and the German recommendations XIV as published by BfR (Bundesinstitut fur Risikobewertung) the German Federal  Institute for Risk Assessment. Both of which state that the adhesive can be  used in direct contact with dry, moist and non-fatty foodstuffs, however  according to the FDA regulations the adhesive must be separated from the  foodstuffs by a functional barrier.</p>
  <h3 >Shelf Life</h3>
  <p>Two years under storage conditions as defined by FINAT (Federation Internationale des Fabricants et Transformateurs  d&rsquo; Adhesifs et Thermocllants sur papiers et autres supports) 20° - 25° C and 40 – 50% RH. </p>
