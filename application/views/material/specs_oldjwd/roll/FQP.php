                          <h3 >Applications and use </h3>
						  <p>This high quality slightly textured face material is designed for the manufacture of labels for application onto glass containers, both bottles and jars for drinks, fragrances, oils, preserves etc.</p>
                          <p>This product is ideal for label applications where face stock appearance is highly important in terms of brand image and appearance. It is suitable for primary labelling of high-end and luxury goods with a modern image.</p>
                        
                          <h3 >Conversion & Printing</h3>
                          
                         <p>Printable by all conventional printing techniques. The best results are in general from those techniques with maximum conformability of printing plate, such as flexographic  and offset litho. Also excellent results can be achieved, in line with a desired image, using offset or screen.</p>
                          <h3 >Approvals</h3>
                         <p>The adhesive complies with the European food directives and legislation FDA 21; CFR 175, 105 and the German recommendations XIV as published by BfR (Bundesinstitut fur Risikobewertung) the German Federal Institute for Risk Assessment. Both of which state that the adhesive can be used in direct contact with dry, moist and non-fatty foodstuffs, however according to the FDA regulations the adhesive must be separated from the foodstuffs by a functional barrier.</p>
                          
                          <h3 >Shelf Life</h3>
                         <p>Two years under storage conditions as defined by FINAT <small>(Federation Internationale des Fabricants et Transformateurs d’ Adhesifs et Thermocllants sur papiers et autres supports)</small> 20° - 25° C and 40 – 50% RH.</p>
