  <p>Features of the PDF templates:</p>
  <ul>
    <li>You can edit all the text.</li>
    <li>You can delete any of the text if it’s not required for your particular use.</li>
    <li>You can change the font and colour of the text to match your occasion or brand.</li>
    <li>You can change the size of the text.</li>
    <li><strong>Autofill</strong> template feature: When you make a change any of the text it will change on
      the other labels in the template. This saves time if you’re printing several labels with the same
      text. </li>
    <li><strong>Fillable</strong> template feature: This type of template will allow you to enter different
      text in each label for when you want to print individual sweet labels. </li>
