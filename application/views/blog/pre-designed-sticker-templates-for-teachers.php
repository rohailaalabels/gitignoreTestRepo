    <p>While much of the team here have been cutting labels and fulfilling your orders. I have been fumbling
      around trying to find my creative cap. The reason? To come up with and create some label templates that
      teachers can use for feedback.</p>
    <p>If you’re a teacher or work at a school you’ll know how important feedback and marking is to give
      feedback to pupils in a way that is engaging and useful. And so there are a lot of different ways this
      can be done – plenaries, formative assessment, even peer and self-feedback. Here though, we’re
      specifically looking at how we can use sticky labels as a quick tool to give pupils an idea of where
      they are.</p>
    <p>That’s why I created a set of pre-designed label templates that focus on learning objectives. The
      feedback options are ‘Objective Met’, ‘Progress Towards Objective’ or ‘Objective not met’.</p>
    <p>I set about coming up with ideas for different concepts that could be applied to those three marking
      choices. I wanted each set of stickers to have a theme that would carry through from Objective Met to
      Objective Not Met so that there was a visual cue as well as a written one to show what level the pupil
      is at.</p>
    <p>The most challenging part in designing these templates was coming up with an Objective Not Met that kept
      to each theme but wouldn’t cause upset or too much disappointment and yet still get across that a lot
      more work was needed to achieve a higher level. A lot of the initial ideas I had fell down on that
      part.</p>
    <p>One idea I rejected was:</p>
    <p>Objective Met = Smiley face in green<br>
      Progress Towards Objective = Straight face in yellow<br>
      Objective Not Met = Sad face in red</p>
    <p>Here’s how it looked:</p>
    <p class="col-xs-10 col-xs-offset-2"><a href="<?=BLOG_ASSETS?>smiley-faces-marking-label.png"><img
                            class="img-responsive " alt="Smiley faces marking label"
