      <h2 class="headingPost">LABELS FOR ROLL LABEL PRINTERS</h2>
      <p>For those new to or inexperienced in the ordering of plain labels for roll label printers this
        article is intended to help with some key issues and ensure that the correct label material and size
        are ordered, the diameter of the label roll/s fit in the printer and that the printer sensors can
        detect the labels to be printed.</p>
      <h2 class="headingPost">PRINTERS</h2>
      <p>There are currently two types of printers manufactured specifically for the printing of pre-cut
        labels, which are Inkjet and Thermal printer technologies. There are some Laser (dry toner) printers
        for the printing of rolls as well as flat sheet format, but these are pre-conversion, reel-to-reel
        format and not for post conversion, pre-cut labels.</p>
      <h2 class="headingPost">Inkjet Printers</h2>
      <p>This technology produces image and text on the labels using aqueous ink in a dye or pigment format.
        Since their introduction in the latter-half of the 1980’s, inkjet printers have grown in popularity
        and performance.<br>

      </p><img class="col-lg-12 img-responsive aligncenter size-full wp-image-1798"
               src="<?=BLOG_ASSETS?>printer-img-1.png">
      <p>An inkjet label printer is any printer that places extremely small droplets of ink onto a paper or
        Polymer label to create an image. The dots of ink are extremely small (in desktop printers, usually
        between 50 and 60 microns in diameter), so small that they are less than the diameter of a human
        hair (80 microns). The amount of ink dispensed by the print heads is measured in Picolitres (1
        Trillionth of a litre) and in larger commercial inkjet printers the number of Picolitres dispensed
        varies by printhead nozzle, typically from 6 – 11 Picolitres, but in desktop printers the printhead
        nozzles are commonly a standard 75 microns. This enables a very precise positioning of ink dots,
        with resolutions of up to 1,440 x 720 dots per inch (DPI). The dots deposited from the ink
        cartridges are typically CMYK in desktop printers and combine colours together in varying quantities
        of these minute dots to create photo-quality images.</p>
      <p><img class="img-responsive aligncenter size-full wp-image-1801 col-lg-12"
                        src="<?=BLOG_ASSETS?>printer-img-2.png" alt="" width="658"
                        height="184"
                        srcset="<?=BLOG_ASSETS?>printer-img-2.png 658w, <?=BLOG_ASSETS?>printer-img-2-300x84.png 300w"
                        sizes="(max-width: 658px) 100vw, 658px"></p>
      <p>Inks are either dye based which consist of a colourant that is fully dissolved and suspended in
        liquid or a pigment ink which consists of a very fine powder of solid colourant particles suspended
        in a liquid carrier. The main difference between dyes and pigments is the particle size and
        therefore dyes are not UV stable whereas pigments are usually UV stable.</p>
      <h2 class="headingPost">Label Materials for Inkjet Printers</h2>
      <p>Because inkjet printers dispense wet ink onto the label surface (face-stock) the ink requires time to
        dry through surface absorption and evaporation. Plain label face-stocks, with absorption
        capabilities therefore work best with inkjet printers i.e. uncoated matt papers.</p>
      <p>Coated papers, such as semi-gloss, gloss and high gloss, along with Polymers will generally not work
        well with desktop inkjet printers as the surface coating in the case of paper and the material
        composition of Polymers will prevent absorption and without heated dryer’s in the printer,
        evaporation will not compensate. Leaving the printed labels at risk of smudging as they emerge from
        the printer.</p>
      <p>The solution is to coat the gloss finish papers and Polymers with a chemical that reacts with the ink
        and dries the ink. Therefore, when selecting a paper label with a gloss finish or a Polymer label
        i.e. Polyester, Polyethylene or Polypropylene always be sure that there is an “Inkjet” version of
        the label material available.</p>
