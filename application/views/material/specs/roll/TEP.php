  <h3 >Applications and use </h3>
  <p>This Direct Thermal product is designed for use in dry weigh scales, process tracking, POS and item information labelling where limited image durability is required. Typical applications include fruit and vegetable weighing scales, hardware, WIP material tracking and any other application for variable information labelling in dry environments e.g. POS price marking, barcodes, shipping forms etc.</p>
  <h3 >Conversion and printing</h3>
  <p>This product is designed to be converted and dispensed at high speed by all conventional roll conversion techniques, including flexographic and UV letterpress. Due to the thermo-graphic properties exposure above 50° C may cause premature imaging or discolouration. Inks containing alcohol or volatile organic solvents may also cause discolouration. It is advisable to test inks and varnishes before conversion.</p>
  <p>Also contact with moisture, oil, fats, plasticisers, strong lighting and direct sunlight for prolonged periods should be avoided because of potential image fade issues.</p>
