      <h2 class="headingPost">TEMPLATES</h2>
      <p>Alignment issues when printing can be both mechanical and software. If the registration of your print
        is progressively off centre or outside of the label edge, then the label template being used is
        set-up incorrectly and is misaligned.</p>
      <p>The most common cause of the labels not matching with the template downloaded is the printer settings
        being used, in the above example scaling would be enabled causing the increasing misalignment and
        can be simply rectified by turning the scaling off.</p>
      <p>Check to ensure that the correct template is being used and always refer to the printer manual
        regarding instructions on the setting options for printing labels. Some printers have specific
        settings that you will need to follow.</p>
      <p>Changing the printer settings for thicker label substrates e.g. Polyethylene and transparent, or high
        gloss face-stocks is usually necessary for most printers. You will need to change the printer
        properties settings for the type of label to be printed and/or the specific thickness and
        weight.</p>
      <p>The printer settings in most models can be accessed by pressing “Print” and a user interface opens,
        in which “Properties” should be an option. In which there will be a dialogue to change print quality
        and the media to be printed.</p>
      <p>It can be useful to print out a hard copy of the blank template with the cutter guides (label edge)
        showing and feed through the printer again and print out your print file to check the alignment and
        registration, before printing labels.</p>
      <h2 class="headingPost">INK SMUDGING &amp; TONER FLAKING</h2>
      <h4>INKJET PRINTER</h4>
      <p>Papers and films made for inkjet printing have a coating which absorbs ink. If your printer is set to
        the Best Print Quality or the highest DPI setting, it is possible the printer is saturating the
        surface and the amount of ink being used will not dry quickly enough to prevent smearing and
        smudging on emergence from the printer.</p>
      <p>If you are only printing one sheet at a time and can afford to wait for the ink to dry, then this
        might not be an issue, but if you are printing multiple sheets at a time, then this will be a
        problem.</p>
      <p>The solution is to only use inkjet specific gloss and transparent label materials and to experiment
        with the print quality options to reduce the ink loading.</p>
      <h4>LASER PRINTERS</h4>
      <p>The two main causes of poor toner bonding causing flaking and smudging is either old, or poor-quality
        toner and dirty rollers. Reducing the amount of toner being used by adjusting the DPI, image
        resolution or print quality can help, but cleaning the printer and changing the toner is the best
        solution.</p>
      <h2 class="headingPost">LOADING LABELS INTO THE PRINTER</h2>
      <p>As label sheet travel through the printer’s transport path, pulled by the small transport rollers
        there can be some marginal movement which if small label sizes are being printed will be noticeable
        in the form of mis-registration of print. To help prevent this always ensure that labels have been
        fanned to remove any static that might be present and sacked neatly and centrally in the feed
        tray.</p>
      <p>Try to avoid touching the label face-stock, as fingertips leave a miniscule oil residue that can
        affect the print quality.</p>
