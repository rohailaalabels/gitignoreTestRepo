  <p>The aim of this brand redesign was to keep things consistent, <a
                        href="http://www.packagingnews.co.uk/news/heinz-unveils-new-pack-design/" target="_blank">said
    Heinz</a>. “Currently, Heinz Mayonnaise is branded differently for foodservice and retail, which can
    lead consumers to struggle with connecting the two brands. To ensure one consistent brand image, the
    redesign will be implemented across both sectors with all consumer facing foodservice packs aligning by
    the end of the summer. The redesign is also being supported with an on-pack promotion, offering
    consumers their money back if they decide “that Heinz is not their favourite mayonnaise”.</p>
  <p>This got me thinking – what impact did this redesign have on me, the consumer? I can sum up my initial
    thoughts and feelings in two stages:</p>
  <ol>
    <li><b>Confusion.</b> I buy Heinz Mayonnaise regularly, which means I am familiar with their branding.
      Without even thinking, I can recognise the Heinz Mayonnaise label from anywhere. So, upon seeing a
      brand I know and&nbsp;love with a different label, it got me thinking – was this the real deal? </li>
    <li><b>Intrigue.</b> Once I realised that this was in fact the Heinz Mayonnaise that I had been buying
      for years, I found myself asking ‘what does this new label mean? Actually, it is quite attractive.
      Now I can easily define the lower calorie mayonnaise in my fridge! It might even be&nbsp;easier for
      me to spot down a shopping aisle.’ </li>
  </ol>
  <p>So what does this mean for established brands thinking of undertaking a label redesign? It means that
    they should expect it to take some time for people to ‘settle into’ the new design. They might even face
    some resistance to it. However, once this time passes, they can expect positive results, but only as
    long as they have a clear and definitive reason to change their label in the first place. The label
    needs to ‘do what it is there to do’.</p>
  <p>Heinz were on a mission. They wanted people to recognise one Heinz Mayonnaise brand and one only. They
    wanted to differentiate between full fat mayo and the light stuff. In this way, the new label worked. It
    did what it was supposed to do, so people liked it. It made their life easier (well, it certainly made
    my life easier)! And it even offered a promotion.</p>
  <p>People tend to be scared of the unknown – we are all creatures of habit. We like familiarity, so a
    totally new look for you and your brand might not be the way to go!</p>
  <p>But like any good marketing decision, especially one about branding, you must take a risk in order to be
    successful.&nbsp; Perhaps, one of the most important messages in marketing is that if you start with a
    clear method to your madness, you’re more likely for success.&nbsp; A complete re-design doesn’t
    necessarily mean that you have to change each and every element of your product (or product label) to
    succeed. Like Heinz, it may be that just by changing the look and feel of the packaging label, it is
    enough of a subtle change to improve your consumers’ buying experience whilst still keeping the overall
    corporate feel of your brand that has been held for decades.</p>
  <p>We’d love to know if, like Heinz, any of you have tried it!</p>
