      <h3 >Applications and use </h3>
      <p>For product labelling in applications where high opacity and resistance against chemicals, oils and water are important considerations. Also, with the advantages of very high opacity, good dimensional stability, good strength at low temperatures and excellent UV stability. </p>
      <p>A label material well suited to commercial and industrial usage, with chemical and logistics industry application and due to the caliper and stability of the material large label sizes can also be used. applications Suitable for thermal-transfer overprinted information labels.</p>
      
      <h3 >Conversion & Printing</h3>
      <p>For commercial printing the labels can be printed by flexography, screen and offset printing methods. For the best results a Corona treatment should be applied on press for optimal ink adhesion. </p>
      <p>For non-commercial printing the material is only really consistently compatible with thermal-transfer printing. </p>
      
      
      <h3>Approvals</h3>
      <p>The ISEGA research centre has approved the adhesive RP37 according to European requirements for direct contact with dry and moist, non-fatty foodstuffs, as well as fatty foodstuffs with a correction factor of at least 2, or for which the foodstuff stimulant D1 applies according to Regulation (EU) No. 10/2011. In addition, labels coated with this adhesive may be used to label food packaging materials. According to FDA regulations RP37 may come into direct contact with dry foodstuffs, or it must be separated from foodstuffs by a functional barrier. RP37 is approved from the patient safety point of view (migration tested) for labelling blood bags ISEGA has EN ISO 17025 accreditation for analytics and EN45011fro certification.</p>
      
      
      
