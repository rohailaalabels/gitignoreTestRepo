  <h3 >Applications and use </h3>
  <p>The product is used in a variety of labelling applications, when the advantages of digital printing: short runs, last minute changes, personalised layouts and fast turnaround full colour printing are required. </p>
  
  <ul>
  	<li>Quick drying and enhanced moisture resistance offer good handleability after printing. </li>
  	<li>Glossy premium face paper for prestigious labels (gifts, cosmetics, special editions). </li>
    <li>Good barcode readability, high opacity, very good dot definition, excellent black printing quality. </li>
  </ul>
  <p> The hygroflat backing paper is generally suited to the re-fan folding demands of high-speed computer printers, providing optimum lay flatness and excellent interfolding characteristics.  </p>
  
  
  <h3 >Approvals</h3>
  <p> The adhesive complies with the European food directives and legislations, FDA 175.105 and the German recommendations. XIV as published by BfR. BfR (Bundesinstitut für Risikobewertung) is the German Federal Institute for Risk Assesment. The adhesive side may stand in direct contact with dry, moist and such fatty foodstuffs which have a correction factor of at least 3 according to the Regulation (EU) No 10/2011.</p>
