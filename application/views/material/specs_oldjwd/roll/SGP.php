                          <h3 >Applications and use </h3>
                          <p>This  product is suitable for a wide range of commercial chilled and frozen goods  storage and transportation, laboratory and home usage. The label can be used  for product branding and promotional purposes, corporate identity and informational  uses, such as batch coding and bin/pallet location labels </p>
<h3 >Approvals</h3>
                          <p>This  label can be used safely for food packaging and can come into direct contact  with both dry and fatty foodstuffs (ISEGA Research  &amp; Development Institute, Aschaffenburg).</p>
