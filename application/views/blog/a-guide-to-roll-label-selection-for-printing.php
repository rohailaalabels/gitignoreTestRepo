    <h2 class="headingPost">LABEL SENSORS</h2>
    <p>There are two main types of sensors currently used in roll label printers to detect the beginning and end
      of the labels. Which are optical or photoelectrical sensors able to detect changes in opacity and/or
      contrast and capacitive or ultrasonic sensors that detect changes in thickness between the label and the
      roll backing.</p>
    <h3 class="headingPost">Optical/Photoelectrical Sensors</h3>
    <p>Optical or photoelectrical sensors are very versatile non-contact sensing devices. The reliability of
      photoelectric “eyes” or “sensors” took a giant leap forward in the early 1970s when the light emitting
      diode (LED) replaced the fragile incandescent light source.</p>
    <p>This solid-state light source also enables the elimination of most problems previously caused by ambient
      room light. Modern pulse modulated photoelectric sensors respond only to the light emitted by their own
      light source.<br>
      This capability allows the sensor to be very sensitive and responsive to small light changes that occur
      to the light beam path between the light source lens and the receiving lens.<br>
      For an object to be detected, it must affect the intensity of the light beam reaching the sensor’s light
      detector in one of two ways:</p>
    <p>The object must break or diminish an existing light beam path between the light source lens and receiver
      lens — <strong>Beam Break</strong> mode.<br>
      <img class="img-responsive aligncenter size-full wp-image-1833"
                     src="<?=BLOG_ASSETS?>beam1.png" alt="" width="266" height="124"><br>
      The object itself must diffuse or reflect the light beam to the receiving lens — <strong>Beam
      Make</strong> mode.<br>
      <img class="img-responsive aligncenter size-full wp-image-1834"
                     src="<?=BLOG_ASSETS?>beam2.png" alt="" width="264" height="109"></p>
    <h4 class="blackheading">Gap Sensors</h4>
    <p>Gap sensors are an excellent choice to sense a distinguishing change or object on a continuous web or
      roll of label materials. By using opacity mode sensing techniques that utilise a light source capable of
      penetrating through the web of materials, gap sensors can register slight contrasting light level
      changes and produce an output signal for a specific machine operation.<br>
      <img class="img-responsive aligncenter size-full wp-image-1836"
                     src="<?=BLOG_ASSETS?>gap.png" alt="" width="673" height="289"
                     srcset="<?=BLOG_ASSETS?>gap.png 673w, <?=BLOG_ASSETS?>gap-300x129.png 300w"
                     sizes="(max-width: 673px) 100vw, 673px"><br>
      A significant advantage of opacity mode sensing with gap sensors is that end users do not need to be
      concerned with web flutter or ambient light, including strobe flashes. Depending on what you are trying
      to sense, opacity mode sensors can give you the desired output signal to perform machine functions.
      Typical applications include sensing labels on a roll, sensing registration marks on printed packaging
      material, and splice detection on continuous webs of material.</p>
    <p>Since opacity mode sensing distinguishes the light level change as light passes through a web of
      material, the web must be translucent or transparent. Most webs of packaging material such as film,
      metallized films, and paper do allow light to pass through. Opaque materials such as aluminium foil does
      not allow light to pass through and, therefore, cannot be used in opacity mode sensing.<br>
      The different sensing options involved are all optical. They can either detect a gap, a hole or a
      black-block line that is printed for the sole purpose of sensing. These are required to trigger the
      start of each label and printing within the set print area of the label. If these are not correctly set
      it will cause printer error or incorrect print placement.</p>
    <h4 class="blackheading">Label Gap</h4>
    <p>Between each die cut label, the waste material is typically stripped away leaving behind what is called a
      “label gap.” The carrier or liner supporting the label is usually less opaque than the label and liner
      together. Special transmissive sensors read the opacity changes where one label ends and a new label
      begins. Label gaps are commonly 2mm – 3mm but can also vary due to automatic label application equipment
      and additions such as perforations between labels.<br>
      If there is not enough contrast between a label and its liner, then either not enough light will pass
      through for the printer to find its “top-of-form” or in the case of transparent labels too much light
      may pass through the label. Sometimes you can recalibrate the sensors to adjust to a new liner;
      otherwise you will have to choose another way to detect the labels.</p>
    <h4 class="blackheading">Slot or Notch</h4>
    <p>To create more contrast, you may need slots or side notches cut in the liner. Before deciding whether to
      use slots or notches, you will need to determine whether your sensors are “fixed” and locked into one
      position in your printer or if they are “movable.” Fixed sensors are commonly located either to read the
      very edge of a label roll or in the centre. Movable sensors can be located anywhere across the web to
      give you much more flexibility as to where you can locate your cut outs.<br>
      <img class="img-responsive aligncenter size-full wp-image-1837"
                     src="<?=BLOG_ASSETS?>fig.png" alt="" width="783" height="375"
                     srcset="<?=BLOG_ASSETS?>fig.png 783w, <?=BLOG_ASSETS?>fig-300x144.png 300w, <?=BLOG_ASSETS?>fig-768x368.png 768w"
                     sizes="(max-width: 783px) 100vw, 783px"></p>
    <p>Sensing slots are narrow oval holes often centred in the liner between labels as shown in Fig. 01 above
      and usually require larger-than-normal label gaps. Because trying to cut and remove a tiny slot from a
      normal 2mm – 3mm gap is difficult, especially if you also have perforations between labels.</p>
    <p>Sensing notches are cut out at the edge of the liner as shown in Fig. 02 above and may not require a
      larger gap. Not only are notches used with labels on brown or highly opaque liners, but also when
      working with tag materials that have no liner or label gap. This explains why you often see hang tags
      with perforations between each tag and four rounded corners that create those notches.</p>
    <p>The size, shape and location of a sensing slot or notch is dependent on your printer specifications.
      Slots and notches typically require special cutting tools. Subject to the size of your label gap, you
      may also need a new label tool which adds to your initial investment but if this is your printer sensor
      requirement, it will make your label printing much easier.</p>
    <h4 class="blackheading">Black-Block Mark</h4>
    <p>Some label printers may also have a “reflective” sensor that relies on black-block registration lines or
      marks printed on the back of the label liner. The width of the black mark is synchronized with the label
      size to tell the printer when to print label to label. Black marks are particularly useful when labels
      or tags are “butt cut” and separated only by a perforation or face cut. Since we will have to print the
      black marks for you, this sensor method does add to the cost of supply.<br>
