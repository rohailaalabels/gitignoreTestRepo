  <p>If you have your heart set on business cards or packaging with vivid colours then you will need a graphic
    designer to set the artwork using the Pantone colour system. Professional printing using this system is
    much more expensive in low run jobs, but it’s the best way to get the exact colours you are after,
    choosing from a range of swatches that include neons.</p>
  <p>So now you know, there is more to colours than meets the eye! And with each printer varying slightly and
    each computer monitor looking different, you may now understand why graphic designers can pull their
    hair out wrestling with colours.</p>
  <p>&nbsp;</p>
  <h4>Size</h4>
  <p>An image setting that is much easier to control is size. There are two parts to this; dimensions and
    resolution, and again on-screen images must be treated differently to printed. Pictures destined for
    professional print (which you now know should be in CMYK) typically need to be 300dpi (dots per inch)
    whereas those of the same overall dimensions but intended for on-screen use, only need to be around
    100dpi. This is because computer screens have a lower resolution (or number of dots per inch) than
    professional printers.</p>
