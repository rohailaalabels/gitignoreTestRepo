                        <h2 class="BlueHeading">LABEL LINERS/BACKING PAPER</h2>
                        <p class="text-justify">
                            While paper labels and water-borne acrylic adhesive are biodegradable, previously and
                            traditionally the layer of Silicone used as a release liner on the liner/backing paper to
                            prevent the label adhesive from curing and making the labels impossible to remove, has not
                            been. Which has meant that the backing sheets or rolls remaining once the labels have been
                            applied have been sent for incineration or landfill.</p>
                        <p class="text-justify">However, manufacturers of label materials are now dispensing with
                            the need for a Silicone release liner by polishing the surface of the backing paper/liner
                            through a process called “Calendering” or “Super Calendering” making the paper impervious to
                            the pressure sensitive adhesive on the label and keeping it in an inert condition until
                            removed and applied.</p>
                    </div>


                    <div class="m-t-15 col-xs-12 col-sm-12 col-md-12 ">
                        <h2 class="BlueHeading">Calendering & Super-Calendering of Paper</h2>
                        <p class="text-justify">In paper manufacturing, calendering is the process of smoothing the
                            surface of the paper by pressing it between hard pressure cylinders or rollers, “the
                            calendars” at the end of the papermaking process. Calendering is a standard part of the
                            papermaking process and occurs in-line as the paper is being manufactured. It is usually the
                            last step of the process before the paper is cut to standard sizes. </p>
                        <p class="text-justify">When an additional set of offline calendars called super-calenders
                            are used after the initial papermaking process but before the paper is trimmed to size, they
                            produce an even smoother and glossier paper called super-calendered paper or SC paper. The
                            super-calender consists of several cylinders alternating between polished metal and soft
                            resilient surfaces. The super-calender runs at high speed and applies pressure, heat, and
                            friction to glaze both surfaces of the paper, making them smooth and glossy.</p>
                    </div>


