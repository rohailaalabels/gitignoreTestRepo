    <p>To me, the sad face was too depressing and because of the red, it looked angry and sad! What do you
      think? I could have changed the faces and colours so that it wouldn’t have this effect but it might have
      diluted the message. In any case, this concept is probably a bit over-used. So I needed to get back to
      the drawing board!</p>
    <p>After quizzing a teacher about it, we came up with five concepts. Here’s a photo of my initial scribbles
      showing a very rough idea of four of the concepts:</p>
