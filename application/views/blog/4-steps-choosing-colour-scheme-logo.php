    <h2>3. Check that these colours have been accepted to represent your product category by analysing
      competitor products.</h2>
    <p>To make sure you aren’t confusing the customer as to what the product is for, do some competitor
      research. You don’t want your brand and packaging to look the same as your competitors otherwise you
      won’t stand out or be any different, however if your choice of colour palette is too obscure for that
      category people will not be able to tell what your product is for at first glance.</p>
    <p>If we were to use the organic skincare range as an example, we might discover that our above list of
      colours are widely accepted as communicating the correct message, except if we were to use an excessive
      amount of black and white, it may not be obvious enough that the products are indeed organic and
      natural.</p>
