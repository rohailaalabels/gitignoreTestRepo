  <h3 >Adhesive</h3>
  <p>An aqueous compounded acrylic emulsion (PSA),  pressure sensitive, permanent adhesive. This adhesive has been formulated for  label, paper and filmic applications where a permanent bond is required and is  generally suitable for application to a number of surfaces, including recycled  carton-board and can also be used in chill cabinet applications.</p>
  <p>A block-out version designed to obscure text on the  substrate to which the label is applied is also available with a vellum  face-stock if required and minimum quantities and prices are available on  application.</p>
  <h3 >Release Liner</h3>
  <p>A one side coated solvent-less siliconised glassine  with release levels designed for high-speed rotary label conversion. The paper  has excellent punchability and is therefore suitable for sprocket fed  fan-folded products, as well as labels on rolls for automatic applications.</p>
