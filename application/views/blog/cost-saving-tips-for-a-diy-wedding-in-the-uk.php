          <h2 class="headingPost">The dress</h2>
          <p>As much as we all dream of that fairy-tale dress, it doesn’t have to cost a fortune. Plenty of
            high street stores sell their own wedding dresses at a fraction of the cost of dedicated
            dressmakers – Monsoon for example have a lovely range for under £200. Another option is to look
            at charity shops, such as Oxfam, who often sell second-hand dresses that have only been worn
            once.</p>
        </div>
      </div>
      <div class="clear10"></div>
      <p>You can always keep an eye on the sales as well to pick up a bargain. Find out the dates of sample and
        designer sales; you could make savings of up to 70% on dresses, veils, shoes and headpieces.</p>
