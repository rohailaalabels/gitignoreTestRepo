    <div class="col-xs-8">
      <p>Speaking of which, you absolutely must find a planner that works for you, so spend the time
        researching your options. Some teachers are happy to have one planner for everything in their
        lives, whilst others may find that having a separate planner for work, home and family works
        best.</p>
      <p></p>
    </div>
  </div>
  <div class="clear10"></div>
  <p>It’s also important to keep track of all your students’ grades so you can see at a glance who are doing
    well, improving, or who might need that extra little bit of attention. A separate book, form or folder
    is the easiest way to do this, and it helps to have it labelled as bold and as clear as possible to help
    your kids to have easy access to their own records.</p>
  <p>In terms of keeping work tidy in the classroom, it’s wise to invest in both a thermal laminator, as well
    as a decent paper trimmer. This ensures that important pages are protected and can be reused, and when
    it comes to cutting out thirty worksheets you can get it done quickly and neatly.</p>
  <div class="clear10"></div>
  <h2 class="headingPost">It’s not all work, work, and more work</h2>
  <p><img src="<?=BLOG_ASSETS?>cr-7.jpg" class="img-responsive col-xs-12"></p>
  <p>Any primary school teacher will tell you that the satisfaction of the job comes from seeing kids grow and
    develop, and how they push themselves both with their work and their social skills. It can be a real
    slog in terms of the academic pressures and sheer amount of work that goes into it, but that’s not to
    say that there isn’t a lot of fun to be had along the way.</p>
  <p>Reading materials such as books, magazines and newspapers are all essentials for the classroom. They can
    provide encouragement for your kids to spend free time reading during their free time instead of staring
    into space. Magazines and papers can also be used for a ton of classroom activities, from paper mache to
    collages and posters, just be sure to label any books so that they don’t end up being used in the same
    way! Books can be sourced from absolutely anywhere, and it’s a nice idea to encourage the children to
    read about topics such as friendship, school, family and rules.</p>
  <p>Show your students how impressed you are with their work by dedicating a section of your wall or board
    with a showcase space. You could treat it as a Wall of Fame or museum of work, so that you can
    congratulate children with <a href="#">star shaped stickers</a> for outstanding work, or even create
    awards for things like being a good listener, most improved, best behaved, and much more.</p>
