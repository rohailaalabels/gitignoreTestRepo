          <h2 class="headingPost">Food and drink</h2>
          <p>To save money on the food, you could choose to not have a sit-down meal, and instead look at
            alternatives such as hog roasts and barbeques or a baked potato bar with all the trimmings. You
            could even incorporate it in to an overall theme for a ‘country-chic’ wedding.</p>
          <p>Don’t feel like you have to have a wedding cake either. This is often an overly expensive option
            that has room for inventive alternatives such as mini pies, a cookie and milk station, and
            muffins or cupcakes.</p>
          <p>The same goes for the booze. You don’t have to offer endless alcohol options. Beer and wine is
            fine, and if you buy in bulk, it helps to lessen the cost.</p>
        </div>
      </div>
      <h2 class="headingPost">Enlist your friends and loved ones to help</h2>
      <div class="row">
        <div class="col-xs-6 col-lg-4 col-sm-4 pull-right"><img class="img-responsive col-xs-12 pull-right"
