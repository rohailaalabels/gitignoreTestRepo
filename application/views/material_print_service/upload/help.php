            <div id="office" class="titleOrange">Microsoft Office (Word, Excel, PowerPoint, Publisher) </div>
            <p><img onerror='imgError(this);' class="helpImage-right img-responsive" src="<?=Assets?>images/help/type/word-office.jpg" alt="MS office Word"> Microsoft Office is the most common suite of programs used for generating documents for printing, simply because it is available to most PC users. However it is not designed automatically to generate documents which are suitable for printing on press (despite what Publisher devotees might tell you), and most documents need to be manipulated or converted into a suitable format before they can be printed. In most cases, AA Labels will be able to undertake this work for you. It will make our job easier, however, if you note the following points before submitting Office documents for printing: <br>
              <br>
              <strong>1.	Check Page Setup</strong><br>
              This may seem obvious, but make sure your document is set up at a standard UK page size before you begin. This is usually a standard 'A' size like A4 or A3. Depending on your application preferences, Word documents can default to US letter size which can cause problems when the document is printed on A4 label sheets.&#13;</p>
            <p> PowerPoint's default page size is 	screen size because it is designed 	to be used for on-screen presentations, so if you are setting up a document for printing using PowerPoint you will need to also change this to  the sheet label page size that you want printed e.g. A4/A3.&#13;</p>
            <p><br>
              <br>
              <strong>2. Use Line/Page Breaks</strong><br>
              This is particularly important for long documents, ingredients labels 	or large informational labels set up in Word which continuously 	reformats documents automatically, which can cause text to reflow from one section of text to another, particularly if you transport the file between 	different computers or different versions of Word. This can result in problems like the sections reformatting.<br>
              <br>
              <strong>3. Use Standard Fonts</strong> <br>
              It is always sensible to stick to standard fonts like Arial, Calibri, Bradley Hand, 	Times New Roman and other popular fonts, as these are installed on virtually every PC – 	so whichever PC you open your file on, the fonts will always appear the way you are expecting. PCs can only display and print fonts which are installed on their hard drive, so if you use a special or 	unusual font, and we don't have it installed on our computers, we will advise you of the cost of acquiring that font. Otherwise we will not be able to replicate it correctly. However our studio library of fonts is reasonably comprehensive, but 	new fonts are regularly being created. <br>
              <br>
              <strong>4. Avoid using Web Graphics or Clipart</strong><br>
              It is highly advisable to avoid using any graphics, logos or clipart images which you have downloaded from the internet, or photos saved from a web page, unless you are certain that they are sufficiently high-resolution to print cleanly. Screen-grabs or pictures, cut and pasted direct from web pages look fine on screen, but will look very poor when printed and should be avoided wherever possible. This is particularly important if you are increasing the size significantly. The more you enlarge a graphic, the worse it's going to look when it's printed. For more detailed guidance on using digital images, refer back to the previous section Printing Digital Images. </p>
            <div class="clear"></div>
            <br>
            <div id="pdf" class="titleOrange">PDF (Portable Document Format) and other graphics file formats. </div>
            <p><img onerror='imgError(this);' class="helpImage-type2" src="<?=Assets?>images/help/type/pdf.jpg" alt="PDF"> PDF is now the industry standard method for submitting artwork for printing, because it generates smaller (i.e. portable) files and, when used correctly, it ensures that all graphics and fonts are properly embedded so that they will print correctly no matter what computer you print them from. Nowadays many programs have a PDF writer built in, but this is not always the case. Where this is not the case, you will need to have access to software which allows you to create PDF files – from Word, Powerpoint, or whatever program you have used to set up your document. The most well known is Adobe Acrobat. Most PCs have Acrobat Reader installed on them, but this does not allow you to generate PDF files – only read them. The full version of the program is required to generate PDF files, and it isn't free! There are, however, some PDF writing programs available on the web which can be downloaded and installed for free, for example Cute PDF. Most work like a printer driver: instead of printing to your desktop printer or network printer, you select PDF as your destination printer, and then choose your formatting options from the print dialogue box. PDF is a great tool, but it is not failsafe – it will only work properly if used correctly. The following points should be noted when generating PDF files: <br>
              <br>
              Choose the right quality setting. Most PDF writers will give you some kind of choice as to what level of quality you want your PDF to be. This will either be in the form of a description (e.g. "low quality", 
              "standard", "press quality"), or output resolution (e.g. 72 dpi, 300 dpi, 1200 dpi), or both. As a rule of thumb, it is usually best to choose the highest available quality setting, as it's safest to make your PDF better quality than you need. Look for "print quality", "press quality" or "high quality" and 300 dpi resolution or more. "Low quality" or "screen quality" or resolution between 72 dpi and 150 dpi will not usually yield good results and should therefore be avoided. </p>
            <br>
            <div class="well orangeBg">
              <p><img onerror='imgError(this);' class="helpImage-type3 img-responsive" src="<?=Assets?>images/help/thumb_pin.png" alt=""><strong>Even if you've selected "press quality" and 1200dpi, if the document you're printing contains low resolution graphics (e.g. screen-grabs or web graphics), the resulting PDF will not necessarily be print quality – converting to PDF won't improve the quality of the graphics you've used in the first place! </strong></p>
            </div>
            <br>
            <p> <img onerror='imgError(this);' class="helpImage-type2" src="<?=Assets?>images/help/type/fonts.jpg" alt="Fonts"> Always embed your fonts As with Microsoft Office, if our PC doesn't have the fonts you have used in your document installed on it, it will substitute them for something else – which can have unexpected and unwanted results. However, you can choose to embed all the fonts you have used in your PDF when you create it. If you do this, it won't matter whether we have the fonts you have used or not – the PDF will contain enough information to make sure that your fonts print out correctly. Sub-heading and new  paragraph.  Many programs allow you to export your document as a graphics file, which, like PDF, will allow you to embed all the fonts and graphics in a single file. There are many different graphics file formats, the most common being JPEG (.jpg), TIFF (.tif) and EPS (.eps). However, saving your document in these formats should be approached with caution. A lot of programs (the Microsoft Office suite, for example) will allow you to save your documents as Jpegs or Tiffs very easily, but do not give you much control as to the image quality of the resulting graphics file. In most cases, the resolution of the graphics file will default to 72 dpi – which is low quality, suitable for screen viewing but not for printing. This is a particular issue in PowerPoint and can cause big problems when PowerPoint is used to create artwork, particularly for posters. Another difficulty with turning your documents into graphics files is that the resulting files tend to be massive (in terms of memory) and very difficult to handle – they might be too large to send by email, for example. Because of this we do not recommend that graphics file formats other than PDF are used, except in conjunction with DTP software as appropriate. </p>
            <div class="clear"></div>
