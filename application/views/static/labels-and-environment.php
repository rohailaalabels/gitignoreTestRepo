                        <h2 class="BlueHeading" style="color: #17b1e3;font-size: 13px!important;margin-top: 10px;">What
                            is Wood-Free Paper?</h2>
                        <p class="text-justify">The term “wood-free” is often interspersed with “free-sheet” and is used
                            to describe 2 different paper types:</p>
                        <p>
                            The first is made from wood, but no longer contain wood, because the wood is chemically
                            treated to release lignin and produce a pulp that is nearly pure cellulose. Lignin is
                            essentially the glue that holds the cellulose fibres together in a tree. It is darker in
                            colour and gives paper opacity, but does not add strength and makes paper less white. By
                            contrast mechanical (or ground wood) papers are made by mechanically grinding the wood to
                            create pulp and in mechanical pulp the lignin remains and therefore the paper is not
                            wood-free.
                        </p>
                        <p>
                            Tree-free papers are made from plants like Kenaf, which is related to cotton and can produce
                            as much fibre in one year as pine would do in twenty. Although not widespread at the moment
                            paper making from non-tree sources is growing.
                        </p>
