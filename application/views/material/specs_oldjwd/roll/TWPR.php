                          <h3>Applications and use </h3>
                         
                         <p>This product combines the characteristics of thermal papers with the durability of Polypropylene films and printing speeds of up to 200mm per second are possible in direct thermal printing.</p>
                         <p>The adhesive features good tack and adhesion in combination with superior and clean removability from most substrates. Good UV resistance and long-term removability. The adhesive allows label application at a wide temperature range and it retains its removable properties even at very low temperatures.</p>
                         <p>A durable synthetic material with enhanced tear resistance and high sensitivity for use in a wide range of applications, including commercial tagging, wristbands, sport/game licensing, amusement pass, and ski lift tickets where short life or low contamination is common. The adhesive (R5000N) removes clean from many substrates from polymeric (e.g. PET, PP, ABS), card and fibre board, aluminium, stainless steel or glass (not suitable for window labelling). The use of R5000N adhesive on certain types of cardboards, or recycled fibres can be critical and it is strongly recommended to proceed to application testing before use.</p>
                         
                         
                          <h3>Conversion and printing</h3>
                         <p>This thermo-sensitive product is designed for use in thermal printing systems at printing speeds up to 200 mm/sec. The product can be converted by all conventional roll conversion technologies including flexographic and UV letter press. However, due to the thermo-graphic properties exposure above 50°C may cause premature imaging or discolouration. Inks containing alcohol or volatile organic solvents may cause discolouration of the thermo-sensitive coating. It is advisable to test inks and varnishes before conversion. </p>
                         
                        <h3>Approvals</h3>
                         <p>R 5009 complies with European food regulation 1935/2004/EC, with the German Recommendation (BfR) XIV and with FDA § 175.105. It also meets the demands of the limit values laid down in 10/2011/EU. In accordance with the requirements of relevant EU food regulations, the adhesive R5000N may safely stand in direct contact with dry, moist and fatty foodstuffs which have a reduction factor of at least 2 according to the Commission Regulation (EU) No 10/2011, with vegetable oil as fatty food simulant used for migration testing.</p>


                          <h3 >Shelf Life</h3>
                          <p>Two years under storage conditions as defined by FINAT <small>(Federation Internationale des Fabricants et Transformateurs d’ Adhesifs et Thermocllants sur papiers et autres supports)</small> 20° - 25° C and 40 – 50% RH. Dark storage is recommended and moisture, heat and direct sunlight also have to be avoided.</p>
