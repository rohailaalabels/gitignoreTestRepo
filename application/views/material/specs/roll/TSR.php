  <h3 >Applications and use </h3>
  <p> This direct thermal product is designed for use in retail item level applications where some resistance to oil, moisture, friction and heat is required alongside some resistance to short-term wet exposure. Longer-term contact with moisture, oils, fats, plasticisers and exposure to strong lighting and direct sunlight for prolonged periods should be avoided because of potential image fade issues.</p>
  
  <p> Typical end use applications include front of store labelling, process tracking, POS and item information labelling where there is a requirement for easy removal of the label. </p>
  
  <h3 >Conversion & Printing</h3>
  <p>This product is designed to be converted and dispensed at high speed by direct thermal and thermal transfer printers but can also be pre-printed before use by most conventional roll conversion techniques, including flexographic and UV letterpress. Due to the thermo-graphic properties exposure above 50° C may cause premature imaging or discolouration. Inks containing alcohol or volatile organic solvents may also cause discolouration. It is advisable to test inks and varnishes before conversion.</p>
  
  
  
  <h3 >Approvals</h3>
  <p> The research centre ISEGA has given the adhesive (Ref. RF-20) approval according to the European requirements for direct contact with dry and moist, non-fatty foodstuffs.</p>
  
  
  
  <h3 >Shelf Life</h3>
  <p> Two years under storage conditions as defined by FINAT (Federation Internationale des Fabricants et Transformateurs d’ Adhesifs et Thermocllants sur papiers et autres supports) 20° - 25° C and 40 – 50% RH.</p>
  
  
  
