      <h2 class="headingPost">LABEL FACE-STOCK &amp; BACKING MATERIAL – HOW IS THIS IMPORTANT?</h2>
      <p>Ink and toner will behave differently dependent upon the label material, for example the ink
        absorption and toner bonding to the surface will be different for an uncoated matt-white paper in
        comparison to a coated high-gloss paper or a transparent gloss Polymer surface. This will not
        materially impact the permanency of print providing good cartridges are used, but absorption in
        particular will influence the colour and for inkjet printers the drying time.</p>
      <p>Because dye and pigment inkjet cartridges use solutions the drying time on a coated material is
        likely to be longer than is practical on a desktop printer without a dryer. This is why all gloss
        and semi-gloss materials for inkjet printers require a chemical coating that reacts with the ink to
        dry it instantly.</p>
      <p>Some finishes of themselves might prove problematic in terms of toner bonding and ink absorption and
        this can vary between printers. The thicker the overall sheet and glossier the face-stock the more
        likely it is that the transport rollers might lose traction causing a slight misalignment of the
        sheet and print registration.</p>
      <p>Dependent on the backing paper and the heat generated in the printing process some Polymer label
        sheets may curl as a consequence but should lay-flat again if turned in the opposite direction and a
        weight applied on top. This can usually be avoided by following the duty cycle for the printer and
        not exceeding the recommended number of continuous sheets, before resting the printer. For some thin
        materials like Polypropylene on a glassine backing the heat generated by the fuser rollers in a
        laser printer will be too much and the material will melt and distort. For these reasons, it is
        always recommended that you trial sample materials before purchasing.</p>
      <h2 class="headingPost">PRINTER CARTRIDGES – WHAT IS THE IMPORTANCE OF THIS?</h2>
      <p>The ink or toner that you use in your printer will also influence the print quality and reproducible
        colour of your labels. OEM’s select toner materials and combinations of Polypropylene fumed silica
        and various other minerals for triboelectrification (the fusing and bonding of toner particles) that
        best suit their printer range. Similarly, OEM inks will use specific mixtures of pigments (fine
        powder suspended in liquid) and dyes (Colourants dissolved in liquid). Whereas remanufactured and
        compatible ink and toner cartridges use one utilitarian combination to suit all printer brands, with
        varying degrees of quality and reliability and may not reproduce the same quality and colour of
        print output as an OEM cartridge.</p>
      <p>Typically, remanufactured/recycled and compatible cartridges are generally cheaper to purchase than
        OEM cartridges, but as with labels you often get what you pay for. We are not suggesting that these
        types of cartridges are inferior, but you may find that the drying time is longer, increasing the
        chance of your label sheets smudging as they emerge from the printer.</p>
      <p>Also, some of these toners because they are a generic compound do not always fuse correctly and bond
        with the substrate leaving the image and text on labels prone to abrasion. Therefore, if you mix the
        types of cartridges that you use in your printer you may not be able to reproduce a consistent
        output of colour and quality on your labels.</p>
      <h2 class="headingPost">PRINTER MANUALS – ALWAYS CHECK THE OPTIONS</h2>
      <p>There are so many different printer models available, with varying levels of functionality and
        options for label printing. Therefore, it is important to ensure that you refer to your printer
        manual regarding instructions on how to print labels with your specific inkjet or laser printer
        model. If the hard copy manual is not available, you can usually find the user manual available on
        OEM’s websites and<br>
        there are often also customer support forums with advice on label printing.</p>
      <h2 class="headingPost">PRINTER CLEANING – IS THIS IMPORTANT?</h2>
      <p>It is very important to keep your printer clean to ensure trouble free printing. Paper and other
        materials that are used for printing are fibrous and shed fibres within the printer, which is
        deposited on subsequent unprinted sheets will leave a speckled effect on the printed labels when
        brushed off. A small dust-blower is an easy way to solve this however if your printer requires more
        attention in order to continue to function at top capacity then there are a few simple processes
        that will assist with this task.</p>
