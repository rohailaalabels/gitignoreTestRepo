  <p>Presuming you have carried out your market research and competitor analysis, and decided that your
    product will (hopefully!) be a success, then perhaps considering how you are planning to integrate your
    existing product design into your chosen markets is good place to visit next.</p>
  <p>When making this decision it is important to consider these factors amongst others:</p>
  <ul>
    <li>Never underestimate the&nbsp;power of your product and brand name, and make sure you test this
      thoroughly&nbsp;before you launch to make sure it works in your target market/s.&nbsp; An
      embarrassing example given by <a
                            href="https://money.cnn.com/magazines/business2/business2_archive/2004/08/01/377394/index.htm"
                            target="_blank">Mark Lasswell</a> was that of the Ikea workbench called ‘Fartfull’ and their
      computer table named ‘Jerker.’ Ok, so unless you speak the estimated 7,000&nbsp;languages in the
      world today then you can never be 100% sure of zero embarrassment, but some basic translation
      research on your product name&nbsp;may be useful! You don’t want to insult your customers and new
      market&nbsp;before you’ve started selling to them. </li>
    <li>Your product may need codes&nbsp;or symbols on its outer packaging, such as specific shipping codes
      for transportation purposes. Your international freight handler should be able&nbsp;to help advise
      on the necessary items required for shipping. </li>
  </ul>
