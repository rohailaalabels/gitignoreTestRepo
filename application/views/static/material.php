                                                <p>At AA Labels we choose from an extensive selection of label materials
                                                    to provide the best solution for your label requirements, which
                                                    includes everything from standard to high quality finish papers to
                                                    films and vinyl, in a range of colours, opacity and textures. </p>
                                                <strong>Paper face materials</strong>
                                                <ul>
                                                    <li> Uncoated paper</li>
                                                    <li> Direct thermal paper</li>
                                                    <li> Thermal transfer paper</li>
                                                    <li> Matt, semi-gloss, gloss and high gloss coated papers</li>
                                                    <li>Textured papers</li>
                                                    <li>Coloured papers</li>
                                                    <li>Fluorescent colour papers</li>
                                                    <li>Paper labels with wet-strength capacity (splash proof and water
                                                        resistant)
                                                    </li>
                                                </ul>
                                                <strong>Polymer face materials</strong>
                                                <ul>
                                                    <li> Polyethylene Terephthalate (PET)</li>
                                                    <li> Polypropylene (PP)</li>
                                                    <li> Polyester (PE)</li>
                                                </ul>
                                                <ul class="list-unstyled">
                                                    <li><strong>PET </strong> Polyethylene films are characterised by
                                                        being a hard, stiff and strong dimensionally stable material
                                                        that absorbs very little water. It has good chemical resistance,
                                                        except to alkalis (which hydrolyse the material). In very thin
                                                        formats it can be highly transparent and colourless, but thicker
                                                        label sections are usually opaque and off-white. This material
                                                        is a good choice when resistance to water, oil or chemicals is
                                                        important. Because of its thickness it is best used on flat
                                                        surfaces.
                                                    </li>
                                                </ul>
                                                <strong>There are two broad categories of polyethylene used as a label
                                                    substrate:</strong> <br/>
                                                <br/>
                                                <ul>
                                                    <li>Low Density Polyethylene (LDPE): has low stiffness, is tear
                                                        resistant, and issued often for squeezable containers, including
                                                        those with irregular shapes such as cartons. LDPE also can be
                                                        made very clear, but is also manufactured to be super white,
                                                        bright glossy white, transparent and metallised which gives a
                                                        glossy metal look to products.
                                                    </li>
                                                    <li>High Density Polyethylene (HDPE): HDPE is much stiffer, and
                                                        tends to be milky or frosted looking in appearance. It is more
                                                        puncture resistant as well.
                                                    </li>
                                                </ul>
                                                <strong>Uses for LDPE:</strong>
                                                <ul>
                                                    <li>Cosmetics</li>
                                                    <li>Consumer disposable products</li>
                                                    <li>Medical products</li>
                                                    <li>Health & beauty aids (HBA)</li>
                                                    <li>Food</li>
                                                    <li>Squeezable bottles & cartons</li>
                                                    <li>Any product which needs a high level of conformability around an
                                                        irregular surface including curved surfaces, where avoidance of
                                                        wrinkling is important.
                                                    </li>
                                                </ul>
                                                <strong>Uses for HDPE:</strong>
                                                <ul>
                                                    <li>Drum labels</li>
                                                    <li>Transportation labels</li>
                                                    <li>Household cleaners</li>
                                                </ul>
                                                <ul class="list-unstyled">
                                                    <li><strong>PP </strong> Polypropylene film face-stocks are derived
                                                        from petroleum using cost-effective high-yield polyolefin
                                                        resins. This film's face-stock has similar properties to
                                                        Polyethylene but is stronger, more rigid, and offers more
                                                        temperature resistance. Efficient to produce, Polypropylene, or
                                                        PP films, require less energy and fewer raw materials than most
                                                        other film substrates. Polypropylene films are oriented, or
                                                        stretched in one direction called a mono-axial film (OPP), or in
                                                        both directions called a biaxial-oriented film (BOPP). This
                                                        orienting or stretching of the film produces better optical
                                                        qualities, while providing stiffness and high tensile strength.
                                                        PP film provides good durability and dimensional stability;
                                                        additionally these films in clear formats provide high clarity
                                                        and are typically selected to achieve the "no-label" look.
                                                    </li>
                                                </ul>
                                                <strong>Advantages and benefits of using Polypropylene for your custom
                                                    label needs include:</strong>
                                                <ul>
                                                    <li>It is non-toxic and odourless</li>
                                                    <li>Light</li>
                                                    <li>Strong and tear resistant</li>
                                                    <li>Provides a good moisture barrier</li>
                                                    <li>Provides uniform flatness</li>
                                                    <li>Provides stiffness for thin films to enhance dispensability</li>
                                                    <li>Are an excellent substrate for even die-cutting</li>
                                                    <li>Accepts ink and toner well</li>
                                                    <li>Provides resistance to abrasion, & chemicals</li>
                                                    <li>Dimensionally stable</li>
                                                    <li>Excellent clarity</li>
                                                    <li>Low cost</li>
                                                    <li>Provides UV stability</li>
                                                    <li>Conformable</li>
                                                </ul>
                                                <ul class="list-unstyled">
                                                    <li><strong>PE </strong> Polyester is unaffected by moisture and
                                                        practically impermeable to odours, and Polyester base materials
                                                        are available in clear, white, metallised, and silver. Optically
                                                        clear over- laminates are also available. Polyester labels are
                                                        well-suited for outdoor applications where they may be exposed
                                                        to the elements. PET also comes in tamper-evident varieties,
                                                        which show a void or other pattern when the label is removed.
                                                        Films are often used in applications that require flexibility of
                                                        the label material e.g. soft plastic containers/dispensers such
                                                        as sauces, shampoo and shower gels.<br>
                                                    </li>
                                                </ul>
                                                <strong>Features and advantages of polyester custom labels or graphics
                                                    and decals include:</strong>
                                                <ul>
                                                    <li>High dimensional stability</li>
                                                    <li>Exceptional clarity</li>
                                                    <li>High tensile strength</li>
                                                    <li>Strong tear & abrasion resistance</li>
                                                    <li>Excellent resistance to solvents, oils & many chemicals</li>
                                                    <li>Thermal stability at high & low temperatures</li>
                                                    <li>Ease of printing & UV curing</li>
                                                </ul>
